    @staticmethod
    def 调整关卡波次配置(波次配置列表: List[波次配置], 难度: 难度类型) -> List[波次配置]:
        """
        调整关卡波次配置，使其符合要求
        
        Args:
            波次配置列表: 原始波次配置列表
            难度: 关卡难度
            
        Returns:
            List[波次配置]: 调整后的波次配置列表
        """
        # 获取关卡体验配置限制
        配置限制 = 关卡体验配置限制.get(难度)
        if not 配置限制:
            错误信息 = f"无效的难度类型: {难度}"
            打印警告(错误信息)
            return 波次配置列表
        
        # 获取boss可能出现波次
        boss可能出现波次 = 配置限制["boss可能出现波次"]
        # 不再需要转换为列表，因为现在配置中已经是列表了
        
        # 统计当前各种波次体验的数量
        当前波次体验数量 = {
            波次体验.轻松: 0,
            波次体验.一般: 0,
            波次体验.困难: 0,
            波次体验.地狱: 0,
            波次体验.Boss: 0
        }
        
        for 波次 in 波次配置列表:
            当前波次体验数量[波次.波次体验] += 1
        
        # 检查Boss总数是否超过3个
        boss总数 = sum(1 for 波次 in 波次配置列表 if any(组.是否Boss for 组 in 波次.怪物组列表))
        if boss总数 > 3:
            打印警告(f"Boss总数({boss总数})超过3个，需要调整")
            # 移除一些Boss
            boss波次 = [i for i, 波次 in enumerate(波次配置列表) if any(组.是否Boss for 组 in 波次.怪物组列表)]
            # 保留前3个Boss波次，移除其余Boss
            for i in boss波次[3:]:
                # 直接移除Boss组
                波次配置列表[i].怪物组列表 = [组 for 组 in 波次配置列表[i].怪物组列表 if not 组.是否Boss]
