"""
关卡数据生成器模块

本模块负责生成关卡数据，包括：
1. 生成单个关卡的随机配置
2. 批量生成关卡设计表中所有数据完整的关卡配置
3. 筛选关卡怪物列表，并传入波次生成逻辑
4. 数据完整性检查和容错处理

主要功能：
- 生成随机关卡配置：根据等级、阵容强度、难度等参数生成完整的关卡配置
- 生成关卡设计表数据：批量处理关卡设计表，跳过数据不完整的关卡
- 数据完整性检查：验证关卡数据的完整性和有效性
- 获取数据完整的关卡列表：预先检查哪些关卡可以正常生成

更新内容：
- 增强了数据完整性检查功能
- 添加了容错处理机制，避免因个别关卡数据不完整导致整个批量生成失败
- 提供了数据统计和报告功能，方便用户了解数据处理情况
"""
