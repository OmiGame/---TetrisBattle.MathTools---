"""生成关卡数据"""
from typing import List, Dict, Tuple
import random
from LevelData.LevelDataType import 关卡基础配置, 难度类型, 关卡体验配置限制
from WaveData.WaveDataType import 波次配置, 波次体验
from WaveData.WaveDataGenerator import 波次生成器
from LubanData import 全局参数,tables
# 导入文本颜色工具
from 文本颜色工具 import 文本颜色, 打印警告, 打印错误, 打印成功, 打印调试

关卡设计表列表 = tables.Tb关卡设计表.getDataList()

class 关卡生成器:
    """关卡生成器类，负责生成随机关卡配置"""
    
    @staticmethod
    def 生成随机关卡配置(关卡ID: int, 角色平均等级参考: int, 难度: 难度类型, 适配地图: str = "森林") -> 关卡基础配置:
        """
        生成随机关卡配置
        
        Args:
            关卡ID: 关卡编号
            角色平均等级参考: 参考角色平均等级
            难度: 关卡难度
            适配地图: 关卡地图类型，默认为"森林"
            
        Returns:
            关卡基础配置: 生成的关卡配置对象
        """
        # 检查参数范围
        if 角色平均等级参考 < 1 or 角色平均等级参考 > 全局参数.角色等级上限:
            错误信息 = f"角色平均等级参考应在1-{全局参数.角色等级上限}之间"
            打印警告(错误信息)
            raise ValueError(错误信息)
        
        # 生成波次配置列表
        波次配置列表 = 关卡生成器.生成关卡波次配置(角色平均等级参考, 难度, 适配地图)
        
        # 获取关卡体验配置限制
        配置限制 = 关卡体验配置限制.get(难度)
        
        # 生成关卡基础配置
        关卡配置 = 关卡基础配置(
            关卡ID=关卡ID,
            生成延迟时间=10.0,  # 可以根据需要调整
            难度类型=难度,
            我方城堡血量=配置限制["我方城堡血量"],
            敌方城堡血量=配置限制["敌方城堡血量"],
            限时生成时长=全局参数.关卡时长,
            角色平均等级参考=角色平均等级参考,
            波次配置=波次配置列表
        )
        
        return 关卡配置
    
    @staticmethod
    def 生成关卡波次配置(角色平均等级参考: int, 难度: 难度类型, 适配地图: str = "森林") -> List[波次配置]:
        """
        根据关卡体验配置限制生成关卡中的所有波次
        
        Args:
            角色平均等级参考: 参考角色平均等级
            难度: 关卡难度
            适配地图: 关卡地图类型，默认为"森林"
            
        Returns:
            List[波次配置]: 生成的波次配置列表
        """
        # 重置Boss计数
        波次生成器.重置Boss计数()
        
        # 计算最大波次数
        最大波次数 = int(全局参数.关卡时长 * 60 / 全局参数.怪物波次间隔)
        
        # 获取关卡体验配置限制
        配置限制 = 关卡体验配置限制.get(难度)
        if not 配置限制:
            错误信息 = f"无效的难度类型: {难度}"
            打印警告(错误信息)
            raise ValueError(错误信息)
        
        # 计算每种波次体验的数量范围
        波次体验数量 = {
            波次体验.轻松: random.randint(*配置限制[波次体验.轻松]),
            波次体验.一般: random.randint(*配置限制[波次体验.一般]),
            波次体验.困难: random.randint(*配置限制[波次体验.困难]),
            波次体验.地狱: random.randint(*配置限制[波次体验.地狱]),
            波次体验.Boss: random.randint(*配置限制[波次体验.Boss])
        }
        
        # 调用调整波次数量函数，确保总波次数等于最大波次数，并且每种波次体验的数量在配置限制范围内
        关卡生成器.调整波次数量(波次体验数量, 最大波次数, 配置限制)
        
        # 确定可能生成Boss的波次ID
        boss可能出现波次 = 配置限制["boss可能出现波次"]
        
        # 创建波次体验序列
        波次体验序列 = 关卡生成器.创建波次体验序列(波次体验数量, 最大波次数, boss可能出现波次)
        
        # 生成波次配置列表
        波次配置列表: List[波次配置] = []
        
        # 统计不合理波次的变量
        不合理波次数量 = 0
        不合理波次详情 = []
        # 记录每个波次重试次数的字典
        波次重试次数 = {}
        # 设置最大尝试次数
        最大尝试次数 = 10
        
        try:
            for 波次ID in range(1, 最大波次数 + 1):
                当前波次体验 = 波次体验序列[波次ID - 1]
                
                # 初始化当前波次的重试次数
                当前波次重试次数 = 0
                波次合理 = False
                
                # 尝试生成合理的波次，直到成功或达到最大尝试次数
                while not 波次合理 and 当前波次重试次数 < 最大尝试次数:
                    try:
                        波次 = 波次生成器.生成随机波次配置(
                            角色平均等级=角色平均等级参考,
                            波次ID=波次ID,
                            波次体验类型=当前波次体验,
                            适配地图=适配地图
                        )
                        
                        # 检查波次是否合理
                        波次合理 = 波次生成器.检查波次数据合理性(波次)
                        if not 波次合理:
                            当前波次重试次数 += 1
                            if 当前波次重试次数 >= 最大尝试次数:
                                不合理波次数量 += 1
                                从WaveDataType导入的波盈缺范围 = __import__('WaveData.WaveDataType', fromlist=['波盈缺范围']).波盈缺范围
                                最小值, 最大值 = 从WaveDataType导入的波盈缺范围[当前波次体验]
                                不合理波次详情.append({
                                    "波次ID": 波次ID,
                                    "波次体验": 当前波次体验.value,
                                    "波盈缺值": 波次.波盈缺换算成英雄,
                                    "合理范围": f"({最小值}, {最大值})",
                                    "重试次数": 当前波次重试次数
                                })
                                打印警告(f"波次{波次ID}达到最大尝试次数({最大尝试次数})，使用最后一次生成的波次")
                        else:
                            # 记录成功前的尝试次数
                            if 当前波次重试次数 > 0:
                                波次重试次数[波次ID] = 当前波次重试次数
                    except ValueError as e:
                        当前波次重试次数 += 1
                        if 当前波次重试次数 >= 最大尝试次数:
                            打印警告(f"波次{波次ID}生成失败并达到最大尝试次数: {e}")
                            # 尝试生成一个默认的轻松波次
                            try:
                                波次 = 波次生成器.生成随机波次配置(
                                    角色平均等级=角色平均等级参考,
                                    波次ID=波次ID,
                                    波次体验类型=波次体验.轻松,
                                    适配地图=适配地图
                                )
                                波次合理 = True  # 假设默认轻松波次是合理的
                            except Exception as e2:
                                打印错误(f"生成默认波次也失败: {e2}")
                                raise
                
                # 将生成的波次添加到列表中
                波次配置列表.append(波次)
        except Exception as e:
            打印错误(f"生成波次配置时发生未预期的错误: {e}")
        
        # 检查是否至少生成了一个波次配置
        if not 波次配置列表:
            打印错误("没有成功生成任何波次配置，关卡生成失败")
            raise ValueError("关卡生成失败：没有有效的波次配置")
        
        # 输出不合理波次的统计信息
        总波次数 = len(波次配置列表)
        print(f"波次生成统计:")
        print(f"总波次数: {总波次数}")
        print(f"不合理波次数量: {不合理波次数量} ({(不合理波次数量 / 总波次数 * 100):.2f}%)")
        
        # 输出重试次数统计
        if 波次重试次数:
            print(f"需要重试的波次数量: {len(波次重试次数)}")
            print("波次重试详情:")
            for 波次ID, 重试次数 in sorted(波次重试次数.items()):
                print(f"  波次ID: {波次ID}, 重试次数: {重试次数}")
        
        if 不合理波次详情:
            print("不合理波次详情:")
            for 详情 in 不合理波次详情:
                print(f"  波次ID: {详情['波次ID']}, 波次体验: {详情['波次体验']}, 波盈缺值: {详情['波盈缺值']:.3f}, 合理范围: {详情['合理范围']}, 重试次数: {详情['重试次数']}")
        
        return 波次配置列表
    
    @staticmethod
    def 调整波次数量(波次体验数量: Dict[波次体验, int], 最大波次数: int, 配置限制: Dict = None) -> None:
        """
        调整各种波次体验的数量，确保总和等于最大波次数，并且每种波次体验的数量在配置限制范围内
        
        Args:
            波次体验数量: 各种波次体验的数量字典
            最大波次数: 最大波次数
            配置限制: 关卡体验配置限制字典，包含每种波次体验的数量范围
        """
        总波次数 = sum(波次体验数量.values())
        
        if 总波次数 == 最大波次数:
            # 即使总波次数相等，也需要检查每种波次体验的数量是否在配置限制范围内
            if 配置限制:
                关卡生成器.检查并调整波次体验数量(波次体验数量, 配置限制)
            return
        
        # 如果总波次数小于最大波次数，增加一些波次
        if 总波次数 < 最大波次数:
            差值 = 最大波次数 - 总波次数
            # 优先增加轻松和一般波次
            可增加波次体验 = [波次体验.轻松, 波次体验.一般]
            while 差值 > 0 and 可增加波次体验:
                波次类型 = random.choice(可增加波次体验)
                波次体验数量[波次类型] += 1
                差值 -= 1
        
        # 如果总波次数大于最大波次数，减少一些波次
        elif 总波次数 > 最大波次数:
            差值 = 总波次数 - 最大波次数
            # 按优先级减少波次：先减少困难和一般波次，再减少轻松波次，最后减少地狱波次，尽量保留Boss波次
            减少优先级 = [波次体验.困难, 波次体验.一般, 波次体验.轻松, 波次体验.地狱, 波次体验.Boss]
            
            for 波次类型 in 减少优先级:
                while 差值 > 0 and 波次体验数量[波次类型] > 0:
                    波次体验数量[波次类型] -= 1
                    差值 -= 1
                    # 确保轻松波次至少有1个（用于第一波）
                    if 波次类型 == 波次体验.轻松 and 波次体验数量[波次类型] == 0:
                        波次体验数量[波次类型] = 1
                        差值 += 1  # 如果恢复了一个轻松波次，差值也要相应增加
                
                if 差值 == 0:
                    break
        
        # 调整后检查每种波次体验的数量是否在配置限制范围内
        if 配置限制:
            关卡生成器.检查并调整波次体验数量(波次体验数量, 配置限制)
    
    @staticmethod
    def 检查并调整波次体验数量(波次体验数量: Dict[波次体验, int], 配置限制: Dict) -> None:
        """
        检查并调整每种波次体验的数量，确保在配置限制范围内
        
        Args:
            波次体验数量: 各种波次体验的数量字典
            配置限制: 关卡体验配置限制字典，包含每种波次体验的数量范围
        """
        from 文本颜色工具 import 打印警告
        
        # 检查每种波次体验的数量是否在配置限制范围内
        需要调整 = False
        for 波次类型, 数量 in 波次体验数量.items():
            if 波次类型 in 配置限制:
                最小值, 最大值 = 配置限制[波次类型]
                if 数量 < 最小值:
                    打印警告(f"{波次类型.value}波次数量({数量})小于最小值({最小值})，将调整为{最小值}")
                    波次体验数量[波次类型] = 最小值
                    需要调整 = True
                elif 数量 > 最大值:
                    打印警告(f"{波次类型.value}波次数量({数量})大于最大值({最大值})，将调整为{最大值}")
                    波次体验数量[波次类型] = 最大值
                    需要调整 = True
        
        # 如果进行了调整，需要重新平衡总波次数
        if 需要调整:
            总波次数 = sum(波次体验数量.values())
            目标波次数 = int(全局参数.关卡时长 * 60 / 全局参数.怪物波次间隔)
            
            # 如果调整后总波次数不等于目标波次数，需要进一步调整
            if 总波次数 != 目标波次数:
                打印警告(f"调整后总波次数({总波次数})不等于目标波次数({目标波次数})，需要进一步调整")
                
                # 计算差值
                差值 = 目标波次数 - 总波次数
                
                # 如果差值为正，增加一些波次
                if 差值 > 0:
                    # 找出可以增加的波次类型（当前数量小于最大值的波次类型）
                    可增加波次类型 = []
                    for 波次类型 in [波次体验.轻松, 波次体验.一般]:
                        最小值, 最大值 = 配置限制[波次类型]
                        if 波次体验数量[波次类型] < 最大值:
                            可增加波次类型.append(波次类型)
                    
                    # 如果没有可以增加的波次类型，尝试增加困难波次
                    if not 可增加波次类型:
                        最小值, 最大值 = 配置限制[波次体验.困难]
                        if 波次体验数量[波次体验.困难] < 最大值:
                            可增加波次类型.append(波次体验.困难)
                    
                    # 增加波次
                    while 差值 > 0 and 可增加波次类型:
                        # 随机选择一种可增加的波次类型
                        波次类型 = random.choice(可增加波次类型)
                        最小值, 最大值 = 配置限制[波次类型]
                        
                        # 检查是否还可以增加
                        if 波次体验数量[波次类型] < 最大值:
                            波次体验数量[波次类型] += 1
                            差值 -= 1
                        else:
                            可增加波次类型.remove(波次类型)
                
                # 如果差值为负，减少一些波次
                elif 差值 < 0:
                    # 按优先级减少波次
                    减少优先级 = [波次体验.困难, 波次体验.一般, 波次体验.轻松, 波次体验.地狱, 波次体验.Boss]
                    
                    for 波次类型 in 减少优先级:
                        最小值, 最大值 = 配置限制[波次类型]
                        while 差值 < 0 and 波次体验数量[波次类型] > 最小值:
                            波次体验数量[波次类型] -= 1
                            差值 += 1
                            
                            # 确保轻松波次至少有1个（用于第一波）
                            if 波次类型 == 波次体验.轻松 and 波次体验数量[波次类型] < 1:
                                波次体验数量[波次类型] = 1
                                差值 -= 1
                        
                        if 差值 == 0:
                            break
    
    @staticmethod
    def 创建波次体验序列(波次体验数量: Dict[波次体验, int], 最大波次数: int, boss可能出现波次: List[int]) -> List[波次体验]:
        """
        创建波次体验序列，满足以下要求：
        1. 第一波必须是轻松波次
        2. 相邻两个波次体验尽可能不相同
        3. 困难、地狱和Boss波次尽量不相连
        4. 地狱和Boss波次只出现在指定的波次ID
        
        Args:
            波次体验数量: 各种波次体验的数量字典
            最大波次数: 最大波次数
            boss可能出现波次: Boss可能出现的波次ID列表
            
        Returns:
            List[波次体验]: 波次体验序列
        """
        # 创建一个空的波次体验序列
        波次体验序列 = [None] * 最大波次数
        
        # 第一波必须是轻松波次
        波次体验序列[0] = 波次体验.轻松
        波次体验数量[波次体验.轻松] -= 1
        
        # 检查Boss和地狱波次总数是否超过可用位置数量
        总特殊波次数 = 波次体验数量[波次体验.Boss] + 波次体验数量[波次体验.地狱]
        可用特殊位置 = [波次ID for 波次ID in boss可能出现波次 if 波次ID <= 最大波次数 and 波次ID > 1]
        可用特殊位置数 = len(可用特殊位置)
        
        # 调整特殊波次数量，确保不超过可用位置数
        if 总特殊波次数 > 可用特殊位置数:
            打印警告(f"Boss和地狱波次总数({总特殊波次数})超过可用位置数量({可用特殊位置数})，将调整波次数量")
            
            # 按优先级减少波次：先减少地狱波次，再减少Boss波次
            减少优先级 = [波次体验.地狱, 波次体验.Boss]
            差值 = 总特殊波次数 - 可用特殊位置数
            
            for 波次类型 in 减少优先级:
                while 差值 > 0 and 波次体验数量[波次类型] > 0:
                    波次体验数量[波次类型] -= 1
                    波次体验数量[波次体验.一般] += 1  # 将减少的特殊波次转为一般波次
                    差值 -= 1
                
                if 差值 == 0:
                    break
        
        for 波次ID in boss可能出现波次:
            if 波次ID <= 最大波次数 and 波次ID > 1:  # 确保波次ID在有效范围内且不是第一波
                # 随机决定是先安排Boss波次还是先安排地狱波次
                if random.random() < 0.5:
                    # 先安排Boss波次，再安排地狱波次
                    if 波次体验数量[波次体验.Boss] > 0:
                        波次体验序列[波次ID - 1] = 波次体验.Boss
                        波次体验数量[波次体验.Boss] -= 1
                    elif 波次体验数量[波次体验.地狱] > 0:
                        波次体验序列[波次ID - 1] = 波次体验.地狱
                        波次体验数量[波次体验.地狱] -= 1
                else:
                    # 先安排地狱波次，再安排Boss波次
                    if 波次体验数量[波次体验.地狱] > 0:
                        波次体验序列[波次ID - 1] = 波次体验.地狱
                        波次体验数量[波次体验.地狱] -= 1
                    elif 波次体验数量[波次体验.Boss] > 0:
                        波次体验序列[波次ID - 1] = 波次体验.Boss
                        波次体验数量[波次体验.Boss] -= 1
        
        # 安排困难波次，尽量不与地狱和Boss波次相邻
        可用位置 = [i for i in range(1, 最大波次数) if 波次体验序列[i] is None]
        for i in range(波次体验数量[波次体验.困难]):
            if not 可用位置:
                break
                
            # 找到一个适合困难波次的位置
            最佳位置 = None
            for 位置 in 可用位置:
                前一波 = 波次体验序列[位置 - 1] if 位置 > 0 else None
                后一波 = 波次体验序列[位置 + 1] if 位置 < 最大波次数 - 1 else None
                
                # 检查前后波次是否是困难、地狱或Boss波次
                if (前一波 not in [波次体验.困难, 波次体验.地狱, 波次体验.Boss] and
                    后一波 not in [波次体验.困难, 波次体验.地狱, 波次体验.Boss]):
                    最佳位置 = 位置
                    break
            
            # 如果没有找到最佳位置，就选择第一个可用位置
            if 最佳位置 is None and 可用位置:
                最佳位置 = 可用位置[0]
                
            if 最佳位置 is not None:
                波次体验序列[最佳位置] = 波次体验.困难
                可用位置.remove(最佳位置)
        
        # 安排剩余的一般和轻松波次
        可用位置 = [i for i in range(1, 最大波次数) if 波次体验序列[i] is None]
        剩余波次类型 = []
        
        # 添加剩余的一般波次
        for _ in range(波次体验数量[波次体验.一般]):
            剩余波次类型.append(波次体验.一般)
            
        # 添加剩余的轻松波次
        for _ in range(波次体验数量[波次体验.轻松]):
            剩余波次类型.append(波次体验.轻松)
            
        # 打乱剩余波次类型的顺序
        random.shuffle(剩余波次类型)
        
        # 填充剩余位置
        for i, 位置 in enumerate(可用位置):
            if i < len(剩余波次类型):
                波次体验序列[位置] = 剩余波次类型[i]
            else:
                # 如果剩余波次类型不够，用轻松波次填充
                波次体验序列[位置] = 波次体验.轻松
        
