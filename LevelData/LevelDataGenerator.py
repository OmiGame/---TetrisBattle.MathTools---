                可用位置 = [pos for pos in boss可能出现波次 if pos <= len(波次配置列表)]
                可用位置 = [pos for pos in 可用位置 if pos != 1]  # 排除第一波
                可用位置 = [pos for pos in 可用位置 if 波次配置列表[pos-1].波次体验 not in [波次体验.Boss, 波次体验.地狱]]
                
