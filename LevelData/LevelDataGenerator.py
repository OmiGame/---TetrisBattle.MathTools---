    @staticmethod
    def 检查关卡数据完整性(关卡) -> bool:
        """
        检查关卡数据是否完整
        
        Args:
            关卡: 关卡数据对象
            
        Returns:
            bool: 数据完整返回True，否则返回False
        """
        try:
            # ----
            # 1. 检查必要字段是否存在且有效
            # ----
            必要字段检查 = [
                (hasattr(关卡, '关卡编号') and 关卡.关卡编号 is not None, "关卡编号"),
                (hasattr(关卡, '关卡难度') and 关卡.关卡难度 is not None and 关卡.关卡难度.strip() != "", "关卡难度"),
                (hasattr(关卡, '绿色等级') and 关卡.绿色等级 is not None, "绿色等级"),
                (hasattr(关卡, '蓝色等级') and 关卡.蓝色等级 is not None, "蓝色等级"),
                (hasattr(关卡, '紫色等级') and 关卡.紫色等级 is not None, "紫色等级"),
                (hasattr(关卡, '阵容强度') and 关卡.阵容强度 is not None, "阵容强度"),
                (hasattr(关卡, '适配地图') and 关卡.适配地图 is not None and 关卡.适配地图.strip() != "", "适配地图")
            ]
            
            for 检查结果, 字段名 in 必要字段检查:
                if not 检查结果:
                    打印调试(f"关卡{关卡.关卡编号}缺少有效的{字段名}")
                    return False
            
            # ----
            # 2. 检查难度类型是否有效
            # ----
            try:
                难度类型[关卡.关卡难度]
            except KeyError:
                打印调试(f"关卡{关卡.关卡编号}的难度类型'{关卡.关卡难度}'无效")
                return False
            
            # ----
            # 3. 检查数值范围是否合理
            # ----
            if 关卡.绿色等级 < 1 or 关卡.绿色等级 > 全局参数.角色等级上限:
                打印调试(f"关卡{关卡.关卡编号}的绿色等级{关卡.绿色等级}超出有效范围")
                return False
                
            if 关卡.蓝色等级 < 1 or 关卡.蓝色等级 > 全局参数.角色等级上限:
                打印调试(f"关卡{关卡.关卡编号}的蓝色等级{关卡.蓝色等级}超出有效范围")
                return False
                
            if 关卡.紫色等级 < 1 or 关卡.紫色等级 > 全局参数.角色等级上限:
                打印调试(f"关卡{关卡.关卡编号}的紫色等级{关卡.紫色等级}超出有效范围")
                return False
                
            if 关卡.阵容强度 <= 0:
                打印调试(f"关卡{关卡.关卡编号}的阵容强度{关卡.阵容强度}必须大于0")
                return False
            
            return True
            
        except Exception as e:
            打印调试(f"检查关卡{关卡.关卡编号}数据完整性时发生异常: {str(e)}")
            return False

