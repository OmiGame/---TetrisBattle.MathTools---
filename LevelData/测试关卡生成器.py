"""测试关卡生成器"""
from LevelData.LevelDataGenerator import 关卡生成器
from LevelData.LevelDataType import 难度类型, 关卡体验配置限制
from WaveData.WaveDataType import 波次体验
from WaveData.WaveDataGenerator import 波次生成器
from 文本颜色工具 import 文本颜色, 打印警告, 打印错误, 打印成功, 打印调试

def 测试生成关卡波次配置():
    """测试生成关卡波次配置"""
    print(f"{文本颜色.青色}===== 测试生成关卡波次配置 ====={文本颜色.结束}")
    
    # 测试不同难度的关卡生成
    for 难度 in 难度类型:
        print(f"\n{文本颜色.青色}测试 {难度.value} 难度关卡生成:{文本颜色.结束}")
        
        try:
            # 重置Boss计数
            波次生成器.重置Boss计数()
            
            # 生成关卡波次配置 - 添加所有必要参数
            波次配置列表 = 关卡生成器.生成关卡波次配置(
                蓝色等级=10,
                绿色等级=12, 
                紫色等级=8,
                阵容强度=3.5,
                难度=难度,
                适配地图="森林"
            )
            
            # 统计波次体验类型分布
            波次体验计数 = {体验: 0 for 体验 in 波次体验}
            for i, 波次 in enumerate(波次配置列表):
                波次体验计数[波次.波次体验] += 1
                print(f"波次{i+1}: {波次.波次体验.value}")
            
            # 打印波次体验类型统计
            print(f"\n{文本颜色.青色}波次体验类型统计:{文本颜色.结束}")
            for 体验, 数量 in 波次体验计数.items():
                print(f"{体验.value}: {数量}个")
            
            # 检查第一波是否是轻松波次
            if 波次配置列表[0].波次体验 != 波次体验.轻松:
                打印错误("第一波不是轻松波次！")
            else:
                打印成功("第一波是轻松波次")
            
            # 检查相邻波次体验是否相同
            相邻相同 = False
            for i in range(1, len(波次配置列表)):
                if 波次配置列表[i].波次体验 == 波次配置列表[i-1].波次体验:
                    相邻相同 = True
                    打印警告(f"波次{i}和波次{i+1}的体验相同: {波次配置列表[i].波次体验.value}")
            
            if not 相邻相同:
                打印成功("相邻波次体验均不相同")
            
            # 检查困难、地狱和Boss波次是否相连
            高难度相连 = False
            高难度波次类型 = [波次体验.困难, 波次体验.地狱, 波次体验.Boss]
            for i in range(1, len(波次配置列表)):
                if (波次配置列表[i].波次体验 in 高难度波次类型 and
                    波次配置列表[i-1].波次体验 in 高难度波次类型):
                    高难度相连 = True
                    打印警告(f"波次{i}({波次配置列表[i-1].波次体验.value})和波次{i+1}({波次配置列表[i].波次体验.value})都是高难度波次")
            
            if not 高难度相连:
                打印成功("高难度波次不相连")
            
            # 检查Boss总数是否超过3个
            boss总数 = sum(1 for 波次 in 波次配置列表 if any(组.是否Boss for 组 in 波次.怪物组列表))
            print(f"Boss总数: {boss总数}")
            if boss总数 > 3:
                打印错误(f"Boss总数({boss总数})超过3个！")
            else:
                打印成功(f"Boss总数({boss总数})不超过3个")
            
            # 检查波次总数是否符合预期
            最大波次数 = int(波次配置列表[0].波次间隔时间 * 60 / 波次配置列表[0].波次间隔时间)
            if len(波次配置列表) != 最大波次数:
                打印警告(f"波次总数({len(波次配置列表)})不等于最大波次数({最大波次数})")
            
            # 检查波次体验数量是否符合配置限制
            配置限制 = 关卡体验配置限制.get(难度)
            print(f"\n{文本颜色.青色}波次体验数量与配置限制比较:{文本颜色.结束}")
            for 体验, 数量 in 波次体验计数.items():
                最小值, 最大值 = 配置限制[体验]
                是否符合 = 最小值 <= 数量 <= 最大值
                状态 = f"{文本颜色.绿色}符合" if 是否符合 else f"{文本颜色.红色}不符合"
                print(f"{体验.value}: {数量}个 (配置限制: {最小值}-{最大值}) {状态}{文本颜色.结束}")
            
            # 测试调整关卡波次配置
            print(f"\n{文本颜色.青色}测试调整关卡波次配置:{文本颜色.结束}")
            调整后波次配置列表 = 关卡生成器.调整关卡波次配置(波次配置列表, 难度)
            
            # 统计调整后的波次体验类型分布
            调整后波次体验计数 = {体验: 0 for 体验 in 波次体验}
            for i, 波次 in enumerate(调整后波次配置列表):
                调整后波次体验计数[波次.波次体验] += 1
            
            # 打印调整后的波次体验类型统计
            print(f"\n{文本颜色.青色}调整后波次体验类型统计:{文本颜色.结束}")
            for 体验, 数量 in 调整后波次体验计数.items():
                原数量 = 波次体验计数[体验]
                变化 = 数量 - 原数量
                变化文本 = f"({'+' if 变化 > 0 else ''}{变化})" if 变化 != 0 else ""
                print(f"{体验.value}: {数量}个 {变化文本}")
            
            # 检查调整后第一波是否是轻松波次
            if 调整后波次配置列表[0].波次体验 != 波次体验.轻松:
                打印错误("调整后第一波不是轻松波次！")
            else:
                打印成功("调整后第一波是轻松波次")
            
            # 检查调整后相邻波次体验是否相同
            调整后相邻相同 = False
            for i in range(1, len(调整后波次配置列表)):
                if 调整后波次配置列表[i].波次体验 == 调整后波次配置列表[i-1].波次体验:
                    调整后相邻相同 = True
                    打印警告(f"调整后波次{i}和波次{i+1}的体验相同: {调整后波次配置列表[i].波次体验.value}")
            
            if not 调整后相邻相同:
                打印成功("调整后相邻波次体验均不相同")
            
            # 检查调整后困难、地狱和Boss波次是否相连
            调整后高难度相连 = False
            for i in range(1, len(调整后波次配置列表)):
                if (调整后波次配置列表[i].波次体验 in 高难度波次类型 and
                    调整后波次配置列表[i-1].波次体验 in 高难度波次类型):
                    调整后高难度相连 = True
                    打印警告(f"调整后波次{i}({调整后波次配置列表[i-1].波次体验.value})和波次{i+1}({调整后波次配置列表[i].波次体验.value})都是高难度波次")
            
            if not 调整后高难度相连:
                打印成功("调整后高难度波次不相连")
            
            # 检查调整后Boss总数是否超过3个
            调整后boss总数 = sum(1 for 波次 in 调整后波次配置列表 if any(组.是否Boss for 组 in 波次.怪物组列表))
            print(f"调整后Boss总数: {调整后boss总数}")
            if 调整后boss总数 > 3:
                打印错误(f"调整后Boss总数({调整后boss总数})超过3个！")
            else:
                打印成功(f"调整后Boss总数({调整后boss总数})不超过3个")
            
        except Exception as e:
            打印错误(f"测试 {难度.value} 难度关卡生成时出错: {e}")
    
    print(f"\n{文本颜色.青色}===== 测试完成 ====={文本颜色.结束}")

