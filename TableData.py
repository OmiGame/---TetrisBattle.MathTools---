@dataclass
class 表格列定义:
    """定义表格列的元数据"""
    列名: str = None  # 必须参数，但给默认值None以解决初始化问题
    列函数: Callable = None  # 必须参数，但给默认值None以解决初始化问题
    参数映射: Dict[str, str] = None  # 必须参数，但给默认值None以解决初始化问题
    表头数据类型: str = "string"  # 默认为字符串类型
    excel公式字符串: str = None

    def __post_init__(self):
        """初始化后检查必须参数是否为None"""
        if self.列名 is None:
            raise ValueError("列名不能为None")
        if self.列函数 is None and self.excel公式字符串 is None:
            raise ValueError("列函数和excel公式字符串不能同时为None")
        if self.参数映射 is None:
            self.参数映射 = {}  # 如果没有提供参数映射，默认为空字典

    """定义表格列的计算方式， 如果有excel公式字符串，则优先使用excel公式计算，否则使用列函数计算，另外，列函数中不能引用excel的单元格值"""
    def 计算值(self, 实际参数: Dict[str, Any], 工作表: Worksheet = None, 当前行号: int = None, 
             列号映射: Dict[str, int] = None, 起始行号: int = None) -> Any:
        if self.excel公式字符串 and 工作表 and 当前行号 and 列号映射 and 起始行号:
            公式 = self.excel公式字符串
            for 列名, 列号 in 列号映射.items():
                列字母 = get_column_letter(列号)
                公式 = 公式.replace(f"{{{列名}}}", 列字母)
            
            # 获取Excel公式参数
            公式参数 = Excel公式参数(工作表, 起始行号).获取公式参数()
            try:
                公式 = 公式.format(行号=当前行号, **公式参数)
            except KeyError:
                pass
            return 公式
        return self.列函数(实际参数)

