    # 表格基本信息
    表格名称: str = "角色成长数据.xlsx"  # Excel文件名
    默认工作表名称: str = "某角色成长"          # 默认工作表名称
    
    # 行数据定义（实际数据的范围，不包括表头），一个行数据值，是一个数据的行索引值，用于标识每一行，当前值就是当前是数据的第几行
    # 决定表格有多少行数据（不包括表头）
    # 决定每行数据对应的"索引值"（比如等级值）
    # 这个"索引值"会作为参数传递给列函数，用于计算该行各列的实际显示值
    行数据类型: str = "等级"  # 行数据的类型，如"等级"、"波次"等
    行数据起始值: int = 1    # 行数据的起始值
    行数据结束值: int = 100  # 行数据的结束值
    
    # 特殊列函数中，需要的参数
    技能列表列号: int = 15   # 技能列表所在的列号，15是N列

