    @classmethod
    def 生成关卡难度列表(cls):
        """生成关卡难度列表
        
        根据难度类型和难度比例，生成一个拥有关卡数量个元素的难度列表。
        按照相邻两关的体验不能相同、很难和极难难度的关卡不相邻的规则来排序。
        
        Returns:
            List[str]: 包含关卡数量个难度类型的列表
        """
        import random
    
        
        # 计算各难度类型的比例
        总比例 = sum(比例.比例值 for 比例 in 难度比例)
        
        # 计算每种难度类型的关卡数量
        难度数量分配 = {}
        已分配关卡数 = 0
        
        # 修复：通过枚举名称来匹配难度类型和难度比例，而不是依赖位置
        for 难度类型枚举 in 难度类型:
            难度名称 = 难度类型枚举.name  # 获取枚举名称，如"简单"、"普通"等
            # 通过名称获取对应的比例枚举
            try:
                比例枚举 = 难度比例[难度名称]
                比例值 = 比例枚举.比例值  # 使用新的比例值属性
                分配数量 = int(cls.关卡数量 * 比例值 / 总比例)
                难度数量分配[难度类型枚举.value] = 分配数量
                已分配关卡数 += 分配数量
            except KeyError:
                # 如果找不到对应的比例枚举，跳过该难度类型
                print(f"警告：找不到难度类型 '{难度名称}' 对应的比例设置")
                continue
        
        # 处理由于整数除法导致的剩余关卡数
        剩余关卡数 = cls.关卡数量 - 已分配关卡数
        if 剩余关卡数 > 0:
            # 将剩余关卡分配给第一个难度类型
            第一个难度 = list(难度数量分配.keys())[0]
            难度数量分配[第一个难度] += 剩余关卡数
        
        # 创建初始难度列表
        难度列表 = []
        for 难度, 数量 in 难度数量分配.items():
            难度列表.extend([难度] * 数量)
        
        # 随机打乱列表
        random.shuffle(难度列表)
        
        # 应用排序规则：相邻两关体验不同，很难和极难不相邻
        def 检查并调整排序规则(列表):
            """检查并调整排序规则"""
            调整次数 = 0
            最大调整次数 = len(列表) * 10  # 防止无限循环
            
            while 调整次数 < 最大调整次数:
                需要调整 = False
                
                for i in range(len(列表) - 1):
                    当前 = 列表[i]
                    下一个 = 列表[i + 1]
                    
                    # 检查规则1：相邻两关体验不能相同
                    if 当前 == 下一个:
                        # 寻找不同的难度进行交换
                        for j in range(i + 2, len(列表)):
                            if 列表[j] != 当前:
                                # 检查交换后不会违反很难极难相邻规则
                                if not ((当前 == "很难" and 列表[j] == "极难") or 
                                       (当前 == "极难" and 列表[j] == "很难")):
                                    列表[i + 1], 列表[j] = 列表[j], 列表[i + 1]
                                    需要调整 = True
                                    break
                        if 需要调整:
                            break
                    
                    # 检查规则2：很难和极难不能相邻
                    if ((当前 == "很难" and 下一个 == "极难") or 
                        (当前 == "极难" and 下一个 == "很难")):
                        # 寻找其他难度进行交换
                        for j in range(i + 2, len(列表)):
                            if 列表[j] not in ["很难", "极难"] and 列表[j] != 当前:
                                列表[i + 1], 列表[j] = 列表[j], 列表[i + 1]
                                需要调整 = True
                                break
                        if 需要调整:
                            break
                
                if not 需要调整:
                    break
                    
                调整次数 += 1
            
            if 调整次数 >= 最大调整次数:
                print(f"警告：排序规则调整达到最大次数（{最大调整次数}），可能仍存在违规情况")
            
            return 列表
        
        # 应用排序规则
        难度列表 = 检查并调整排序规则(难度列表)
        
        return 难度列表
    
    @classmethod
    def 生成适配地图列表(cls):
        """生成适配地图列表
        
        根据地图类型生成一个与关卡数量对应的地图列表。
        每轮遍历所有地图类型后打乱顺序加入列表，循环直到达到关卡数量。
        
        Returns:
            List[str]: 包含关卡数量个地图类型的列表
        """
        import random
        
        # 获取所有地图类型的值
        所有地图类型值 = [地图.value for 地图 in 地图类型]
        
        地图列表 = []
        
        while len(地图列表) < cls.关卡数量:
            # 每轮：遍历所有地图类型，然后打乱顺序加入列表
            当前轮地图 = 所有地图类型值.copy()
            random.shuffle(当前轮地图)
            
            # 将打乱后的地图加入列表，但不超过总关卡数量
            for 地图类型值 in 当前轮地图:
                if len(地图列表) >= cls.关卡数量:
                    break
                地图列表.append(地图类型值)
        
        return 地图列表


