from openpyxl.worksheet.worksheet import Worksheet
from openpyxl.utils import get_column_letter

@dataclass
class 表格列定义:
    """定义表格列的元数据
    Attributes:
        列名: 列的中文名称
        列函数: 用于计算该列值的函数
        参数映射: 将通用参数映射到该列函数需要的具体参数名
        excel公式: 可选的Excel公式，如果提供则优先使用公式计算
    """
    列名: str
    列函数: Callable
    参数映射: Dict[str, str]  # 用于映射函数参数和实际数据
    excel公式字符串: str = None  # 可选的Excel公式

    def 计算值(self, 实际参数: Dict[str, Any], 工作表: Worksheet = None, 当前行号: int = None, 
             列号映射: Dict[str, int] = None, 起始行号: int = None) -> Any:
        """计算列的值，优先使用Excel公式，如果没有则使用列函数
        
        Args:
            实际参数: 计算所需的参数
            工作表: 当前工作表对象（用于Excel公式计算）
            当前行号: 当前处理的行号（用于Excel公式计算）
            列号映射: 列名到列号的映射字典
            起始行号: 数据起始行号（用于Excel公式计算）
            
        Returns:
            Any: 计算得到的值
        """
        if self.excel公式字符串 and 工作表 and 当前行号 and 列号映射 and 起始行号:
            # 替换列名引用为列字母
            公式 = self.excel公式字符串
            for 列名, 列号 in 列号映射.items():
                列字母 = get_column_letter(列号)
                公式 = 公式.replace(f"{{{列名}}}", 列字母)
            
            # 使用format方法替换行号，将数据按指定格式插入到字符串的占位符中
            try:
                公式 = 公式.format(行号=当前行号, 起始行号=起始行号)
            except KeyError:
                pass  # 如果公式中没有行号占位符，则忽略
                
            return 公式
        return self.列函数(实际参数)

