class 阵容强度计算:
    """模拟阵容强度计算供测试使用"""
    # 从阵容强度数据类引用数据
    角色品质 = 阵容强度数据.角色品质
    同等级的角色品质强度 = 阵容强度数据.同等级的角色品质强度
    角色品质成长倍率 = 阵容强度数据.角色品质成长倍率
    角色强度表 = {}
    排序后的角色 = []
    
    @classmethod
    def 计算角色强度(cls, 品质: str, 等级: int) -> float:
        """计算角色强度"""
        return cls.同等级的角色品质强度[品质] * (cls.角色品质成长倍率[品质]**(等级-1))
    
    @classmethod
    def 初始化角色强度表(cls):
        """初始化角色强度表"""
        角色强度表 = {}
        for 品质 in cls.角色品质:
            for 等级 in range(1, 全局参数.角色等级上限 + 1):
                角色强度表[(品质, 等级)] = cls.计算角色强度(品质, 等级)
        return 角色强度表
    
    @classmethod
    def 初始化排序后的角色(cls):
        """初始化排序后的角色列表"""
        return sorted(cls.角色强度表.keys(), key=lambda x: cls.角色强度表[x])

class 每日碎片投放:
    """每日角色碎片投放计算"""
    
    @classmethod
    def 反向计算阶段每日数据(cls, 阶段: dict) -> dict:
        """
        根据阶段的等级范围和每日可通关数量，反向计算每日角色等级、碎片数和强度数据
        
        Args:
            阶段: 关卡阶段枚举中的阶段字典
            
        Returns:
            dict: 包含阶段每日数据的字典
        """
        # 获取阶段信息
        阶段名称 = 阶段["名称"]
        起始关卡 = 阶段["起始关卡"]
        结束关卡 = 阶段["结束关卡"]
        持续时间 = 阶段["持续时间"]
        基准品质 = 阶段.get("基准品质", "绿色")  # 提供默认值
        
        # 获取阶段等级范围和每日可通关数量
        if "阶段等级上下限" in 阶段:
            等级下限, 等级上限 = 阶段["阶段等级上下限"]
        else:
            等级下限, 等级上限 = 1, 全局参数.角色等级上限
            
        if "每日可通关数量" in 阶段:
            最小通关数, 最大通关数 = 阶段["每日可通关数量"]
        else:
            最小通关数, 最大通关数 = 1, 2
        
        # 计算阶段总关卡数
        总关卡数 = 结束关卡 - 起始关卡 + 1
        
        # 初始化每日数据字典
        每日数据 = {}
        
        # 计算每日等级增长
        # 使用贝塞尔曲线来实现平滑的等级增长
        for 日期 in range(持续时间 + 1):  # 包含第0天
            # 使用贝塞尔曲线计算当前日期的等级进度比例
            t = 日期 / 持续时间 if 持续时间 > 0 else 0
            # 使用三阶贝塞尔曲线：B(t) = (1-t)³P₀ + 3(1-t)²tP₁ + 3(1-t)t²P₂ + t³P₃
            # 控制点设为 P₀(0,0), P₁(0.2,0.1), P₂(0.7,0.5), P₃(1,1) 使曲线前期缓慢后期加速
            进度比例 = (1-t)**3 * 0 + 3*(1-t)**2*t * 0.1 + 3*(1-t)*t**2 * 0.5 + t**3 * 1
            
            # 计算当前日期的基准品质等级（使用绿色品质为基准）
            基准品质 = "绿色"  # 最低品质为绿色
            绿色等级上限 = min(等级上限, 全局参数.角色等级上限)
            当日基准等级 = 等级下限 + math.floor((绿色等级上限 - 等级下限) * 进度比例)
            当日基准等级 = max(等级下限, min(当日基准等级, 绿色等级上限))  # 确保在范围内
            
            # 计算基准强度（绿色品质角色的强度）
            基准强度 = 阵容强度计算.计算角色强度(基准品质, 当日基准等级)
            
            # 计算各品质等级（基于基准强度，反推各品质等级）
            各品质等级 = {}
            各品质满级 = {}  # 记录各品质是否已满级
            
            for 品质 in 阵容强度数据.角色品质:
                品质等级上限 = 全局参数.角色等级上限
                
                # 如果是基准品质或进度为1（即最后一天），直接使用当日基准等级
                if 品质 == 基准品质 or t >= 0.99:
                    各品质等级[品质] = 当日基准等级
                    各品质满级[品质] = (当日基准等级 >= 品质等级上限)
                    continue
                
                # 根据基准强度反推等级
                # 强度公式: 强度 = 基础强度 * (成长倍率^(等级-1))
                # 因此等级 = log(强度/基础强度, 成长倍率) + 1
                基础强度 = 阵容强度数据.同等级的角色品质强度[品质]
                成长倍率 = 阵容强度数据.角色品质成长倍率[品质]
                
                # 计算应该达到的等级(向下取整以确保不超过目标强度)
                if 基础强度 > 0 and 成长倍率 > 1:
                    计算等级 = math.log(基准强度 / 基础强度, 成长倍率) + 1
                    理论等级 = math.floor(计算等级)
                    
                    # 确保等级在合理范围内
                    品质等级 = max(等级下限, min(理论等级, 品质等级上限))
                    各品质等级[品质] = 品质等级
                    各品质满级[品质] = (品质等级 >= 品质等级上限)
                else:
                    # 防止异常情况
                    各品质等级[品质] = 等级下限
                    各品质满级[品质] = False
            
            # 计算各品质累计碎片数
            各品质碎片数 = {}
            for 品质 in 阵容强度数据.角色品质:
                等级 = 各品质等级[品质]
                # 计算从1级升到当前等级所需的碎片总数
                所需碎片 = sum(角色碎片数据.角色碎片消耗[品质][:等级-1])
                各品质碎片数[品质] = 所需碎片
            
            # 计算各品质强度
            各品质强度 = {}
            for 品质 in 阵容强度数据.角色品质:
                等级 = 各品质等级[品质]
                强度 = 阵容强度计算.计算角色强度(品质, 等级)
                各品质强度[品质] = 强度
            
            # 计算阵容强度（三个同等级同品质角色的总强度）
            阵容强度 = sum(各品质强度.values()) * 3 / len(阵容强度数据.角色品质)
            
            # 计算当日可通关数
            # 使用贝塞尔曲线计算，使前期较少后期较多
            通关比例 = (1-t)**3 * 0 + 3*(1-t)**2*t * 0.2 + 3*(1-t)*t**2 * 0.7 + t**3 * 1
            当日可通关数 = 最小通关数 + math.floor((最大通关数 - 最小通关数) * 通关比例)
            
            # 存储当日数据
            每日数据[日期] = {
                "各品质等级": 各品质等级,
                "各品质碎片数": 各品质碎片数,
                "各品质强度": 各品质强度,
                "阵容强度": 阵容强度,
                "当日可通关数": 当日可通关数,
            }
        
        # 修正第0天数据
        if 0 in 每日数据:
            每日数据[0]["当日可通关数"] = 0
        
        # 计算累计可通关数
        累计可通关数 = 0
        for 日期 in sorted(每日数据.keys()):
            if 日期 == 0:
                每日数据[日期]["累计可通关数"] = 0
                continue
                
            累计可通关数 += 每日数据[日期]["当日可通关数"]
            累计可通关数 = min(累计可通关数, 总关卡数)  # 确保不超过总关卡数
            每日数据[日期]["累计可通关数"] = 累计可通关数
        
        return {
            "阶段": 阶段,
            "每日数据": 每日数据
        }
    
    @classmethod
    def 计算每日碎片产出量(cls, 阶段每日数据: dict) -> dict:
        """
        计算每日碎片产出量
        
        Args:
            阶段每日数据: 包含阶段每日数据的字典，格式为 反向计算阶段每日数据 的返回值
            
        Returns:
            dict: 包含阶段每日碎片产出量的字典
        """
        阶段 = 阶段每日数据["阶段"]
        每日数据 = 阶段每日数据["每日数据"]
        
        # 初始化产出数据
        产出数据 = {品质: {} for 品质 in 阵容强度数据.角色品质}
        
        # 计算每日产出量
        for 日期 in sorted(每日数据.keys()):
            # 第0天产出为0
            if 日期 == 0:
                for 品质 in 阵容强度数据.角色品质:
                    产出数据[品质][日期] = 0
                continue
                
            # 计算当日产出量（当日碎片数 - 前一日碎片数）
            前一日期 = 日期 - 1
            for 品质 in 阵容强度数据.角色品质:
                当日碎片数 = 每日数据[日期]["各品质碎片数"][品质]
                前一日碎片数 = 每日数据[前一日期]["各品质碎片数"][品质]
                当日产出量 = 当日碎片数 - 前一日碎片数
                产出数据[品质][日期] = 当日产出量
        
        return {
            "阶段": 阶段,
            "产出数据": 产出数据
        }

