    @classmethod
    def 获取各阶段各品质的碎片产出总量(cls, 关卡角色碎片总需求: dict) -> dict:
        """
        计算每个阶段各品质的实际产出量，确保累积产出能达到每个阶段的需求总量
        
        Args:
            关卡角色碎片总需求: 每个关卡中各品质角色的碎片总需求，格式为 {关卡数: {品质: 碎片总数}}
            
        Returns:
            dict: 每个阶段各品质的实际产出量，格式为 {阶段名称: {品质: 产出量}}
        """
        各阶段各品质的碎片产出总量 = {}
        上一阶段累积产出 = {品质: 0 for 品质 in 阵容强度数据.角色品质}
        
        for 阶段 in 关卡阶段枚举.获取所有阶段():
            各阶段各品质的碎片产出总量[阶段["名称"]] = {}
            
            # 获取当前阶段所有关卡的需求总量
            当前阶段需求总量 = {品质: 0 for 品质 in 阵容强度数据.角色品质}
            
            # 遍历当前阶段的所有关卡，找出每个品质的最大需求
            for 关卡数 in range(阶段["起始关卡"], 阶段["结束关卡"] + 1):
                if 关卡数 in 关卡角色碎片总需求:
                    for 品质 in 阵容强度数据.角色品质:
                        if 品质 in 关卡角色碎片总需求[关卡数]:
                            当前阶段需求总量[品质] = max(当前阶段需求总量[品质], 关卡角色碎片总需求[关卡数][品质])
            
            # 计算当前阶段需要的实际产出量
            for 品质 in 阵容强度数据.角色品质:
                # 当前阶段的实际产出 = 当前阶段需求总量 - 上一阶段累积产出
                当前阶段产出 = 当前阶段需求总量[品质] - 上一阶段累积产出[品质]
                各阶段各品质的碎片产出总量[阶段["名称"]][品质] = 当前阶段产出
                
                # 更新累积产出
                上一阶段累积产出[品质] = 当前阶段需求总量[品质]
        
        return 各阶段各品质的碎片产出总量
    

    
    @classmethod
    def 线性计算某阶段的碎片的日产出关系(cls, 阶段:关卡阶段枚举, 各阶段各品质碎片产出总量: dict) -> dict:
        """
        线性计算某阶段的碎片的日产出关系
        Args:
            阶段: 关卡阶段枚举
            各阶段各品质碎片产出总量: 各阶段各品质的碎片产出总量
        Returns:
            dict: 包含阶段信息的日产出数据，格式为 {"阶段": 阶段枚举, "产出数据": {品质: {日: 产出量}}}
        """
        # 如果输入是新的格式（包含阶段信息），则提取产出总量
        if isinstance(各阶段各品质碎片产出总量, dict) and "产出总量" in 各阶段各品质碎片产出总量:
            该阶段各品质碎片产出总量 = 各阶段各品质碎片产出总量["产出总量"]
        else:
            # 兼容旧格式
            该阶段各品质碎片产出总量 = 各阶段各品质碎片产出总量[阶段["名称"]]
            
        产出时间 = 阶段["持续时间"]
        日产出 = {}
        for 品质 in 阵容强度数据.角色品质:
            日产出[品质] = {}
            for 日 in range(产出时间+1):
                日产出[品质][日] = math.ceil(该阶段各品质碎片产出总量[品质] / 产出时间*日)
                if 日产出[品质][日] < 0:
                    raise ValueError(f"负数产出，{品质}在{阶段['名称']}的第{日}天产出为{日产出[品质][日]}")
        
        # 返回包含阶段信息的字典
        return {
            "阶段": 阶段,
            "产出数据": 日产出
        }
    
    
    @classmethod
    def 贝塞尔曲线计算某阶段碎片的日产出关系(cls, 阶段:关卡阶段枚举, 各阶段各品质碎片产出总量: dict, 中间点相对日期: int = None, 中间点产出比例: float = None, 第一天目标强度: int = None) -> dict:
        """
        使用二阶贝塞尔曲线计算某阶段的碎片的日产出关系
        
        Args:
            阶段: 关卡阶段枚举
            各阶段各品质碎片产出总量: 各阶段各品质的碎片产出总量
            中间点相对日期: 贝塞尔曲线中间控制点的日期，它以这个阶段开始的时间点为开始。默认为产出时间的一半
            中间点产出比例: 中间控制点的产出占该阶段总产出的比例。默认为1/2
            第一天目标强度: 第一天要达到的目标强度，如果提供此参数，将自动计算所需的中间点产出比例
            
        Returns:
            dict: 包含阶段信息的日产出数据，格式为 {"阶段": 阶段枚举, "产出数据": {品质: {日: 产出量}}}
        """
        # 如果输入是新的格式（包含阶段信息），则提取产出总量
        if isinstance(各阶段各品质碎片产出总量, dict) and "产出总量" in 各阶段各品质碎片产出总量:
            该阶段各品质碎片产出总量 = 各阶段各品质碎片产出总量["产出总量"]
        else:
            # 兼容旧格式
            该阶段各品质碎片产出总量 = 各阶段各品质碎片产出总量[阶段["名称"]]
            
        产出时间 = 阶段["持续时间"]
        日产出 = {}
        
        # 设置默认值
        if 中间点相对日期 is None:
            中间点相对日期 = 产出时间 // 2  # 默认使用产出时间的一半
            
        if 第一天目标强度 is not None:
            # 如果提供了第一天目标强度，计算所需的中间点产出比例
            for 品质 in 阵容强度数据.角色品质:
                总产出 = 该阶段各品质碎片产出总量[品质]
                中间点产出比例 = cls.计算达到第一天目标强度所需的中间点产出比例(产出时间, 总产出, 第一天目标强度)
                break  # 只需要计算一次，因为所有品质使用相同的产出比例
        elif 中间点产出比例 is None:
            # 如果都没有提供，使用默认值1/2
            中间点产出比例 = 1/2
        
        # 确保中间点日期在有效范围内
        if 中间点相对日期 <= 0 or 中间点相对日期 >= 产出时间:
            raise ValueError(f"中间点日期必须在0到{产出时间}之间")
            
        # 确保中间点产出比例在有效范围内
        if 中间点产出比例 <= 0 or 中间点产出比例 >= 1:
            raise ValueError("中间点产出比例必须在0到1之间")
        
        for 品质 in 阵容强度数据.角色品质:
            日产出[品质] = {}
            总产出 = 该阶段各品质碎片产出总量[品质]
            
            # 定义贝塞尔曲线的三个控制点
            # P0: 起始点 (0,0)
            # P1: 中间控制点
            # P2: 结束点 (产出时间,总产出)
            
            # 已知点：
            P0 = (0, 0)  # 起点
            P2 = (产出时间, 总产出)  # 终点
            P1 = (中间点相对日期, 总产出 * 中间点产出比例)  # 中间点
            
            # 计算每天的产出量
            for 日 in range(产出时间+1):
                # 计算贝塞尔曲线上的点
                t = 日 / 产出时间  # 参数t从0到1
                
                # 二阶贝塞尔曲线公式：B(t) = (1-t)²P0 + 2(1-t)tP1 + t²P2
                x = (1-t)**2 * P0[0] + 2*(1-t)*t * P1[0] + t**2 * P2[0]
                y = (1-t)**2 * P0[1] + 2*(1-t)*t * P1[1] + t**2 * P2[1]
                
                日产出[品质][日] = math.ceil(y)
                if 日产出[品质][日] < 0:
                    raise ValueError(f"负数产出，{品质}在{阶段['名称']}的第{日}天产出为{日产出[品质][日]}")
        
        # 返回包含阶段信息的字典
        return {
            "阶段": 阶段,
            "产出数据": 日产出
        }
    
