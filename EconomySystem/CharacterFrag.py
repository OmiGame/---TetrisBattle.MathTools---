    @classmethod
    def 计算每日可通过关卡字典(cls, 日产出关系: dict, 关卡角色碎片总需求: dict, 产出需求倍数: float = 1.05) -> dict:
        """
        计算某阶段每日可通过关卡数
        Args:
            日产出关系: 包含阶段信息的日产出数据，格式为 {阶段名称: {日: {品质: 产出数量}}}
            关卡角色碎片总需求: 关卡角色碎片总需求，格式为 {关卡数: {品质: 碎片总数}}
            产出需求倍数: 产出需要超过需求的倍数，默认为1.05，该参数主要是为了平衡同品质角色碎片产出差异
        Returns:
            dict: 每日可通过关卡数，格式为 {阶段名称: {日: 最大关卡数}}
        """
        每日可通过关卡 = {}
        
        # 遍历所有阶段
        for 阶段名称, 阶段产出数据 in 日产出关系.items():
            每日可通过关卡[阶段名称] = {}
            
            # 获取该阶段的所有日期并排序
            所有日期 = sorted(阶段产出数据.keys())
            
            # 获取阶段对应的关卡范围
            阶段信息 = None
            for 阶段 in 关卡阶段枚举.获取所有阶段():
                if 阶段.value["名称"] == 阶段名称:
                    阶段信息 = 阶段
                    break
            
            if 阶段信息 is None:
                print(f"警告：未找到阶段 {阶段名称} 的信息")
                continue
                
            起始关卡 = 阶段信息.value["起始关卡"]
            结束关卡 = 阶段信息.value["结束关卡"]
            
            # 遍历每一天
            for 日 in 所有日期:
                # 获取当天的产出数据
                当日产出 = 阶段产出数据[日]
                
                # 计算最大可通过关卡
                最大关卡 = 0
                for 关卡数 in range(起始关卡, 结束关卡 + 1):
                    if 关卡数 not in 关卡角色碎片总需求:
                        continue
                        
                    可通过 = True
                    for 品质 in 阵容强度数据.角色品质:
                        # 获取关卡需求
                        需求 = 关卡角色碎片总需求[关卡数][品质]
                        产出 = 当日产出.get(品质, 0)
                        
                        # 检查产出是否满足需求（包含平衡因子）
                        if 产出 < 需求 * 产出需求倍数:
                            可通过 = False
                            break
                    
                    if 可通过:
                        最大关卡 = 关卡数
                    else:
                        break
                
                每日可通过关卡[阶段名称][日] = 最大关卡 - 起始关卡 + 1 if 最大关卡 > 0 else 0
        
        return 每日可通过关卡


    @classmethod
    def 计算可达到等级(cls, 品质: str, 碎片数量: int) -> int:
        """
        计算给定碎片数量可达到的等级
        
        Args:
            品质: 角色品质
            碎片数量: 可用碎片数量
            
        Returns:
            int: 可达到的等级
        """
        等级 = 1
        剩余碎片 = 碎片数量
        
        # 获取该品质的碎片消耗列表
        碎片消耗列表 = 角色碎片数据.角色碎片消耗[品质]
        
        # 遍历等级，每次减去当前等级升级所需的碎片
        while 等级 < 全局参数.角色等级上限:
            # 获取当前等级升级所需的碎片（注意索引从0开始，所以是等级-1）
            if 等级 - 1 < len(碎片消耗列表):
                升级所需碎片 = 碎片消耗列表[等级 - 1]
            else:
                # 如果等级超出列表范围，使用最后一个值
                升级所需碎片 = 碎片消耗列表[-1]
                print(f"等级超出列表范围，使用最后一个值: {品质} {等级} 升级所需碎片: {升级所需碎片}")
                
            if 剩余碎片 >= 升级所需碎片:
                剩余碎片 -= 升级所需碎片
                等级 += 1
            else:
                break
                
        return 等级

    @classmethod
    def 获取每日详细数据(cls, 日产出关系: dict, 关卡角色碎片总需求: dict, 关卡角色等级要求: dict, 关卡角色强度要求: dict) -> dict:
        """
        获取每日的详细数据，包括各品质的碎片数、等级、强度、阵容强度等
        
        Args:
            日产出关系: 包含阶段信息的日产出数据，格式为 {阶段名称: {日: {品质: 产出量}}}
            关卡角色碎片总需求: 关卡角色碎片总需求，格式为 {关卡数: {品质: 碎片总数}}
            关卡角色等级要求: 关卡角色等级要求，格式为 {关卡数: {品质: 等级}}
            关卡角色强度要求: 关卡角色强度要求，格式为 {关卡数: {品质: 强度要求}}
            
        Returns:
            dict: 包含阶段信息的每日详细数据，格式为 {
                阶段名称: {
                    日: {
                        "各品质碎片数": {品质: 碎片数},
                        "各品质等级": {品质: 等级},
                        "各品质强度": {品质: 强度},
                        "阵容强度": float,
                        "最大关卡数": int,
                        "最大关卡要求": {
                            "各品质碎片数": {品质: 碎片数},
                            "各品质等级": {品质: 等级},
                            "各品质强度": {品质: 强度},
                            "阵容强度": float
                        }
                    }
                }
            }
        """
        每日详细数据 = {}
        
        # 初始化各品质累积产出 - 放在阶段循环外部，实现跨阶段累积
        累积产出 = {品质: 0 for 品质 in 阵容强度数据.角色品质}
        
        # 计算每日可通过关卡
        每日可通过关卡 = cls.计算每日可通过关卡字典(日产出关系, 关卡角色碎片总需求)
        
        # 遍历所有阶段
        for 阶段 in 关卡阶段枚举.获取所有阶段():
            阶段名称 = 阶段.value["名称"]
            每日详细数据[阶段名称] = {}
            各品质角色数量 = 阶段.value["各品质角色数量"]
            
            # 获取阶段对应的关卡范围
            起始关卡 = 阶段.value["起始关卡"]
            结束关卡 = 阶段.value["结束关卡"]
            
            # 获取该阶段的产出数据
            产出数据 = 日产出关系[阶段名称]
            
            # 获取所有天数
            所有天数 = sorted(产出数据.keys())
            
            # 确保至少包含第0天
            if 0 not in 所有天数:
                所有天数.insert(0, 0)
                
            # 遍历每一天
            for 日 in 所有天数:
                每日详细数据[阶段名称][日] = {
                    "各品质碎片数": {},
                    "各品质等级": {},
                    "各品质强度": {},
                    "阵容强度": 0.0,
                    "最大关卡数": 0,
                    "最大关卡要求": {}
                }
                
                # 第0天特殊处理
                if 日 == 0:
                    continue
                
                # 计算各品质的碎片数、等级和强度
                for 品质 in 阵容强度数据.角色品质:
                    # 获取当天的产出并累加
                    当日产出 = 产出数据[日].get(品质, 0)
                    累积产出[品质] += 当日产出
                    
                    # 记录累积产出
                    每日详细数据[阶段名称][日]["各品质碎片数"][品质] = 当日产出   #不许改这里，这里就是记录每日中每个品质的当日产出数据
                    
                    # 计算单个角色获得的碎片数量，使用临时变量避免修改原始累积产出
                    单个角色碎片数 = 0
                    if 品质 == "绿色":
                        单个角色碎片数 = 累积产出[品质] / 各品质角色数量[0]
                    elif 品质 == "蓝色":
                        单个角色碎片数 = 累积产出[品质] / 各品质角色数量[1]
                    elif 品质 == "紫色":
                        单个角色碎片数 = 累积产出[品质] / 各品质角色数量[2]
                    
                    # 计算可达到的等级 - 使用单个角色碎片数而不是累积产出
                    等级 = cls.计算可达到等级(品质, 单个角色碎片数)
                    每日详细数据[阶段名称][日]["各品质等级"][品质] = 等级
                    
                    # 计算强度
                    强度 = 阵容强度计算.计算角色强度(品质, 等级)
                    每日详细数据[阶段名称][日]["各品质强度"][品质] = 强度
                
                # 计算阵容强度 - 选择最高强度的品质，然后乘以最大上阵角色数
                各品质强度 = 每日详细数据[阶段名称][日]["各品质强度"]
                最高强度品质 = max(各品质强度.items(), key=lambda x: x[1], default=(None, 0))
                if 最高强度品质[0] is not None:
                    最高单体强度 = 最高强度品质[1]
                    阵容强度 = 最高单体强度 * 全局参数.最大上阵角色数
                else:
                    阵容强度 = 0
                每日详细数据[阶段名称][日]["阵容强度"] = 阵容强度
                
                # 获取最大可通过关卡
                最大关卡数 = 每日可通过关卡[阶段名称].get(日, 0)
                每日详细数据[阶段名称][日]["最大关卡数"] = 最大关卡数
                
                # 如果有可通过的关卡，获取最大关卡的要求
                if 最大关卡数 > 0:
                    最大关卡 = 起始关卡 + 最大关卡数 - 1
                    
                    各关卡品质强度 = {品质: 关卡角色强度要求[最大关卡][品质] for 品质 in 阵容强度数据.角色品质}
                    最高要求品质 = max(各关卡品质强度.items(), key=lambda x: x[1], default=(None, 0))
                    if 最高要求品质[0] is not None:
                        最高要求强度 = 最高要求品质[1]
                        关卡阵容强度 = 最高要求强度 * 全局参数.最大上阵角色数
                    else:
                        关卡阵容强度 = 0
                    
                    每日详细数据[阶段名称][日]["最大关卡要求"] = {
                        "各品质碎片数": {品质: 关卡角色碎片总需求[最大关卡][品质] for 品质 in 阵容强度数据.角色品质},
                        "各品质等级": {品质: 关卡角色等级要求[最大关卡][品质] for 品质 in 阵容强度数据.角色品质},
                        "各品质强度": 各关卡品质强度,
                        "阵容强度": 关卡阵容强度
                    }
        
        return 每日详细数据
    
    @classmethod
    def 计算达到目标强度所需最低等级(cls, 品质: str, 目标强度: float) -> int:
        """
        计算达到目标强度的某个品质角色所需的最低等级
        
        Args:
            品质: 角色品质（'绿色'、'蓝色'、'紫色'等）
            目标强度: 要达到的目标强度值
            
        Returns:
            int: 达到目标强度所需的最低等级，如果无法达到则返回0
        """
        # 获取该品质角色的基础强度和成长倍率
        基础强度 = 阵容强度数据.同等级的角色品质强度[品质]
        成长倍率 = 阵容强度数据.角色品质成长倍率[品质]
        
        # 检查目标强度是否可达到
        最大强度 = 基础强度 * (成长倍率 ** (全局参数.角色等级上限 - 1))
        if 最大强度 < 目标强度:
            # 即使满级也无法达到目标强度
            return 0
        
        # 计算达到目标强度所需的最低等级
        if 目标强度 <= 基础强度:
            # 如果目标强度小于等于基础强度，则1级就可以达到
            return 1
        else:
            # 使用对数计算所需等级: 目标强度 = 基础强度 * (成长倍率 ^ (等级-1))
            # 所以: 等级 = log_(成长倍率)(目标强度/基础强度) + 1
            所需等级 = math.ceil(math.log(目标强度 / 基础强度, 成长倍率)) + 1
            
            # 确保等级在合理范围内（1到角色等级上限）
            所需等级 = max(1, min(所需等级, 全局参数.角色等级上限))
            
            return 所需等级
    

