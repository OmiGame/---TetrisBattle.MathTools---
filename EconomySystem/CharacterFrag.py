    @classmethod
    def 计算角色升级所需碎片(cls, 品质: str, 当前等级: int, 目标等级: int = None) -> int:
        """
        计算角色从当前等级升级到目标等级所需的碎片数量
        
        Args:
            品质: 角色品质（绿色、蓝色、紫色）
            当前等级: 角色当前等级
            目标等级: 角色目标等级，默认为当前等级+1
            
        Returns:
            int: 所需碎片数量
        """
        if 目标等级 is None:
            目标等级 = 当前等级 + 1
            
        if 当前等级 >= 目标等级:
            return 0
            
        if 品质 not in 角色碎片数据.角色碎片消耗:
            raise ValueError(f"无效的角色品质: {品质}")
            
        # 获取对应品质的碎片消耗列表
        碎片消耗列表 = 角色碎片数据.角色碎片消耗[品质]
        
        # 确保等级在有效范围内
        if 当前等级 < 1 or 目标等级 > len(碎片消耗列表) + 1:
            raise ValueError(f"等级超出范围: 当前等级={当前等级}, 目标等级={目标等级}, 最大等级={len(碎片消耗列表)}")
        
        # 计算从当前等级到目标等级所需的总碎片数
        总碎片数 = 0
        for 等级 in range(当前等级, 目标等级):
            总碎片数 += 碎片消耗列表[等级 - 1]  # 列表索引从0开始，等级从1开始
            
        return 总碎片数
    
    
    @classmethod
    def 计算碎片养成时间(cls, 当前关卡: int, 目标品质: str, 当前等级: int, 目标等级: int, 每日总碎片量: int = None) -> int:
        """
        计算角色从当前等级升级到目标等级所需的时间
        
        Args:
            当前关卡: 当前关卡数
            目标品质: 角色品质
            当前等级: 角色当前等级
            目标等级: 角色目标等级
            每日总碎片量: 每日总碎片产出量，如果为None则使用当前阶段的默认值
            
        Returns:
            int: 所需天数
        """
        # 计算所需碎片数量
        所需碎片 = cls.计算角色升级所需碎片(目标品质, 当前等级, 目标等级)
        
        # 计算每日获得的目标品质碎片数量（考虑角色数量）
        每日碎片掉落 = cls.计算每日碎片掉落量(当前关卡, 每日总碎片量)
        每日碎片 = 每日碎片掉落["每个角色"][目标品质]
        
        # 计算所需天数
        if 每日碎片 <= 0:
            return float('inf')  # 无法获得该品质碎片
        
        return math.ceil(所需碎片 / 每日碎片)
    
    
    @classmethod
    def 计算达到目标强度时各品质角色的最低等级(cls, 目标强度: float) -> dict:
        """
        计算达到目标强度时各品质角色的最低等级
        
        Args:
            目标强度: 目标强度值
            
        Returns:
            dict: 各品质角色所需的最低等级（如不能达到则为0）
        """
        result = {}
        
        for 品质 in cls.角色品质:
            if 目标强度 <= 0:
                result[品质] = 0
                continue
                
            # 计算所需等级
            if 阵容强度计算.计算角色强度(品质, 全局参数.角色等级上限) < 目标强度:
                # 即使满级也无法达到目标强度
                result[品质] = 0
            else:
                # 计算最小等级
                min_level = math.ceil(math.log(目标强度 / 阵容强度计算.计算角色强度(品质, 全局参数.角色等级上限), 
                                             阵容强度数据.角色品质成长倍率[品质])) + 1
                # 确保等级在合理范围内
                min_level = max(1, min(min_level, 全局参数.角色等级上限))
                result[品质] = min_level
        
        return result
    



