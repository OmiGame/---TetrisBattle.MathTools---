    @classmethod
    def 获取各阶段各品质的碎片产出总量(cls, 关卡角色碎片总需求: dict) -> dict:
        """
        计算每个阶段各品质的实际产出量，确保累积产出能达到每个阶段的需求总量
        
        Args:
            关卡角色碎片总需求: 每个关卡中各品质角色的碎片总需求，格式为 {关卡数: {品质: 碎片总数}}
            
        Returns:
            dict: 每个阶段各品质的实际产出量，格式为 {阶段名称: {品质: 产出量}}
        """
        各阶段各品质的碎片产出总量 = {}
        上一阶段累积产出 = {品质: 0 for 品质 in 阵容强度数据.角色品质}
        
        for 阶段 in 关卡阶段枚举.获取所有阶段():
            各阶段各品质的碎片产出总量[阶段["名称"]] = {}
            
            # 获取当前阶段所有关卡的需求总量
            当前阶段需求总量 = {品质: 0 for 品质 in 阵容强度数据.角色品质}
            
            # 遍历当前阶段的所有关卡，找出每个品质的最大需求
            for 关卡数 in range(阶段["起始关卡"], 阶段["结束关卡"] + 1):
                for 品质 in 阵容强度数据.角色品质:
                    当前阶段需求总量[品质] = max(当前阶段需求总量[品质], 关卡角色碎片总需求[关卡数][品质])
            
            # 计算当前阶段需要的实际产出量
            for 品质 in 阵容强度数据.角色品质:
                # 当前阶段的实际产出 = 当前阶段需求总量 - 上一阶段累积产出
                当前阶段产出 = 当前阶段需求总量[品质] - 上一阶段累积产出[品质]
                各阶段各品质的碎片产出总量[阶段["名称"]][品质] = 当前阶段产出
                
                # 更新累积产出
                上一阶段累积产出[品质] = 当前阶段需求总量[品质]
        
        return 各阶段各品质的碎片产出总量    
    
    @classmethod
    def 线性计算某阶段的碎片的日产出关系(cls, 阶段:关卡阶段枚举, 各阶段各品质碎片产出总量: dict) -> dict:
        """
        线性计算某阶段的碎片的日产出关系
        Args:
            阶段: 关卡阶段枚举
            各阶段各品质碎片产出总量: 各阶段各品质的碎片产出总量
        Returns:
            dict: 包含阶段信息的日产出数据，格式为 {"阶段": 阶段枚举, "产出数据": {品质: {日: 产出量}}}
        """
        该阶段各品质碎片产出总量 = 各阶段各品质碎片产出总量[阶段["名称"]]
        产出时间 = 阶段["持续时间"]
        日产出 = {}
        for 品质 in 阵容强度数据.角色品质:
            日产出[品质] = {}
            for 日 in range(产出时间+1):
                日产出[品质][日] = math.ceil(该阶段各品质碎片产出总量[品质] / 产出时间*日)
                if 日产出[品质][日] < 0:
                    raise ValueError(f"负数产出，{品质}在{阶段['名称']}的第{日}天产出为{日产出[品质][日]}")
        
        # 返回包含阶段信息的字典
        return {
            "阶段": 阶段,
            "产出数据": 日产出
        }
    
    
    @classmethod
    def 贝塞尔曲线计算某阶段碎片的日产出关系(cls, 阶段:关卡阶段枚举, 各阶段各品质碎片产出总量: dict, 中间点相对日期: int, 中间点产出比例: float = None) -> dict:
        """
        使用三阶贝塞尔曲线计算某阶段的碎片的日产出关系
        
        Args:
            阶段: 关卡阶段枚举
            各阶段各品质碎片产出总量: 各阶段各品质的碎片产出总量
            中间点相对日期: 贝塞尔曲线中间控制点的日期，它以这个阶段开始的时间点为开始
            中间点产出比例: 中间控制点的产出占总产出的比例，默认为0.5
            
        Returns:
            dict: 包含阶段信息的日产出数据，格式为 {"阶段": 阶段枚举, "产出数据": {品质: {日: 产出量}}}
        """
        该阶段各品质碎片产出总量 = 各阶段各品质碎片产出总量[阶段["名称"]]
        产出时间 = 阶段["持续时间"]
        日产出 = {}
        
        # 确保中间点日期在有效范围内
        if 中间点相对日期 <= 0 or 中间点相对日期 >= 产出时间:
            raise ValueError(f"中间点日期必须在0到{产出时间}之间")
            
        # 确保中间点产出比例在有效范围内
        if 中间点产出比例 <= 0 or 中间点产出比例 >= 1 or 中间点产出比例 is None:
            raise ValueError("中间点产出比例必须在0到1之间，或不能为None")
        
        for 品质 in 阵容强度数据.角色品质:
            日产出[品质] = {}
            总产出 = 该阶段各品质碎片产出总量[品质]
            
            # 定义贝塞尔曲线的四个控制点
            # P0: 起始点 (0,0)
            # P1: 第一个中间控制点
            # P2: 第二个中间控制点
            # P3: 结束点 (产出时间,总产出)
            
            # 计算中间控制点的位置
            # 为了确保曲线经过指定的中间点，我们需要计算合适的控制点
            t = 中间点相对日期 / 产出时间  # 中间点在曲线上的参数值
            
            # 已知点：
            P0 = (0, 0)  # 起点
            P3 = (产出时间, 总产出)  # 终点
            P = (中间点相对日期, 总产出 * 中间点产出比例)  # 中间点
            
            # 根据贝塞尔曲线公式，在t时刻经过点P：
            # P = (1-t)³P0 + 3(1-t)²tP1 + 3(1-t)t²P2 + t³P3
            # 我们需要解出P1和P2
            
            # 为了简化计算，我们让P1和P2在P0-P和P-P3的连线上
            # 这样可以保证曲线经过中间点，同时保持平滑
            P1 = (P[0] * 0.5, P[1] * 0.5)  # P1在P0和P之间
            P2 = (P[0] + (P3[0] - P[0]) * 0.5, P[1] + (P3[1] - P[1]) * 0.5)  # P2在P和P3之间
            
            # 计算每天的产出量
            for 日 in range(产出时间+1):
                # 计算贝塞尔曲线上的点
                t = 日 / 产出时间  # 参数t从0到1
                
                # 三阶贝塞尔曲线公式：B(t) = (1-t)³P0 + 3(1-t)²tP1 + 3(1-t)t²P2 + t³P3
                x = (1-t)**3 * P0[0] + 3*(1-t)**2*t * P1[0] + 3*(1-t)*t**2 * P2[0] + t**3 * P3[0]
                y = (1-t)**3 * P0[1] + 3*(1-t)**2*t * P1[1] + 3*(1-t)*t**2 * P2[1] + t**3 * P3[1]
                
                日产出[品质][日] = math.ceil(y)
                if 日产出[品质][日] < 0:
                    raise ValueError(f"负数产出，{品质}在{阶段['名称']}的第{日}天产出为{日产出[品质][日]}")
        
        # 返回包含阶段信息的字典
        return {
            "阶段": 阶段,
            "产出数据": 日产出
        }
    
    @classmethod
    def 计算各品质每日产出(cls, 日产出关系: dict) -> dict:
        """
        计算各品质每日产出数量
        
        Args:
            日产出关系: 包含阶段信息的日产出数据，格式为 {"阶段": 阶段枚举, "产出数据": {品质: {日: 产出量}}}
            
        Returns:
            dict: 包含阶段信息的每日产出数量，格式为 {"阶段": 阶段枚举, "产出数据": {品质: {日: 产出数量}}}
        """
        各品质每日产出 = {}
        阶段 = 日产出关系["阶段"]
        产出数据 = 日产出关系["产出数据"]
        
        for 品质 in 阵容强度数据.角色品质:
            各品质每日产出[品质] = {}
            上一日产出量 = 0
            
            # 遍历每一天
            for 日 in sorted(产出数据[品质].keys()):
                # 当日产出量
                当日产出量 = 产出数据[品质][日]
                # 计算当日实际产出数量（当日产出量 - 上一日产出量）
                当日产出数量 = 当日产出量 - 上一日产出量
                if 当日产出数量 < 0:
                    raise ValueError(f"负数产出，{品质}在{阶段['名称']}的第{日}天产出为{当日产出数量}") 
                各品质每日产出[品质][日] = 当日产出数量

                # 更新上一日产出量
                上一日产出量 = 当日产出量

        # 返回包含阶段信息的字典
        return {
            "阶段": 阶段,
            "产出数据": 各品质每日产出
        }
    
    @classmethod
    def 计算每日可通过关卡字典(cls, 日产出关系: dict, 关卡角色碎片总需求: dict, 产出需求倍数: float = 1.05) -> dict:
        """
        计算某阶段每日可通过关卡数
        Args:
            日产出关系: 包含阶段信息的日产出数据，格式为 {"阶段": 阶段枚举, "产出数据": {品质: {日: 产出量}}}
            关卡角色碎片总需求: 关卡角色碎片总需求，格式为 {关卡数: {品质: 碎片总数}}
            产出需求倍数: 产出需要超过需求的倍数，默认为1.05，该参数主要是为了平衡同品质角色碎片产出差异
        Returns:
            dict: 包含阶段信息的每日可通过关卡数，格式为 {"阶段": 阶段枚举, "关卡数据": {日: 最大关卡数}}
        """
        每日可通过关卡 = {}
        阶段 = 日产出关系["阶段"]
        产出数据 = 日产出关系["产出数据"]
        
        # 获取所有天数
        所有天数 = set()
        for 品质 in 产出数据:
            所有天数.update(产出数据[品质].keys())
        所有天数 = sorted(list(所有天数))
        
        # 确保至少包含第0天
        if 0 not in 所有天数:
            所有天数.insert(0, 0)
        
        # 获取阶段对应的关卡范围
        起始关卡 = 阶段["起始关卡"]
        结束关卡 = 阶段["结束关卡"]
        
        for 日 in 所有天数:
            # 累积产出
            当日累积产出 = {品质: 产出数据[品质].get(日, 0) for 品质 in 阵容强度数据.角色品质}
            
            最大关卡 = 0
            for 关卡数 in range(起始关卡, 结束关卡 + 1):
                可通过 = True
                for 品质 in 阵容强度数据.角色品质:
                    # 获取关卡需求
                    if 关卡数 in 关卡角色碎片总需求:
                        需求 = 关卡角色碎片总需求[关卡数][品质]
                        产出 = 当日累积产出[品质]
                        
                        # 检查产出是否满足需求（包含平衡因子）
                        if 产出 < 需求 * 产出需求倍数:
                            可通过 = False
                            break
                
                if 可通过:
                    最大关卡 = 关卡数
                else:
                    break
            
            每日可通过关卡[日] = 最大关卡 - 起始关卡 + 1 if 最大关卡 > 0 else 0
        
        # 返回包含阶段信息的字典
        return {
            "阶段": 阶段,
            "关卡数据": 每日可通过关卡
        }


    @classmethod
    def 获取每日详细数据(cls, 日产出关系: dict, 关卡角色碎片总需求: dict, 关卡角色等级要求: dict, 关卡角色要求: dict) -> dict:
        """
        获取每日的详细数据，包括各品质的碎片数、等级、强度、阵容强度等
        
        Args:
            日产出关系: 包含阶段信息的日产出数据，格式为 {"阶段": 阶段枚举, "产出数据": {品质: {日: 产出量}}}
            关卡角色碎片总需求: 关卡角色碎片总需求，格式为 {关卡数: {品质: 碎片总数}}
            关卡角色等级要求: 关卡角色等级要求，格式为 {关卡数: {品质: 等级}}
            关卡角色要求: 关卡角色强度要求，格式为 {关卡数: {品质: 强度要求}}
            
        Returns:
            dict: 包含阶段信息的每日详细数据，格式为 {
                "阶段": 阶段枚举,
                "详细数据": {
                    日: {
                        "各品质碎片数": {品质: 碎片数},
                        "各品质等级": {品质: 等级},
                        "各品质强度": {品质: 强度},
                        "阵容强度": float,
                        "最大关卡数": int,
                        "最大关卡要求": {
                            "各品质碎片数": {品质: 碎片数},
                            "各品质等级": {品质: 等级},
                            "各品质强度": {品质: 强度},
                            "阵容强度": float
                        }
                    }
                }
            }
        """
        每日详细数据 = {}
        阶段 = 日产出关系["阶段"]
        产出数据 = 日产出关系["产出数据"]
        
        # 获取阶段对应的关卡范围
        起始关卡 = 阶段["起始关卡"]
        结束关卡 = 阶段["结束关卡"]
        
        # 获取所有天数
        所有天数 = set()
        for 品质 in 产出数据:
            所有天数.update(产出数据[品质].keys())
        所有天数 = sorted(list(所有天数))
        
        # 确保至少包含第0天
        if 0 not in 所有天数:
            所有天数.insert(0, 0)
        
        # 遍历每一天
        for 日 in 所有天数:
            每日详细数据[日] = {
                "各品质碎片数": {},
                "各品质等级": {},
                "各品质强度": {},
                "阵容强度": 0.0,
                "最大关卡数": 0,
                "最大关卡要求": {}
            }
            
            # 第0天特殊处理
            if 日 == 0:
                continue
            
            # 计算各品质的碎片数、等级和强度
            for 品质 in 阵容强度数据.角色品质:
                # 获取当前日期的累积产出量
                当前累积产出 = 产出数据[品质].get(日, 0)
                每日详细数据[日]["各品质碎片数"][品质] = 当前累积产出
                
                # 计算可达到的等级
                等级 = 1
                剩余碎片 = 当前累积产出
                while 等级 < 全局参数.角色等级上限:
                    所需碎片 = cls.计算角色升级所需碎片(品质, 等级)
                    if 剩余碎片 >= 所需碎片:
                        剩余碎片 -= 所需碎片
                        等级 += 1
                    else:
                        break
                每日详细数据[日]["各品质等级"][品质] = 等级
                
                # 计算强度
                强度 = 阵容强度计算.计算角色强度(品质, 等级)
                每日详细数据[日]["各品质强度"][品质] = 强度
            
            # 计算阵容强度
            各品质强度 = 每日详细数据[日]["各品质强度"]
            阵容强度 = sum(各品质强度.values())
            每日详细数据[日]["阵容强度"] = 阵容强度
            
            # 计算最大可通过关卡
            最大关卡 = 0
            for 关卡数 in range(起始关卡, 结束关卡 + 1):
                if 关卡数 not in 关卡角色碎片总需求:
                    continue
                    
                可通过 = True
                for 品质 in 阵容强度数据.角色品质:
                    # 获取关卡需求
                    需求 = 关卡角色碎片总需求[关卡数][品质]
                    产出 = 每日详细数据[日]["各品质碎片数"][品质]
                    
                    # 检查产出是否满足需求
                    if 产出 < 需求:
                        可通过 = False
                        break
                
                if 可通过:
                    最大关卡 = 关卡数
                else:
                    break
            
            每日详细数据[日]["最大关卡数"] = 最大关卡 - 起始关卡 + 1 if 最大关卡 > 0 else 0
            
            # 如果有可通过的关卡，获取最大关卡的要求
            if 最大关卡 > 0:
                每日详细数据[日]["最大关卡要求"] = {
                    "各品质碎片数": {品质: 关卡角色碎片总需求[最大关卡][品质] for 品质 in 阵容强度数据.角色品质},
                    "各品质等级": {品质: 关卡角色等级要求[最大关卡][品质] for 品质 in 阵容强度数据.角色品质},
                    "各品质强度": {品质: 关卡角色要求[最大关卡][品质] for 品质 in 阵容强度数据.角色品质},
                    "阵容强度": sum(关卡角色要求[最大关卡][品质] for 品质 in 阵容强度数据.角色品质)
                }
        
        # 返回包含阶段信息的字典
        return {
            "阶段": 阶段,
            "详细数据": 每日详细数据
        }

