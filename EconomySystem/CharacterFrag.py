"""计算每日的角色碎片获取"""
import math
from EconomySystem.EcoDataType import 角色碎片数据, 关卡阶段枚举, 阵容强度数据
from LubanData import 全局参数


class 角色碎片计算:
    """角色碎片计算"""
    # 引用角色品质数据
    角色品质 = 阵容强度数据.角色品质
    
    @classmethod
    def 计算每日碎片掉落量(cls, 当前关卡: int, 每日总碎片量: int = None) -> dict:
        """
        计算每日不同品质角色的碎片掉落量
        
        Args:
            当前关卡: 玩家当前所在关卡
            每日总碎片量: 每日总碎片产出量，如果为None则使用当前阶段的默认值
            
        Returns:
            dict: 包含各品质角色每日碎片掉落量的字典
        """
        # 获取当前关卡所在的关卡阶段
        当前阶段 = 关卡阶段枚举.获取关卡所在阶段(当前关卡)
        
        # 如果未指定每日总碎片量，使用当前阶段的默认值
        if 每日总碎片量 is None:
            每日总碎片量 = 当前阶段["每日碎片掉落量"]
        
        # 获取当前关卡阶段的碎片掉落比例
        碎片掉落比例 = 当前阶段["碎片掉落比例"]
        
        # 将百分比字符串转换为浮点数
        绿色比例 = float(碎片掉落比例[0].strip('%')) / 100
        蓝色比例 = float(碎片掉落比例[1].strip('%')) / 100
        紫色比例 = float(碎片掉落比例[2].strip('%')) / 100
        
        # 计算每日各品质碎片掉落量
        绿色碎片 = int(每日总碎片量 * 绿色比例)
        蓝色碎片 = int(每日总碎片量 * 蓝色比例)
        紫色碎片 = int(每日总碎片量 * 紫色比例)
        
        # 处理由于取整可能导致的总和不等于每日总碎片量的情况
        剩余碎片 = 每日总碎片量 - (绿色碎片 + 蓝色碎片 + 紫色碎片)
        if 剩余碎片 > 0:
            # 将剩余碎片分配给掉落比例最高的品质
            最高比例品质 = max([(绿色比例, "绿色"), (蓝色比例, "蓝色"), (紫色比例, "紫色")])[1]
            if 最高比例品质 == "绿色":
                绿色碎片 += 剩余碎片
            elif 最高比例品质 == "蓝色":
                蓝色碎片 += 剩余碎片
            else:
                紫色碎片 += 剩余碎片
        
        return {
            "绿色": 绿色碎片,
            "蓝色": 蓝色碎片,
            "紫色": 紫色碎片
        }
    
    @classmethod
    def 计算角色升级所需碎片(cls, 品质: str, 当前等级: int, 目标等级: int = None) -> int:
        """
        计算角色从当前等级升级到目标等级所需的碎片数量
        
        Args:
            品质: 角色品质（绿色、蓝色、紫色）
            当前等级: 角色当前等级
            目标等级: 角色目标等级，默认为当前等级+1
            
        Returns:
            int: 所需碎片数量
        """
        if 目标等级 is None:
            目标等级 = 当前等级 + 1
            
        if 当前等级 >= 目标等级:
            return 0
            
        if 品质 not in 角色碎片数据.角色碎片消耗公式:
            raise ValueError(f"无效的角色品质: {品质}")
            
        # 获取对应品质的碎片消耗公式
        碎片消耗公式 = 角色碎片数据.角色碎片消耗公式[品质]
        
        # 计算从当前等级到目标等级所需的总碎片数
        总碎片数 = 0
        for 等级 in range(当前等级, 目标等级):
            总碎片数 += 碎片消耗公式(等级)
            
        return 总碎片数
    
    @classmethod
    def 计算各阶段天数(cls, 当前关卡: int, 当前天数: int) -> dict:
        """
        计算玩家在各个关卡阶段花费的天数
        
        Args:
            当前关卡: 当前关卡数
            当前天数: 游戏进行的天数
            
        Returns:
            dict: 各关卡阶段花费的天数
        """
        # 获取当前关卡所在阶段
        当前阶段 = 关卡阶段枚举.获取关卡所在阶段(当前关卡)
        
        # 初始化阶段天数字典
        阶段天数 = {}
        for 阶段 in 关卡阶段枚举.获取所有阶段():
            阶段天数[阶段["名称"]] = 0
        
        # 计算各阶段天数
        剩余天数 = 当前天数
        
        for 阶段 in 关卡阶段枚举.获取所有阶段():
            if 阶段 == 当前阶段:
                # 当前阶段的天数
                阶段天数[阶段["名称"]] = 剩余天数
                break
            else:
                # 之前阶段的天数
                阶段天数[阶段["名称"]] = min(阶段["持续时间"], 剩余天数)
                剩余天数 -= 阶段天数[阶段["名称"]]
        
        return 阶段天数
    
    @classmethod
    def 计算角色养成进度(cls, 当前关卡: int, 当前天数: int, 每日总碎片量: int = None) -> dict:
        """
        根据当前关卡和天数计算角色养成进度，考虑不同关卡阶段的碎片掉落差异
        
        Args:
            当前关卡: 当前关卡数
            当前天数: 游戏进行的天数
            每日总碎片量: 每日总碎片产出量，如果为None则使用当前阶段的默认值
            
        Returns:
            dict: 各品质角色的养成进度，包括当前等级和所需碎片
        """
        # 计算每个阶段的天数
        阶段天数 = cls.计算各阶段天数(当前关卡, 当前天数)
        
        # 初始化累计碎片
        累计碎片 = {
            "绿色": 0,
            "蓝色": 0,
            "紫色": 0
        }
        
        # 计算每个阶段的碎片获取量
        for 阶段 in 关卡阶段枚举.获取所有阶段():
            天数 = 阶段天数[阶段["名称"]]
            if 天数 <= 0:
                continue
                
            # 获取该阶段代表关卡的碎片掉落量
            代表关卡 = (阶段["起始关卡"] + 阶段["结束关卡"]) // 2
            每日碎片掉落 = cls.计算每日碎片掉落量(代表关卡, 阶段["每日碎片掉落量"])
            
            # 累加各品质碎片
            for 品质 in cls.角色品质:
                累计碎片[品质] += 每日碎片掉落[品质] * 天数
        
        # 计算各品质角色可以达到的等级
        角色等级 = {}
        剩余碎片 = {}
        
        for 品质 in cls.角色品质:
            当前等级 = 1
            剩余 = 累计碎片[品质]
            
            # 逐级计算可以升到的最高等级
            while 当前等级 < 全局参数.角色等级上限:
                所需碎片 = 角色碎片数据.角色碎片消耗公式[品质](当前等级)
                if 剩余 >= 所需碎片:
                    剩余 -= 所需碎片
                    当前等级 += 1
                else:
                    break
            
            角色等级[品质] = 当前等级
            剩余碎片[品质] = 剩余
        
        return {
            "角色等级": 角色等级,
            "剩余碎片": 剩余碎片,
            "累计碎片": 累计碎片,
            "阶段天数": 阶段天数
        }
    
    @classmethod
    def 计算每日碎片来源分布(cls, 当前关卡: int, 每日总碎片量: int = 100) -> dict:
        """
        计算每日碎片的来源分布
        
        Args:
            当前关卡: 当前关卡数
            每日总碎片量: 每日总碎片产出量
            
        Returns:
            dict: 各品质角色碎片的来源分布
        """
        # 获取当前关卡的碎片掉落量
        每日碎片掉落 = cls.计算每日碎片掉落量(当前关卡, 每日总碎片量)
        
        # 获取碎片系统产出百分比
        产出百分比 = 角色碎片数据.角色碎片系统产出百分比
        
        # 计算各品质角色碎片的来源分布
        碎片来源分布 = {}
        
        for 品质 in cls.角色品质:
            碎片来源分布[品质] = {}
            for 来源, 比例 in 产出百分比.items():
                碎片来源分布[品质][来源] = int(每日碎片掉落[品质] * 比例)
                
            # 处理由于取整可能导致的总和不等于每日碎片掉落量的情况
            总和 = sum(碎片来源分布[品质].values())
            差值 = 每日碎片掉落[品质] - 总和
            
            if 差值 > 0:
                # 将差值添加到关卡宝箱中
                碎片来源分布[品质]["关卡宝箱"] += 差值
        
        return 碎片来源分布
    
    @classmethod
    def 计算碎片养成时间(cls, 当前关卡: int, 目标品质: str, 当前等级: int, 目标等级: int, 每日总碎片量: int = 100) -> int:
        """
        计算角色从当前等级升级到目标等级所需的时间
        
        Args:
            当前关卡: 当前关卡数
            目标品质: 角色品质
            当前等级: 角色当前等级
            目标等级: 角色目标等级
            每日总碎片量: 每日总碎片产出量
            
        Returns:
            int: 所需天数
        """
        # 计算所需碎片数量
        所需碎片 = cls.计算角色升级所需碎片(目标品质, 当前等级, 目标等级)
        
        # 计算每日获得的目标品质碎片数量
        每日碎片 = cls.计算每日碎片掉落量(当前关卡, 每日总碎片量)[目标品质]
        
        # 计算所需天数
        if 每日碎片 <= 0:
            return float('inf')  # 无法获得该品质碎片
        
        return math.ceil(所需碎片 / 每日碎片)
    
    @classmethod
    def 计算角色强度(cls, 品质: str, 等级: int) -> float:
        """
        计算角色强度
        
        Args:
            品质: 角色品质
            等级: 角色等级
            
        Returns:
            float: 角色强度
        """
        同等级的角色品质强度 = 阵容强度数据.同等级的角色品质强度
        角色品质成长倍率 = 阵容强度数据.角色品质成长倍率
        
        return 同等级的角色品质强度[品质] * (角色品质成长倍率[品质]**(等级-1))
    
    @classmethod
    def 计算达到目标强度时各品质角色的最低等级(cls, 目标强度: float) -> dict:
        """
        计算达到目标强度时各品质角色的最低等级
        
        Args:
            目标强度: 目标强度值
            
        Returns:
            dict: 各品质角色所需的最低等级（如不能达到则为0）
        """
        result = {}
        
        for 品质 in cls.角色品质:
            if 目标强度 <= 0:
                result[品质] = 0
                continue
                
            # 计算所需等级
            if cls.计算角色强度(品质, 全局参数.角色等级上限) < 目标强度:
                # 即使满级也无法达到目标强度
                result[品质] = 0
            else:
                # 计算最小等级
                min_level = math.ceil(math.log(目标强度 / 阵容强度数据.同等级的角色品质强度[品质], 
                                             阵容强度数据.角色品质成长倍率[品质])) + 1
                # 确保等级在合理范围内
                min_level = max(1, min(min_level, 全局参数.角色等级上限))
                result[品质] = min_level
        
        return result
    
    @classmethod
    def 生成角色养成报告(cls, 当前关卡: int, 当前天数: int, 每日总碎片量: int = 100) -> dict:
        """
        生成角色养成报告，包括当前养成进度和未来养成预测
        
        Args:
            当前关卡: 当前关卡数
            当前天数: 游戏进行的天数
            每日总碎片量: 每日总碎片产出量
            
        Returns:
            dict: 角色养成报告
        """
        # 计算当前养成进度（考虑阶段）
        养成进度 = cls.计算角色养成进度(当前关卡, 当前天数, 每日总碎片量)
        
        # 计算当前角色强度
        当前角色强度 = {}
        for 品质 in cls.角色品质:
            当前角色强度[品质] = cls.计算角色强度(品质, 养成进度["角色等级"][品质])
        
        # 计算达到下一关卡阶段所需的角色等级
        下一阶段 = None
        下一阶段强度要求 = None
        
        if 当前关卡 <= 30:
            下一阶段 = "31-60关"
            下一阶段强度要求 = {
                "绿色": 当前角色强度["绿色"] * 1.2,  # 绿色比蓝色高20%
                "蓝色": 当前角色强度["绿色"],
                "紫色": 当前角色强度["绿色"] / 1.2  # 绿色比紫色高20%
            }
        elif 当前关卡 <= 60:
            下一阶段 = "61-100关"
            下一阶段强度要求 = {
                "绿色": 当前角色强度["蓝色"] / 1.2,  # 蓝色比绿色高20%
                "蓝色": 当前角色强度["蓝色"],
                "紫色": 当前角色强度["蓝色"] * 1.2  # 紫色比蓝色高20%
            }
            
        # 计算达到下一阶段所需的等级和时间
        下一阶段所需等级 = {}
        下一阶段所需时间 = {}
        
        if 下一阶段:
            for 品质 in cls.角色品质:
                所需强度 = 下一阶段强度要求[品质]
                当前强度 = 当前角色强度[品质]
                
                # 计算达到所需强度的最低等级
                所需等级 = cls.计算达到目标强度时各品质角色的最低等级(所需强度)[品质]
                下一阶段所需等级[品质] = 所需等级
                
                # 计算达到所需等级的时间
                当前等级 = 养成进度["角色等级"][品质]
                if 所需等级 > 当前等级:
                    所需时间 = cls.计算碎片养成时间(当前关卡, 品质, 当前等级, 所需等级, 每日总碎片量)
                    下一阶段所需时间[品质] = 所需时间
                else:
                    下一阶段所需时间[品质] = 0
        
        return {
            "当前养成进度": 养成进度,
            "当前角色强度": 当前角色强度,
            "下一阶段": 下一阶段,
            "下一阶段强度要求": 下一阶段强度要求,
            "下一阶段所需等级": 下一阶段所需等级,
            "下一阶段所需时间": 下一阶段所需时间
        }


