    @classmethod
    def 获取单一阶段的碎片产出总量(cls, 阶段: 关卡阶段枚举, 阶段碎片总需求: dict) -> dict:
        """
        计算单一阶段各品质的碎片产出总量，不考虑上一阶段的累积产出
        
        Args:
            阶段: 关卡阶段枚举，表示要计算的阶段
            阶段碎片总需求: 该阶段的碎片总需求，格式为 {关卡数: {品质: 碎片总数}}
            
        Returns:
            dict: 该阶段各品质的碎片产出总量，格式为 {品质: 产出量}
        """
        # 初始化结果字典
        该阶段各品质的碎片产出总量 = {}
        
        # 获取阶段关卡范围
        起始关卡 = 阶段["起始关卡"]
        结束关卡 = 阶段["结束关卡"]
        
        # 获取当前阶段所有关卡的需求总量
        当前阶段需求总量 = {品质: 0 for 品质 in 阵容强度数据.角色品质}
        
        # 遍历当前阶段的所有关卡，找出每个品质的最大需求
        for 关卡数 in range(起始关卡, 结束关卡 + 1):
            if 关卡数 in 阶段碎片总需求:
                for 品质 in 阵容强度数据.角色品质:
                    当前阶段需求总量[品质] = max(当前阶段需求总量[品质], 阶段碎片总需求[关卡数][品质])
        
        # 产出量就等于需求总量
        for 品质 in 阵容强度数据.角色品质:
            该阶段各品质的碎片产出总量[品质] = 当前阶段需求总量[品质]
        
        return 该阶段各品质的碎片产出总量

