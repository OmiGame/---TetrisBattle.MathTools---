        """计算玩家实际的宝箱获得次数和金币相关数据
        
        Args:
            看钻石广告百分比: 看钻石广告的百分比 (0.0-1.0)
            看金币广告百分比: 看金币广告的百分比 (0.0-1.0)
            看精致宝箱广告百分比: 看精致宝箱广告的百分比 (0.0-1.0)
            钻石金币商店购买比例: 用钻石购买金币的比例 (0.0-1.0)
        
        Returns:
            dict: 包含普通宝箱、精致宝箱获得次数和金币相关数据的字典
        """
        import math
        
        # 获取无广告系统的固定钻石数
        无广告的系统数据 = 系统产出金币和碎片计算.获取无广告系统的字典()
        每日可获得钻石 = 0
        for 系统, 系统数据对象 in 无广告的系统数据.items():
            for 资源, 资源数值对象 in 系统数据对象.经济系统数据.items():
                if 资源 == "钻石":
                    每日可获得钻石 += 资源数值对象.数值
        
        # 加上通过广告获取的钻石数
        每日实际看钻石广告次数 = math.ceil(tables.Tb经济系统配置表.get("每日商店").经济系统数据["广告"].数值 * 看钻石广告百分比)
        每个广告的钻石数 = cls.获取每个广告产出的钻石()
        每日可获得钻石 += 每日实际看钻石广告次数 * 每个广告的钻石数

        # 根据钻石金币商店购买比例分配钻石用途
        购买宝箱的钻石 = 每日可获得钻石 * (1 - 钻石金币商店购买比例)
        购买金币的钻石 = 每日可获得钻石 * 钻石金币商店购买比例
        
        # 计算购买宝箱结果
        购买宝箱结果 = cls.计算玩家购买普通宝箱和精致宝箱的次数(购买宝箱的钻石)
        
        # 计算精致宝箱广告获得次数
        每日实际看精致宝箱广告次数 = math.ceil(tables.Tb经济系统配置表.get("精致宝箱").经济系统数据["广告"].数值 * 看精致宝箱广告百分比)

        # 计算金币相关数据
