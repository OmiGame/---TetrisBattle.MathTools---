"""计算总产出，包括各个系统的产出，角色碎片和金币、钻石"""
import math
from LubanData import  tables
from EconomySystem.TotalDemand import 角色碎片计算
from EconomySystem.EcoDataType import 角色养成数据

经济系统配置 = tables.Tb经济系统配置表.getDataMap()

class 系统产出金币和碎片计算:
    """系统产出计算"""

    @classmethod
    def 获取固定值字典(cls):
        """获取相关资源字典"""
        固定值字典 = {}
        for 经济系统,经济系统数据对象 in 经济系统配置.items():
            固定值字典[经济系统] = {}
            for 资源,资源数据对象 in 经济系统数据对象.经济系统数据.items():
                    if 资源数据对象.类型 == "固定":
                        固定值字典[经济系统][资源] = 资源数据对象.数值
        return 固定值字典
    
    @classmethod
    def 获取比例字典(cls):
        """获取相关资源字典"""
        比例字典 = {}
        for 经济系统,经济系统数据对象 in 经济系统配置.items():
            比例字典[经济系统] = {}
            for 资源,资源数据对象 in 经济系统数据对象.经济系统数据.items():
                if 资源数据对象.类型 == "比例":
                    比例字典[经济系统][资源] = 资源数据对象.数值
        return 比例字典
    
    @classmethod
    def 获取保底字典(cls):
        """获取保底字典"""
        保底字典 = {}
        for 经济系统,经济系统数据对象 in 经济系统配置.items():
            保底字典[经济系统] = {}
            for 资源,资源数据对象 in 经济系统数据对象.经济系统数据.items():
                if 资源数据对象.类型 == "保底":
                    保底字典[经济系统][资源] = 资源数据对象.数值
        return 保底字典
    
    @classmethod
    def 获取某资源的比例字典(cls,比例字典:dict,资源名称:str):
        """获取相关资源字典"""
        if not any(资源名称 in 资源字典 for 资源字典 in 比例字典.values()):
             raise ValueError(f"资源{资源名称}不存在")

        某资源的百分比字典 = {}
        for 经济系统,资源字典 in 比例字典.items():
            某资源的百分比字典[经济系统] = {}
            for 资源,数值 in 资源字典.items():
                if 资源 == 资源名称:
                    某资源的百分比字典[经济系统][资源] = 数值
        return 某资源的百分比字典
    
    @classmethod
    def 去除每日固定资源产出(cls,各品质碎片产出:dict,金币总产出:int,固定值字典:dict):
        """去除每日固定资源产出"""
        
        # 检查各品质碎片产出中的键名格式
        if "绿色" in 各品质碎片产出:
            # 初始化剩余需分配的资源字典
            剩余需分配的资源 = {
                "绿色碎片": 各品质碎片产出['绿色'],
                "蓝色碎片": 各品质碎片产出['蓝色'],
                "紫色碎片": 各品质碎片产出['紫色'],
                "金币": 金币总产出
            }
        else:
            # 如果无法识别键名格式，打印调试信息并抛出异常
            print(f"错误: 无法识别的碎片产出键名格式，各品质碎片产出内容: {各品质碎片产出}")
            raise KeyError("无法识别的碎片产出键名格式")
        
        # 从各系统的固定产出中减去
        for 经济系统, 资源字典 in 固定值字典.items():
            if 经济系统 != "精致宝箱":
                for 资源, 固定数值 in 资源字典.items():
                    if 资源 in 剩余需分配的资源:
                        剩余需分配的资源[资源] -= 固定数值
            else:
                for 资源, 固定数值 in 资源字典.items():
                    if 资源 in 剩余需分配的资源:
                        剩余需分配的资源[资源] -= 固定数值 * 经济系统配置.get("精致宝箱").获得次数
        
                
        
        # 确保没有负值
        for 资源 in 剩余需分配的资源:
            if 剩余需分配的资源[资源] < 0:
                print(f"警告: {资源}的固定产出总和超过了需求，已调整为0")
                剩余需分配的资源[资源] = 0
                
        return 剩余需分配的资源
    
    @classmethod
    def 获取碎片和金币在各系统中每日产出(cls):
        """获取碎片和金币在各系统中每日产出"""
        """
        returns:
            dict: 各系统每日产出，格式为 {阶段名称: {日: {经济系统: {资源: 产出量}}}}
        """
        
        # 获取固定值字典
        固定值字典 = cls.获取固定值字典()

        # 获取比例字典
        比例字典 = cls.获取比例字典()

        # 获取保底字典
        保底字典 = cls.获取保底字典()

        # 获取每日详细数据
        每日详细数据 = 角色碎片计算.生成完整每日角色需求详细数据()

        系统每日产出 = {}
        
        for 阶段名称,阶段数据 in 每日详细数据.items():
            # 初始化阶段每日产出
            系统每日产出[阶段名称] = {}
            for 日,日数据 in 阶段数据.items():
                # 初始化日每日产出
                系统每日产出[阶段名称][日] = {}
                # 去除每日固定资源产出
                剩余需分配的资源 = cls.去除每日固定资源产出(日数据["各品质碎片数"],日数据["每日金币总产出"],固定值字典)
                    
                # 将剩余资源按照比例分配到各个系统
                for 经济系统, 资源字典 in 比例字典.items():
                    系统每日产出[阶段名称][日][经济系统] = {}
                        
                    for 资源, 比例 in 资源字典.items():
                        #筛选了要处理的资源，不处理钻石
                        if 资源 in 剩余需分配的资源 and 资源 in ["绿色碎片", "蓝色碎片", "紫色碎片", "金币"]:
                            剩余资源量 = 剩余需分配的资源[资源]
                            # 根据资源类型应用产出系数
                            if 资源 in ["绿色碎片", "蓝色碎片", "紫色碎片"]:
                                    剩余资源量 *= 角色养成数据.碎片产出系数
                            elif 资源 == "金币":
                                    剩余资源量 *= 角色养成数据.金币产出系数
                                
                                # 根据比例分配
                            if 经济系统 != "普通宝箱" and 经济系统 != "精致宝箱":
                                系统每日产出[阶段名称][日][经济系统][资源] = math.ceil(剩余资源量 * 比例)
                                保底产出 = 保底字典[经济系统][f"保底{资源}"]
                                #和保底产出比较，保证最小产出
                                if 保底产出 > 系统每日产出[阶段名称][日][经济系统][资源]:
                                    系统每日产出[阶段名称][日][经济系统][资源] = 保底产出
                                
                            elif 经济系统 == "普通宝箱":
                                系统每日产出[阶段名称][日][经济系统][资源] = math.ceil(剩余资源量 * 比例/ 经济系统配置.get("普通宝箱").获得次数)
                                保底产出 = 保底字典[经济系统][f"保底{资源}"]
                                #和保底产出比较，保证最小产出
                                if 保底产出 > 系统每日产出[阶段名称][日][经济系统][资源]:
                                    系统每日产出[阶段名称][日][经济系统][资源] = 保底产出
                            elif 经济系统 == "精致宝箱":
                                系统每日产出[阶段名称][日][经济系统][资源] = math.ceil(剩余资源量 * 比例/ 经济系统配置.get("精致宝箱").获得次数)
                                保底产出 = 保底字典[经济系统][f"保底{资源}"]
                                #和保底产出比较，保证最小产出
                                if 保底产出 > 系统每日产出[阶段名称][日][经济系统][资源]:
                                    系统每日产出[阶段名称][日][经济系统][资源] = 保底产出
                    

                # 添加固定产出
                for 经济系统, 资源字典 in 固定值字典.items():
                    if 经济系统 not in 系统每日产出[阶段名称][日]:
                            系统每日产出[阶段名称][日][经济系统] = {}
                            
                    for 资源, 固定值 in 资源字典.items():
                        if 资源 in ["绿色碎片", "蓝色碎片", "紫色碎片", "金币"]:
                            if 资源 not in 系统每日产出[阶段名称][日][经济系统]:
                                    系统每日产出[阶段名称][日][经济系统][资源] = 0
                            系统每日产出[阶段名称][日][经济系统][资源] += 固定值

        return 系统每日产出
    
    @classmethod
    def 去掉阶段的系统每日产出(cls,系统每日产出:dict):
        """去掉阶段的系统每日产出"""
        """
        将包含阶段的产出字典转换为不包含阶段的产出字典
        
        Args:
            系统每日产出: dict, 格式为 {阶段名称: {日: {经济系统: {资源: 产出量}}}}
            
        Returns:
            dict: 不包含阶段的系统每日产出，格式为 {日: {经济系统: {资源: 产出量}}}
        """
        合并后的系统每日产出 = {}
        
        # 遍历所有阶段
        for 阶段名称, 阶段数据 in 系统每日产出.items():
            # 遍历该阶段的每一天
            for 日, 日数据 in 阶段数据.items():
                # 如果该日期还未添加到合并后的字典中，则初始化
                if 日 not in 合并后的系统每日产出:
                    合并后的系统每日产出[日] = {}
                
                # 遍历该日的每个经济系统
                for 经济系统, 系统数据 in 日数据.items():
                    # 如果该经济系统还未添加到该日的合并字典中，则初始化
                    if 经济系统 not in 合并后的系统每日产出[日]:
                        合并后的系统每日产出[日][经济系统] = {}
                    
                    # 遍历该经济系统的每种资源
                    for 资源, 产出量 in 系统数据.items():
                        # 如果该资源还未添加到该经济系统的合并字典中，则初始化为0
                        if 资源 not in 合并后的系统每日产出[日][经济系统]:
                            合并后的系统每日产出[日][经济系统][资源] = 0
                        
                        # 累加产出量
                        合并后的系统每日产出[日][经济系统][资源] += 产出量
        
        return 合并后的系统每日产出
    

class 系统产出钻石和钻石宝箱等级计算:
    """先计算钻石需求，主要是买金币和买宝箱"""
    """保底钻石产出很少，暂不考虑"""
