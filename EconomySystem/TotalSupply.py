"""计算总产出，包括各个系统的产出，角色碎片和金币、钻石"""
import math
from LubanData import  tables
from EconomySystem.TotalDemand import 角色碎片计算
from EconomySystem.EcoDataType import 角色养成数据

经济系统配置 = tables.Tb经济系统配置表.getDataMap()
具体每日详细数据 = 角色碎片计算.生成完整每日角色需求详细数据()
角色每日等级 = 角色碎片计算.获取角色每日等级(具体每日详细数据)

class 系统产出金币和碎片计算:
    """系统产出计算"""
    # 获取每日详细数据
    

    @classmethod
    def 获取固定值字典(cls):
        """获取相关资源字典"""
        固定值字典 = {}
        for 经济系统,经济系统数据对象 in 经济系统配置.items():
            固定值字典[经济系统] = {}
            for 资源,资源数据对象 in 经济系统数据对象.经济系统数据.items():
                    if 资源数据对象.类型 == "固定":
                        固定值字典[经济系统][资源] = 资源数据对象.数值
        return 固定值字典
    
    @classmethod
    def 获取比例字典(cls):
        """获取相关资源字典"""
        比例字典 = {}
        for 经济系统,经济系统数据对象 in 经济系统配置.items():
            比例字典[经济系统] = {}
            for 资源,资源数据对象 in 经济系统数据对象.经济系统数据.items():
                if 资源数据对象.类型 == "比例":
                    比例字典[经济系统][资源] = 资源数据对象.数值
        return 比例字典
    
    @classmethod
    def 获取保底字典(cls):
        """获取保底字典"""
        保底字典 = {}
        for 经济系统,经济系统数据对象 in 经济系统配置.items():
            保底字典[经济系统] = {}
            for 资源,资源数据对象 in 经济系统数据对象.经济系统数据.items():
                if 资源数据对象.类型 == "保底":
                    保底字典[经济系统][资源] = 资源数据对象.数值
        return 保底字典
    
    @classmethod
    def 获取某资源的比例字典(cls,比例字典:dict,资源名称:str):
        """获取相关资源字典"""
        if not any(资源名称 in 资源字典 for 资源字典 in 比例字典.values()):
             raise ValueError(f"资源{资源名称}不存在")

        某资源的百分比字典 = {}
        for 经济系统,资源字典 in 比例字典.items():
            某资源的百分比字典[经济系统] = {}
            for 资源,数值 in 资源字典.items():
                if 资源 == 资源名称:
                    某资源的百分比字典[经济系统][资源] = 数值
        return 某资源的百分比字典
    
    @classmethod
    def 去除每日固定资源产出(cls,各品质碎片产出:dict,金币总产出:int,固定值字典:dict):
        """去除每日固定资源产出"""
        
        # 检查各品质碎片产出中的键名格式
        if "绿色" in 各品质碎片产出:
            # 初始化剩余需分配的资源字典
            剩余需分配的资源 = {
                "绿色碎片": 各品质碎片产出['绿色'],
                "蓝色碎片": 各品质碎片产出['蓝色'],
                "紫色碎片": 各品质碎片产出['紫色'],
                "金币": 金币总产出
            }
        else:
            # 如果无法识别键名格式，打印调试信息并抛出异常
            print(f"错误: 无法识别的碎片产出键名格式，各品质碎片产出内容: {各品质碎片产出}")
            raise KeyError("无法识别的碎片产出键名格式")
        
        # 从各系统的固定产出中减去
        for 经济系统, 资源字典 in 固定值字典.items():
            if 经济系统 != "精致宝箱":
                for 资源, 固定数值 in 资源字典.items():
                    if 资源 in 剩余需分配的资源:
                        剩余需分配的资源[资源] -= 固定数值
            else:
                for 资源, 固定数值 in 资源字典.items():
                    if 资源 in 剩余需分配的资源:
                        剩余需分配的资源[资源] -= 固定数值 * 经济系统配置.get("精致宝箱").获得次数
        
                
        
        # 确保没有负值
        for 资源 in 剩余需分配的资源:
            if 剩余需分配的资源[资源] < 0:
                print(f"警告: {资源}的固定产出总和超过了需求，已调整为0")
                剩余需分配的资源[资源] = 0
                
        return 剩余需分配的资源
    
    @classmethod
    def 获取碎片和金币在各系统中每日产出(cls):
        """获取碎片和金币在各系统中每日产出"""
        """
        returns:
            dict: 各系统每日产出，格式为 {阶段名称: {日: {经济系统: {资源: 产出量}}}}
        """
        
        # 获取固定值字典
        固定值字典 = cls.获取固定值字典()

        # 获取比例字典
        比例字典 = cls.获取比例字典()

        # 获取保底字典
        保底字典 = cls.获取保底字典()

        每日详细数据 = 具体每日详细数据

        系统每日产出 = {}
        
        for 阶段名称,阶段数据 in 每日详细数据.items():
            # 初始化阶段每日产出
            系统每日产出[阶段名称] = {}
            for 日,日数据 in 阶段数据.items():
                # 初始化日每日产出
                系统每日产出[阶段名称][日] = {}
                # 去除每日固定资源产出
                剩余需分配的资源 = cls.去除每日固定资源产出(日数据["各品质碎片数"],日数据["每日金币总产出"],固定值字典)
                    
                # 将剩余资源按照比例分配到各个系统
                for 经济系统, 资源字典 in 比例字典.items():
                    系统每日产出[阶段名称][日][经济系统] = {}
                        
                    for 资源, 比例 in 资源字典.items():
                        #筛选了要处理的资源，不处理钻石
                        if 资源 in 剩余需分配的资源 and 资源 in ["绿色碎片", "蓝色碎片", "紫色碎片", "金币"]:
                            剩余资源量 = 剩余需分配的资源[资源]
                            # 根据资源类型应用产出系数
                            if 资源 in ["绿色碎片", "蓝色碎片", "紫色碎片"]:
                                    剩余资源量 *= 角色养成数据.碎片产出系数
                            elif 资源 == "金币":
                                    剩余资源量 *= 角色养成数据.金币产出系数
                                
                                # 根据比例分配
                            if 经济系统 != "普通宝箱" and 经济系统 != "精致宝箱":
                                系统每日产出[阶段名称][日][经济系统][资源] = math.ceil(剩余资源量 * 比例)
                                保底产出 = 保底字典[经济系统][f"保底{资源}"]
                                #和保底产出比较，保证最小产出
                                if 保底产出 > 系统每日产出[阶段名称][日][经济系统][资源]:
                                    系统每日产出[阶段名称][日][经济系统][资源] = 保底产出
                                
                            elif 经济系统 == "普通宝箱":
                                系统每日产出[阶段名称][日][经济系统][资源] = math.ceil(剩余资源量 * 比例/ 经济系统配置.get("普通宝箱").获得次数)
                                保底产出 = 保底字典[经济系统][f"保底{资源}"]
                                #和保底产出比较，保证最小产出
                                if 保底产出 > 系统每日产出[阶段名称][日][经济系统][资源]:
                                    系统每日产出[阶段名称][日][经济系统][资源] = 保底产出
                            elif 经济系统 == "精致宝箱":
                                系统每日产出[阶段名称][日][经济系统][资源] = math.ceil(剩余资源量 * 比例/ 经济系统配置.get("精致宝箱").获得次数)
                                保底产出 = 保底字典[经济系统][f"保底{资源}"]
                                #和保底产出比较，保证最小产出
                                if 保底产出 > 系统每日产出[阶段名称][日][经济系统][资源]:
                                    系统每日产出[阶段名称][日][经济系统][资源] = 保底产出
                    

                # 添加固定产出
                for 经济系统, 资源字典 in 固定值字典.items():
                    if 经济系统 not in 系统每日产出[阶段名称][日]:
                            系统每日产出[阶段名称][日][经济系统] = {}
                            
                    for 资源, 固定值 in 资源字典.items():
                        if 资源 in ["绿色碎片", "蓝色碎片", "紫色碎片", "金币"]:
                            if 资源 not in 系统每日产出[阶段名称][日][经济系统]:
                                    系统每日产出[阶段名称][日][经济系统][资源] = 0
                            系统每日产出[阶段名称][日][经济系统][资源] += 固定值

        return 系统每日产出
    
    @classmethod
    def 去掉阶段的系统每日产出(cls,系统每日产出:dict):
        """去掉阶段的系统每日产出"""
        """
        将包含阶段的产出字典转换为不包含阶段的产出字典
        
        Args:
            系统每日产出: dict, 格式为 {阶段名称: {日: {经济系统: {资源: 产出量}}}}
            
        Returns:
            dict: 不包含阶段的系统每日产出，格式为 {日: {经济系统: {资源: 产出量}}}
        """
        合并后的系统每日产出 = {}
        
        # 遍历所有阶段
        for 阶段名称, 阶段数据 in 系统每日产出.items():
            # 遍历该阶段的每一天
            for 日, 日数据 in 阶段数据.items():
                # 如果该日期还未添加到合并后的字典中，则初始化
                if 日 not in 合并后的系统每日产出:
                    合并后的系统每日产出[日] = {}
                
                # 遍历该日的每个经济系统
                for 经济系统, 系统数据 in 日数据.items():
                    # 如果该经济系统还未添加到该日的合并字典中，则初始化
                    if 经济系统 not in 合并后的系统每日产出[日]:
                        合并后的系统每日产出[日][经济系统] = {}
                    
                    # 遍历该经济系统的每种资源
                    for 资源, 产出量 in 系统数据.items():
                        # 如果该资源还未添加到该经济系统的合并字典中，则初始化为0
                        if 资源 not in 合并后的系统每日产出[日][经济系统]:
                            合并后的系统每日产出[日][经济系统][资源] = 0
                        
                        # 累加产出量
                        合并后的系统每日产出[日][经济系统][资源] += 产出量
        
        return 合并后的系统每日产出
    

class 系统产出钻石和钻石宝箱等级计算:
    """先计算钻石需求，主要是买金币和买宝箱"""
    """保底钻石产出很少，暂不考虑"""
    广告产出金币 = 500
    绿色碎片价格 = 经济系统配置.get("每日商店").钻石价格
    蓝色碎片价格 = 角色养成数据.计算碎片价值比()["蓝色碎片价值"]/角色养成数据.计算碎片价值比()["绿色碎片价值"] * 绿色碎片价格
    紫色碎片价格 = 角色养成数据.计算碎片价值比()["紫色碎片价值"]/角色养成数据.计算碎片价值比()["绿色碎片价值"] * 绿色碎片价格

    绿色碎片数量 = 经济系统配置.get("每日商店").经济系统数据["绿色碎片"].数值 * 经济系统配置.get("每日商店").获得次数
    蓝色碎片数量 = 经济系统配置.get("每日商店").经济系统数据["蓝色碎片"].数值 * 经济系统配置.get("每日商店").获得次数
    紫色碎片数量 = 经济系统配置.get("每日商店").经济系统数据["紫色碎片"].数值 * 经济系统配置.get("每日商店").获得次数

    每日商店消耗钻石 = 绿色碎片数量 * 绿色碎片价格 + 蓝色碎片数量 * 蓝色碎片价格 + 紫色碎片数量 * 紫色碎片价格

    金币兑换 = 经济系统配置.get("金币商店").钻石价格/(经济系统配置.get("金币商店").经济系统数据["金币"].数值 - 广告产出金币*经济系统配置.get("金币商店").经济系统数据["广告"].数值)
    金币价格 = 经济系统配置.get("金币商店").钻石价格
    普通宝箱价格 = 经济系统配置.get("普通宝箱").钻石价格
    精致宝箱价格 = 经济系统配置.get("精致宝箱").钻石价格

    普通宝箱购买次数 = 经济系统配置.get("普通宝箱").购买次数
    精致宝箱购买次数 = 经济系统配置.get("精致宝箱").购买次数

    钻石需求 = 每日商店消耗钻石 + 普通宝箱价格 * 普通宝箱购买次数 + 精致宝箱价格 * 精致宝箱购买次数 + 金币价格

    产出钻石广告数量 = 经济系统配置.get("每日商店").经济系统数据["广告"].数值

    @classmethod
    def 获取钻石固定值资源字典(cls):
        """获取钻石固定值资源字典"""
        固定值字典 = 系统产出金币和碎片计算.获取固定值字典()
        钻石固定值字典 = {}
        for 系统,资源字典 in 固定值字典.items():
            for 资源,资源数值 in 资源字典.items():
                if 资源 == "钻石":
                    钻石固定值字典[系统] = 资源数值
        return 钻石固定值字典

    @classmethod
    def 获取钻石比例值资源字典(cls):
        """获取钻石比例值资源字典"""
        比例值字典 = 系统产出金币和碎片计算.获取比例字典()
        钻石比例值字典 = {}
        for 系统,资源字典 in 比例值字典.items():
            for 资源,资源数值 in 资源字典.items():
                if 资源 == "钻石":
                    钻石比例值字典[系统] = 资源数值
        return 钻石比例值字典
    
    @classmethod
    def 获取可分配钻石数值(cls):
        """获取实际可以分配的钻石数值"""
        钻石固定值字典 = cls.获取钻石固定值资源字典()
        钻石比例值字典 = cls.获取钻石比例值资源字典()
        
        钻石固定总产出 = 0
        for 系统,产出数值 in 钻石固定值字典.items():
            钻石固定总产出 += 产出数值
        
        剩余可分配钻石 = cls.钻石需求- 钻石固定总产出

        系统产出比例 = 0
        for 系统,比例数值 in 钻石比例值字典.items():
            系统产出比例 +=比例数值
        
        剩余可分配钻石 *=(1-系统产出比例)
        return 剩余可分配钻石
    
    @classmethod
    def 获取每个广告产出的钻石(cls):
        剩余可分配钻石 = cls.获取可分配钻石数值()
        每个广告产出的钻石 = 剩余可分配钻石/cls.产出钻石广告数量
        return 每个广告产出的钻石
    
    @classmethod
    def 计算精致宝箱升级经验值(cls,合并后的系统每日产出:dict):
        """计算精致宝箱升级所需经验值
        
        经验值的计算以蓝色角色的升级天数为基准，因为蓝色品质的角色在一段周期内升级比较均匀，前期升级的频率也差不多。
        当蓝色品质角色等级升级时，宝箱等级也提升，升级所需经验是之前蓝色品质没升级的天数 * 每日花费钻石。
        
        Returns:
            dict: 宝箱等级字典，键为宝箱等级，值为升级到该等级所需的经验值
        """
        每日购买精致宝箱次数 = 经济系统配置.get("精致宝箱").购买次数
        精致宝箱价格 = 经济系统配置.get("精致宝箱").钻石价格
        每日花费钻石 = 每日购买精致宝箱次数 * 精致宝箱价格
        当前角色等级 = 0
        宝箱等级 = 1
        宝箱等级字典 = {}
        等级相同日 = 0
        
        for 日, 各品质等级数据 in 角色每日等级.items():
            for 品质, 等级 in 各品质等级数据.items():
                if 品质 == "蓝色":
                    # 初始化当前角色等级
                    if 当前角色等级 == 0:
                        当前角色等级 = 等级
                        等级相同日 = 1
                        continue
                        
                    # 判断角色等级是否变化
                    if 等级 == 当前角色等级:
                        # 等级没变，累加天数
                        等级相同日 += 1
                    elif 等级 > 当前角色等级:
                        # 等级升级，宝箱等级也提升
                        宝箱等级 += 1
                        # 计算升级所需经验 = 之前等级相同的天数 * 每日花费钻石
                        宝箱等级字典[宝箱等级] = { "升到此所需经验值": 每日花费钻石 * 等级相同日, "产出数据": 合并后的系统每日产出[日]["精致宝箱"]}
                        
                        # 打印调试信息
                        print(f"宝箱等级升至：{宝箱等级}，经过天数：{等级相同日}，经验值：{宝箱等级字典[宝箱等级]['升到此所需经验值']},产出数据：{宝箱等级字典[宝箱等级]['产出数据']}")
                        
                        # 更新当前角色等级和重置计数器
                        当前角色等级 = 等级
                        等级相同日 = 1
        #对不同宝箱等级的升级所需经验值做平滑处理，避免经验值突然降低
        
        # ----
        # 平滑处理经验值，保持总花费不变，从1600开始递增
        # ----
        if len(宝箱等级字典) > 1:
            # 计算总花费和宝箱等级数量
            总花费 = sum(数据["升到此所需经验值"] for 数据 in 宝箱等级字典.values())
            宝箱等级数量 = len(宝箱等级字典)
            
            # 设定起始经验值为1600
            首项经验值 = 1600
            
            # 根据等差数列公式计算合适的公差
            # S = n*(2*a1 + (n-1)*d)/2 = 总花费
            # 解得：d = (2*总花费 - 2*n*a1)/(n*(n-1))
            if 宝箱等级数量 > 1:
                公差 = (2 * 总花费 - 2 * 宝箱等级数量 * 首项经验值) // (宝箱等级数量 * (宝箱等级数量 - 1))
                # 确保公差为正数且为合理的递增单位（100的倍数）
                公差 = max(100, (公差 // 100) * 100)
            else:
                公差 = 100
            
            # 重新分配经验值，保持产出数据不变
            平滑后的宝箱等级字典 = {}
            当前等级 = 1
            
            for 原宝箱等级 in sorted(宝箱等级字典.keys()):
                新经验值 = 首项经验值 + (当前等级 - 1) * 公差
                平滑后的宝箱等级字典[当前等级 + 1] = {
                    "升到此所需经验值": 新经验值,
                    "产出数据": 宝箱等级字典[原宝箱等级]["产出数据"],
                    "原始经验值": 宝箱等级字典[原宝箱等级]["升到此所需经验值"]
                }
                当前等级 += 1
            
            # 调整最后一级经验值以保持总花费不变
            新总花费 = sum(数据["升到此所需经验值"] for 数据 in 平滑后的宝箱等级字典.values())
            花费差额 = 总花费 - 新总花费
            
            if 花费差额 != 0:
                最高等级 = max(平滑后的宝箱等级字典.keys())
                平滑后的宝箱等级字典[最高等级]["升到此所需经验值"] += 花费差额
                # 确保调整后的经验值仍为100的倍数
                调整后经验值 = 平滑后的宝箱等级字典[最高等级]["升到此所需经验值"]
                平滑后的宝箱等级字典[最高等级]["升到此所需经验值"] = (调整后经验值 // 100) * 100
            
            print("\n=== 平滑处理前后对比 ===")
            print("原始经验值分配：")
            for 等级, 数据 in sorted(宝箱等级字典.items()):
                print(f"等级{等级}: {数据['升到此所需经验值']}")
            
            print(f"\n平滑后经验值分配（起始1600，递增{公差}）：")
            for 等级, 数据 in sorted(平滑后的宝箱等级字典.items()):
                print(f"等级{等级}: {数据['升到此所需经验值']} (原始: {数据['原始经验值']})")
            
            print(f"\n总花费保持不变: {总花费}")
            新总花费验证 = sum(数据["升到此所需经验值"] for 数据 in 平滑后的宝箱等级字典.values())
            print(f"平滑后总花费: {新总花费验证}")
            print(f"使用的递增单位: {公差}")
            
            宝箱等级字典 = 平滑后的宝箱等级字典
            
        return 宝箱等级字典
    


