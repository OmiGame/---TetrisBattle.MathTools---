            新手末项 = max(新手首项 + 1, int((新手阶段最小值 + 新手阶段最大值) / 2))
            
            # 计算新手阶段的公差
            if 新手阶段关卡数 > 1:
                # 使用首项和末项来计算公差，确保范围合理
                理想公差 = (新手末项 - 新手首项) / (新手阶段关卡数 - 1)
                新手公差 = max(0, 理想公差)
            else:
                新手公差 = 0
            
            # 生成新手阶段平滑数据
            新手平滑列表 = []
            for i in range(新手阶段关卡数):
                产出 = 新手首项 + i * 新手公差
                新手平滑列表.append(max(1, int(产出)))
            
            # 调整新手阶段总和以保持总产出不变
            新手当前总和 = sum(新手平滑列表)
            新手差额 = 新手阶段原始总产出 - 新手当前总和
            
            if 新手差额 != 0:
                # 按比例调整每个值，保持递增趋势
                调整比例 = 新手阶段原始总产出 / 新手当前总和 if 新手当前总和 > 0 else 1
                for i in range(新手阶段关卡数):
                    新手平滑列表[i] = max(1, int(新手平滑列表[i] * 调整比例))
                
                # 精确调整剩余差额
                新手当前总和 = sum(新手平滑列表)
                剩余差额 = 新手阶段原始总产出 - 新手当前总和
                
                if 剩余差额 != 0:
                    每关调整 = 剩余差额 // 新手阶段关卡数
                    剩余调整 = 剩余差额 % 新手阶段关卡数
                    
                    for i in range(新手阶段关卡数):
                        新手平滑列表[i] += 每关调整
                        if i >= 新手阶段关卡数 - abs(剩余调整):
                            if 剩余差额 > 0:
                                新手平滑列表[i] += 1
                            elif 剩余差额 < 0 and 新手平滑列表[i] > 1:
                                新手平滑列表[i] -= 1
                        新手平滑列表[i] = max(1, 新手平滑列表[i])
            
            # 应用新手阶段数据
            for i, 关卡 in enumerate(新手阶段关卡):
                平滑后产出数据[关卡] = 新手平滑列表[i]
        
