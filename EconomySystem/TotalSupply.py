    # region 计算平滑产出序列
    @classmethod
    def _计算平滑产出序列(cls, 原始产出列表: list, 总产出: int, 总关卡数: int, 资源类型: str) -> list:
        """计算平滑的产出序列"""
        if 总关卡数 <= 1:
            return 原始产出列表
        
        # 计算平均产出
        平均产出 = 总产出 / 总关卡数
        
        # 根据资源类型调整平滑策略
        if 资源类型 == "金币":
            # 金币产出相对稳定，首项设为平均值的80%
            首项产出 = max(1, int(平均产出 * 0.8))
        else:
            # 碎片类资源，首项设为平均值的60%，确保有一定的递增趋势
            首项产出 = max(1, int(平均产出 * 0.6))
        
        # 计算公差：d = (2*S - 2*n*a1)/(n*(n-1))
        理论公差 = (2 * 总产出 - 2 * 总关卡数 * 首项产出) / (总关卡数 * (总关卡数 - 1))
        
        # 调整公差，确保合理的递增
        if 理论公差 < 0:
            # 如果公差为负，说明首项过大，重新调整
            首项产出 = max(1, int(平均产出 * 0.5))
            理论公差 = (2 * 总产出 - 2 * 总关卡数 * 首项产出) / (总关卡数 * (总关卡数 - 1))
        
        # 确保公差至少为0.1，避免完全平坦
        公差 = max(0.1, 理论公差)
        
        # 生成平滑的产出数据（使用等差数列）
        平滑产出列表 = []
        for i in range(总关卡数):
            平滑产出 = 首项产出 + i * 公差
            平滑产出列表.append(max(1, int(平滑产出)))  # 确保最小为1
        
        # 调整总和以保持不变
        当前总和 = sum(平滑产出列表)
        差额 = 总产出 - 当前总和
        
        if 差额 != 0:
            # 将差额分配到各关卡
            每关调整 = 差额 // 总关卡数
            剩余差额 = 差额 % 总关卡数
            
            for i in range(总关卡数):
                平滑产出列表[i] += 每关调整
                # 将剩余差额分配给后面的关卡
                if i >= 总关卡数 - abs(剩余差额):
                    if 剩余差额 > 0:
                        平滑产出列表[i] += 1
                    elif 剩余差额 < 0 and 平滑产出列表[i] > 1:
                        平滑产出列表[i] -= 1
                
                # 确保最小为1
                平滑产出列表[i] = max(1, 平滑产出列表[i])
        
        return 平滑产出列表
    # endregion

