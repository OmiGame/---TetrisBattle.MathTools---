    # region 计算实际宝箱每日产出
    @classmethod
    def 计算实际宝箱每日产出(cls, 日数: int) -> Dict[str, Dict[str, int]]:
        """计算指定天数的实际宝箱产出数据
        
        Args:
            日数: 计算的天数
            
        Returns:
            Dict: 包含普通宝箱和精致宝箱每日产出的字典
            
        Note:
            返回的是单次购买宝箱的产出数据，不包含获得次数的乘法
        """

        # 加载宝箱数据
        宝箱数据 = cls._加载宝箱数据()
        
        # 计算累积经验值，这里已经考虑到了宝石投放的折算，和购买前天商品的折算
        每日宝箱升级经验值 = cls._计算每日宝箱升级经验值()
        累积经验值 = 每日宝箱升级经验值 * 日数
        
        # 计算当前宝箱等级
        当前宝箱等级 = cls._计算累积经验值对应的宝箱等级(累积经验值)
        
        # 获取玩家实际获得的宝箱次数
        玩家实际获得数据 = cls._玩家实际获得宝箱和金币数()
        普通宝箱获得次数 = 玩家实际获得数据["普通宝箱获得次数"]
        精致宝箱获得次数 = 玩家实际获得数据["精致宝箱获得次数"]
        
        # 获取对应等级的宝箱产出
        普通宝箱单次产出 = cls._根据等级获取宝箱产出(宝箱数据["普通宝箱"], 当前宝箱等级)
        精致宝箱单次产出 = cls._根据等级获取宝箱产出(宝箱数据["精致宝箱"], 当前宝箱等级)
        
        # 计算每日总产出
        普通宝箱每日产出 = {}
        精致宝箱每日产出 = {}
        
        for 资源, 单次产出 in 普通宝箱单次产出.items():
            普通宝箱每日产出[资源] = 单次产出
        
        for 资源, 单次产出 in 精致宝箱单次产出.items():
            精致宝箱每日产出[资源] = 单次产出
        
        print(f"\n=== 第{日数}天实际宝箱产出计算 ===")
        print(f"累积经验值: {累积经验值}")
        print(f"当前宝箱等级: {当前宝箱等级}")
        print(f"普通宝箱获得次数: {普通宝箱获得次数}")
        print(f"精致宝箱获得次数: {精致宝箱获得次数}")
        print(f"普通宝箱每日产出: {普通宝箱每日产出}")
        print(f"精致宝箱每日产出: {精致宝箱每日产出}")
        
        return {
            "普通宝箱": 普通宝箱每日产出,
            "精致宝箱": 精致宝箱每日产出
        }
