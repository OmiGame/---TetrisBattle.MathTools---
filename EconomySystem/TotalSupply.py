    # region 对宝箱产出进行平滑处理
    @classmethod
    def _对宝箱产出进行平滑处理(cls, 原始每关宝箱产出: Dict) -> Dict:
        """对宝箱产出进行平滑处理，避免数值跳跃"""
        if not 原始每关宝箱产出:
            return 原始每关宝箱产出
        
        print("\n=== 开始对每关宝箱产出进行分阶段平滑处理 ===")
        
        # 获取关卡列表和宝箱名称列表
        关卡列表 = sorted(原始每关宝箱产出.keys())
        总关卡数 = len(关卡列表)
        
        if 总关卡数 <= 1:
            print("关卡数量不足，跳过平滑处理")
            return 原始每关宝箱产出
        
        # 定义阶段分界点
        新手阶段结束关卡 = 平滑处理的分段数值
        
        # 分割关卡列表
        新手阶段关卡 = [关卡 for 关卡 in 关卡列表 if 关卡 <= 新手阶段结束关卡]
        进阶阶段关卡 = [关卡 for 关卡 in 关卡列表 if 关卡 > 新手阶段结束关卡]
        
        print(f"总关卡数: {总关卡数}")
        print(f"新手阶段关卡: {新手阶段关卡} (共{len(新手阶段关卡)}关)")
        print(f"进阶阶段关卡: {进阶阶段关卡[0]}-{进阶阶段关卡[-1]} (共{len(进阶阶段关卡)}关)")
        
        # 获取所有宝箱名称（从第一关获取）
        第一关数据 = 原始每关宝箱产出[关卡列表[0]]
        宝箱名称列表 = list(第一关数据.keys())
        
        # 获取所有资源类型（排除解锁波次）
        所有资源类型 = set()
        for 关卡数据 in 原始每关宝箱产出.values():
            for 宝箱数据 in 关卡数据.values():
                for 资源名称 in 宝箱数据.keys():
                    if 资源名称 != "解锁波次":
                        所有资源类型.add(资源名称)
        
        # 复制原始数据作为基础
        平滑后的数据 = {}
        for 关卡 in 关卡列表:
            平滑后的数据[关卡] = {}
            for 宝箱名称 in 宝箱名称列表:
                平滑后的数据[关卡][宝箱名称] = 原始每关宝箱产出[关卡][宝箱名称].copy()
        
        print(f"宝箱类型: {宝箱名称列表}")
        print(f"需要平滑的资源类型: {sorted(所有资源类型)}")
        
        # ----
        # 对每个宝箱的每种资源进行分阶段平滑处理
        # ----
        for 宝箱名称 in 宝箱名称列表:
            print(f"\n处理宝箱: {宝箱名称}")
            
            for 资源类型 in 所有资源类型:
                # 对固定值资源（体力、钻石）进行特殊处理
                if 资源类型 in ["体力", "钻石"]:
                    print(f"  {资源类型}: 固定值资源，保持原值不变")
                    continue
                
                # 收集该宝箱该资源在所有关卡的原始产出数据
                原始产出列表 = []
                for 关卡 in 关卡列表:
                    原始产出 = 原始每关宝箱产出[关卡][宝箱名称].get(资源类型, 0)
                    原始产出列表.append(原始产出)
                
                # 计算总产出
                总产出 = sum(原始产出列表)
                
                if 总产出 == 0:
                    print(f"  {资源类型}: 总产出为0，跳过平滑处理")
                    continue
                
                # 分别处理两个阶段
                平滑后产出数据 = cls._分阶段平滑处理资源(
                    原始每关宝箱产出, 宝箱名称, 资源类型, 
                    新手阶段关卡, 进阶阶段关卡, 总产出
                )
                
                # 应用平滑后的产出数据
                for 关卡, 产出量 in 平滑后产出数据.items():
                    平滑后的数据[关卡][宝箱名称][资源类型] = 产出量
                
                # 输出调试信息
                新手阶段原始 = [原始每关宝箱产出[关卡][宝箱名称].get(资源类型, 0) for 关卡 in 新手阶段关卡]
                进阶阶段原始 = [原始每关宝箱产出[关卡][宝箱名称].get(资源类型, 0) for 关卡 in 进阶阶段关卡]
                新手阶段平滑 = [平滑后产出数据[关卡] for 关卡 in 新手阶段关卡]
                进阶阶段平滑 = [平滑后产出数据[关卡] for 关卡 in 进阶阶段关卡]
                
                if 新手阶段原始:
                    新手原始范围 = f"{min(新手阶段原始)}-{max(新手阶段原始)}"
                    新手平滑范围 = f"{min(新手阶段平滑)}-{max(新手阶段平滑)}"
                else:
                    新手原始范围 = "无"
                    新手平滑范围 = "无"
                
                if 进阶阶段原始:
                    进阶原始范围 = f"{min(进阶阶段原始)}-{max(进阶阶段原始)}"
                    进阶平滑范围 = f"{min(进阶阶段平滑)}-{max(进阶阶段平滑)}"
                else:
                    进阶原始范围 = "无"
                    进阶平滑范围 = "无"
                
                print(f"  {资源类型}:")
                print(f"    新手阶段(1-6关): 原始{新手原始范围} → 平滑{新手平滑范围}")
                print(f"    进阶阶段(7-100关): 原始{进阶原始范围} → 平滑{进阶平滑范围}")
                print(f"    总产出保持: {总产出}")
        
        print(f"\n=== 分阶段平滑处理完成 ===")
        return 平滑后的数据
    # endregion

    # region 分阶段平滑处理资源
    @classmethod
    def _分阶段平滑处理资源(cls, 原始每关宝箱产出: Dict, 宝箱名称: str, 资源类型: str, 
                        新手阶段关卡: list, 进阶阶段关卡: list, 总产出: int) -> Dict:
        """分阶段对资源进行平滑处理"""
        平滑后产出数据 = {}
        
        # 收集所有关卡的原始产出数据，用于确定最小值和最大值
        所有关卡 = 新手阶段关卡 + 进阶阶段关卡
        原始产出列表 = []
        
        for 关卡 in 所有关卡:
            原始产出 = 原始每关宝箱产出[关卡][宝箱名称].get(资源类型, 0)
            原始产出列表.append(原始产出)
        
        if not 原始产出列表 or 总产出 == 0:
            # 如果没有产出数据，直接返回0值
            for 关卡 in 所有关卡:
                平滑后产出数据[关卡] = 0
            return 平滑后产出数据
        
        # 获取原始数据的最小值和最大值
        原始最小值 = min(原始产出列表)
        原始最大值 = max(原始产出列表)
        
        print(f"    {资源类型} 原始范围: {原始最小值}-{原始最大值}")
        
        # 计算各阶段的原始产出总量
        新手阶段原始总产出 = 0
        进阶阶段原始总产出 = 0
        
        for 关卡 in 新手阶段关卡:
            新手阶段原始总产出 += 原始每关宝箱产出[关卡][宝箱名称].get(资源类型, 0)
        
        for 关卡 in 进阶阶段关卡:
            进阶阶段原始总产出 += 原始每关宝箱产出[关卡][宝箱名称].get(资源类型, 0)
        
        # ----
        # 新手阶段平滑处理（1-6关）
        # ----
        if 新手阶段关卡 and 新手阶段原始总产出 > 0:
            新手阶段关卡数 = len(新手阶段关卡)
            
            # 新手阶段的起始值接近原始最小值，结束值适中
            新手阶段原始产出列表 = [原始每关宝箱产出[关卡][宝箱名称].get(资源类型, 0) for 关卡 in 新手阶段关卡]
            新手阶段最小值 = min(新手阶段原始产出列表) if 新手阶段原始产出列表 else 原始最小值
            新手阶段最大值 = max(新手阶段原始产出列表) if 新手阶段原始产出列表 else 原始最小值
            
            # 新手阶段使用较小的范围，从接近最小值开始，到适中值结束
            新手首项 = max(1, 新手阶段最小值)
            新手末项 = max(新手首项 + 1, int((新手阶段最小值 + 新手阶段最大值) / 2))
            
            # 计算新手阶段的公差
            if 新手阶段关卡数 > 1:
                # 使用首项和末项来计算公差，确保范围合理
                理想公差 = (新手末项 - 新手首项) / (新手阶段关卡数 - 1)
                新手公差 = max(0, 理想公差)
            else:
                新手公差 = 0
            
            # 生成新手阶段平滑数据
            新手平滑列表 = []
            for i in range(新手阶段关卡数):
                产出 = 新手首项 + i * 新手公差
                新手平滑列表.append(max(1, int(产出)))
            
            # 调整新手阶段总和以保持总产出不变
            新手当前总和 = sum(新手平滑列表)
            新手差额 = 新手阶段原始总产出 - 新手当前总和
            
            if 新手差额 != 0:
                # 按比例调整每个值，保持递增趋势
                调整比例 = 新手阶段原始总产出 / 新手当前总和 if 新手当前总和 > 0 else 1
                for i in range(新手阶段关卡数):
                    新手平滑列表[i] = max(1, int(新手平滑列表[i] * 调整比例))
                
                # 精确调整剩余差额
                新手当前总和 = sum(新手平滑列表)
                剩余差额 = 新手阶段原始总产出 - 新手当前总和
                
                if 剩余差额 != 0:
                    每关调整 = 剩余差额 // 新手阶段关卡数
                    剩余调整 = 剩余差额 % 新手阶段关卡数
                    
                    for i in range(新手阶段关卡数):
                        新手平滑列表[i] += 每关调整
                        if i >= 新手阶段关卡数 - abs(剩余调整):
                            if 剩余差额 > 0:
                                新手平滑列表[i] += 1
                            elif 剩余差额 < 0 and 新手平滑列表[i] > 1:
                                新手平滑列表[i] -= 1
                        新手平滑列表[i] = max(1, 新手平滑列表[i])
            
            # 应用新手阶段数据
            for i, 关卡 in enumerate(新手阶段关卡):
                平滑后产出数据[关卡] = 新手平滑列表[i]
        
        # ----
        # 进阶阶段平滑处理（7-100关）
        # ----
        if 进阶阶段关卡 and 进阶阶段原始总产出 > 0:
            进阶阶段关卡数 = len(进阶阶段关卡)
            
            # 进阶阶段的产出范围从新手阶段结束值到原始最大值
            进阶阶段原始产出列表 = [原始每关宝箱产出[关卡][宝箱名称].get(资源类型, 0) for 关卡 in 进阶阶段关卡]
            进阶阶段最小值 = min(进阶阶段原始产出列表) if 进阶阶段原始产出列表 else 原始最小值
            进阶阶段最大值 = max(进阶阶段原始产出列表) if 进阶阶段原始产出列表 else 原始最大值
            
            # 进阶阶段的起始值与新手阶段衔接，结束值接近原始最大值
            if 新手阶段关卡:
                新手最后产出 = 平滑后产出数据[新手阶段关卡[-1]]
                进阶首项 = max(新手最后产出, 进阶阶段最小值)
            else:
                进阶首项 = max(1, 进阶阶段最小值)
            
            # 进阶阶段的结束值接近原始最大值
            进阶末项 = max(进阶首项 + 1, 进阶阶段最大值)
            
            # 计算进阶阶段的公差
            if 进阶阶段关卡数 > 1:
                # 使用首项和末项来计算公差，确保能达到最大值附近
                理想公差 = (进阶末项 - 进阶首项) / (进阶阶段关卡数 - 1)
                进阶公差 = max(0, 理想公差)
            else:
                进阶公差 = 0
            
            # 生成进阶阶段平滑数据
            进阶平滑列表 = []
            for i in range(进阶阶段关卡数):
                产出 = 进阶首项 + i * 进阶公差
                进阶平滑列表.append(max(1, int(产出)))
            
            # 调整进阶阶段总和以保持总产出不变
            进阶当前总和 = sum(进阶平滑列表)
            进阶差额 = 进阶阶段原始总产出 - 进阶当前总和
            
            if 进阶差额 != 0:
                # 按比例调整每个值，保持递增趋势
                调整比例 = 进阶阶段原始总产出 / 进阶当前总和 if 进阶当前总和 > 0 else 1
                for i in range(进阶阶段关卡数):
                    进阶平滑列表[i] = max(1, int(进阶平滑列表[i] * 调整比例))
                
                # 精确调整剩余差额
                进阶当前总和 = sum(进阶平滑列表)
                剩余差额 = 进阶阶段原始总产出 - 进阶当前总和
                
                if 剩余差额 != 0:
                    每关调整 = 剩余差额 // 进阶阶段关卡数
                    剩余调整 = 剩余差额 % 进阶阶段关卡数
                    
                    for i in range(进阶阶段关卡数):
                        进阶平滑列表[i] += 每关调整
                        if i >= 进阶阶段关卡数 - abs(剩余调整):
                            if 剩余差额 > 0:
                                进阶平滑列表[i] += 1
                            elif 剩余差额 < 0 and 进阶平滑列表[i] > 1:
                                进阶平滑列表[i] -= 1
                        进阶平滑列表[i] = max(1, 进阶平滑列表[i])
            
            # 应用进阶阶段数据
            for i, 关卡 in enumerate(进阶阶段关卡):
                平滑后产出数据[关卡] = 进阶平滑列表[i]
        
        # 处理没有产出的关卡
        for 关卡 in 所有关卡:
            if 关卡 not in 平滑后产出数据:
                平滑后产出数据[关卡] = 0
        
        # 验证平滑后的范围
        平滑后产出列表 = [平滑后产出数据[关卡] for 关卡 in 所有关卡]
        平滑后最小值 = min(平滑后产出列表) if 平滑后产出列表 else 0
        平滑后最大值 = max(平滑后产出列表) if 平滑后产出列表 else 0
        
        print(f"    {资源类型} 平滑后范围: {平滑后最小值}-{平滑后最大值} (目标: {原始最小值}-{原始最大值})")
        
        return 平滑后产出数据
