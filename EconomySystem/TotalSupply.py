class 波次产出系统的宝箱产出计算:
    """波次产出系统的宝箱产出计算
    
    负责将每天的波次奖励系统产出数据转换为每关的产出数据，
    然后将每关的产出分配到三个不同的宝箱中。
    """
    
    @classmethod
    def 获取波次奖励系统每日产出(cls) -> Dict:
        """获取波次奖励系统的每日产出数据
        
        Returns:
            dict: 波次奖励系统每日产出，格式为 {日: {资源: 产出量}}
        """
        合并后的系统每日产出 = 系统产出金币和碎片计算.获取合并后的系统每日产出()
        
        波次奖励每日产出 = {}
        
        for 日, 日数据 in 合并后的系统每日产出.items():
            if "波次奖励" in 日数据:
                波次奖励每日产出[日] = 日数据["波次奖励"]
            else:
                # 如果某天没有波次奖励数据，设为空字典
                波次奖励每日产出[日] = {}
        
        return 波次奖励每日产出
    
    @classmethod
    def 将每日产出转换为每关产出(cls, 波次奖励每日产出: Dict) -> Dict:
        """将每天的波次奖励产出数据转换为每关的产出数据
        
        Args:
            波次奖励每日产出: 每日波次奖励产出数据，格式为 {日: {资源: 产出量}}
            
        Returns:
            dict: 每关的波次奖励产出，格式为 {关卡: {资源: 产出量}}
        """
        from EconomySystem.EcoDataType import 关卡阶段枚举
        
        每关产出 = {}
        
        # 遍历所有阶段
        for 阶段 in 关卡阶段枚举.获取所有阶段():
            阶段数据 = 阶段.value
            通关进度字典 = 阶段数据["通关进度字典"]
            
            # 遍历该阶段的每一天
            for 日, 当日最高关卡 in 通关进度字典.items():
                # 检查该日是否有波次奖励产出数据
                if 日 in 波次奖励每日产出 and 波次奖励每日产出[日]:
                    当日波次奖励产出 = 波次奖励每日产出[日]
                    
                    # 计算前一天的最高关卡（用于确定当天新通过的关卡范围）
                    前一日最高关卡 = 0
                    if 日 > 1:
                        # 查找前一天的最高关卡
                        if 日 - 1 in 通关进度字典:
                            前一日最高关卡 = 通关进度字典[日 - 1]
                        else:
                            # 如果前一天不在当前阶段，需要查找前一个阶段的最后一天
                            所有阶段 = 关卡阶段枚举.获取所有阶段()
                            当前阶段索引 = 所有阶段.index(阶段)
                            if 当前阶段索引 > 0:
                                前一阶段 = 所有阶段[当前阶段索引 - 1]
                                前一阶段通关进度 = 前一阶段.value["通关进度字典"]
                                if 前一阶段通关进度:
                                    前一日最高关卡 = max(前一阶段通关进度.values())
                    
                    # 计算当天新通过的关卡数量
                    当天新通过关卡数 = 当日最高关卡 - 前一日最高关卡
                    
                    if 当天新通过关卡数 > 0:
                        # 计算每关的平均产出
                        for 资源, 总产出量 in 当日波次奖励产出.items():
                            # 处理所有资源类型
                            if 资源 in ["绿色碎片", "蓝色碎片", "紫色碎片", "金币", "钻石", "体力"]:
                                每关产出量 = 总产出量 / 当天新通过关卡数
                                
                                # 分配给当天新通过的每个关卡
                                for 关卡 in range(前一日最高关卡 + 1, 当日最高关卡 + 1):
                                    if 关卡 not in 每关产出:
                                        每关产出[关卡] = {}
                                    每关产出[关卡][资源] = math.ceil(每关产出量)
                    
                    # 调试信息
                    if 当天新通过关卡数 > 0:
                        print(f"第{日}天: 从关卡{前一日最高关卡 + 1}到{当日最高关卡}，共{当天新通过关卡数}关")
                        print(f"当日波次奖励产出: {当日波次奖励产出}")
        
        # 按关卡数排序输出
        排序后的每关产出 = dict(sorted(每关产出.items()))
        
        print(f"\n=== 波次奖励每关产出计算完成 ===")
        print(f"总计算关卡数: {len(排序后的每关产出)}")
        if 排序后的每关产出:
            最小关卡 = min(排序后的每关产出.keys())
            最大关卡 = max(排序后的每关产出.keys())
            print(f"关卡范围: {最小关卡}-{最大关卡}")
        
        return 排序后的每关产出
    
