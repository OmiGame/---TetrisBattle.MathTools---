    # region 计算资源递增序列
    @classmethod
    def _计算资源递增序列(cls, 总产出: int, 等级数量: int, 资源类型: str) -> list:
        """计算资源的递增序列分配"""
        if 等级数量 <= 0 or 总产出 <= 0:
            return []
        
        if 等级数量 == 1:
            return [总产出]
        
        # ----
        # 新的合理分配策略
        # ----
        平均值 = 总产出 / 等级数量
        
        # 设定合理的起始值和最大值，控制膨胀
        最小值 = max(1, int(平均值 * 0.3))  # 最小值为平均值的30%
        最大值 = min(int(平均值 * 2.0), int(总产出 * 0.4))  # 最大值不超过总产出的40%
        
        # 如果资源总产出很小，使用更保守的策略
        if 总产出 <= 等级数量 * 3:
            # 对于小数值资源，采用更平缓的分配
            基础值 = max(1, 总产出 // (等级数量 + 2))
            递增序列 = [基础值] * 等级数量
            
            # 将剩余产出分配给后面的等级
            剩余产出 = 总产出 - sum(递增序列)
            for i in range(等级数量 - 1, -1, -1):
                if 剩余产出 <= 0:
                    break
                增加量 = min(剩余产出, 基础值)
                递增序列[i] += 增加量
                剩余产出 -= 增加量
        else:
            # 对于较大数值资源，使用阶梯式递增
            递增序列 = []
            剩余产出 = 总产出
            
            # 计算合理的递增步长
            总递增量 = 最大值 - 最小值
            if 等级数量 > 1:
                基础步长 = 总递增量 // (等级数量 - 1)
                基础步长 = max(1, 基础步长)
            else:
                基础步长 = 0
            
            # 生成阶梯式序列，允许相同值
            当前值 = 最小值
            连续相同计数 = 0
            
            for i in range(等级数量):
                # 决定是否增加数值
                应该增加 = False
                
                if i == 0:
                    # 第一级使用最小值
                    当前值 = 最小值
                elif i == 等级数量 - 1:
                    # 最后一级，分配剩余的所有产出
                    当前值 = 剩余产出 - sum(递增序列)
                    当前值 = max(最小值, min(当前值, 最大值))
                else:
                    # 中间等级的分配策略
                    if 连续相同计数 >= 2:
                        # 如果已经连续两级相同，必须增加
                        应该增加 = True
                        连续相同计数 = 0
                    elif 剩余产出 - sum(递增序列) - 当前值 > (等级数量 - i - 1) * 最大值:
                        # 如果剩余产出过多，需要增加当前等级的分配
                        应该增加 = True
                        连续相同计数 = 0
                    else:
                        # 可以选择保持相同或增加
                        if 连续相同计数 < 2 and i % 2 == 1:  # 适度控制相同等级的出现
                            连续相同计数 += 1
                        else:
                            应该增加 = True
                            连续相同计数 = 0
                    
                    if 应该增加:
                        增加量 = min(基础步长, (剩余产出 - sum(递增序列)) // (等级数量 - i))
                        当前值 = min(当前值 + 增加量, 最大值)
                
                递增序列.append(当前值)
                
                # 确保不超出剩余产出
                if sum(递增序列) > 剩余产出:
                    递增序列[-1] = 剩余产出 - sum(递增序列[:-1])
                    break
        
        # 最终调整，确保总和匹配
        当前总和 = sum(递增序列)
        差额 = 总产出 - 当前总和
        
        if 差额 != 0:
            # 优先调整后面的等级
            for i in range(等级数量 - 1, -1, -1):
                if 差额 == 0:
                    break
                
                if 差额 > 0:
                    # 需要增加
                    增加量 = min(差额, max(1, int(递增序列[i] * 0.5)))
                    递增序列[i] += 增加量
                    差额 -= 增加量
                else:
                    # 需要减少
                    减少量 = min(abs(差额), 递增序列[i] - 1)
                    if 减少量 > 0:
                        递增序列[i] -= 减少量
                        差额 += 减少量
        
        # 确保所有值都大于0
        for i in range(len(递增序列)):
            递增序列[i] = max(1, 递增序列[i])
        
        # 验证膨胀控制
        最终总和 = sum(递增序列)
        膨胀率 = abs(最终总和 - 总产出) / 总产出 if 总产出 > 0 else 0
        
        if 膨胀率 > 0.1:  # 如果膨胀超过10%，进行修正
            print(f"警告：{资源类型} 膨胀率 {膨胀率:.2%} 超过10%，进行修正")
            # 按比例缩放回原始总产出
            缩放因子 = 总产出 / 最终总和
            for i in range(len(递增序列)):
                递增序列[i] = max(1, int(递增序列[i] * 缩放因子))
        
        return 递增序列
