    # region 计算累积经验值对应的宝箱等级
    @classmethod
    def _计算累积经验值对应的宝箱等级(cls, 累积经验值: int) -> int:
        """根据累积经验值计算对应的宝箱等级"""
        # 获取宝箱升级经验值列表
        升级经验值列表 = cls.获取统一宝箱升级经验值列表()
        
        当前等级 = 1
        累积所需经验 = 0
        
