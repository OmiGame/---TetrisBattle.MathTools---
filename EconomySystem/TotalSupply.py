"""
经济系统总产出计算模块

本模块负责计算游戏中各种资源的产出，包括：
- 金币和碎片的系统产出计算
- 钻石和宝箱等级的计算
- 波次奖励和通关奖励的产出计算

主要类：
- 系统产出金币和碎片计算: 计算金币和碎片的各系统产出
- 系统产出钻石和钻石宝箱等级计算: 计算钻石需求和宝箱等级
- 波次产出系统的宝箱产出计算: 计算波次奖励中的宝箱产出
- 通关奖励产出计算: 计算通关奖励的产出分配
"""

from typing import Dict, List, Tuple
import math
import json
from LubanData import tables
from EconomySystem.TotalDemand import 角色碎片计算
from EconomySystem.EcoDataType import 角色养成数据

# ---- 全局配置数据 ----
经济系统配置 = tables.Tb经济系统配置表
具体每日详细数据 = 角色碎片计算.生成完整每日角色需求详细数据()
角色每日等级 = 角色碎片计算.获取角色每日等级(具体每日详细数据)
#平滑处理的分段数值，指的是波次奖励和通关产出的分段数值，具体是两个阶段的分界点：第一天过的6关和剩余相对平均分布的关卡
平滑处理的分段数值 = 6
#为了制造缺口，实际投放是按照理论所需的值  *  折扣计算的，下面就是这个折扣
每日商店广告投放钻石折扣比例 = 0.8

# region 系统产出金币和碎片计算
class 系统产出金币和碎片计算:
    """计算各系统中金币和碎片的产出分配"""

    # region 获取固定值字典
    @classmethod
    def 获取固定值字典(cls):
        """获取各系统的固定产出数值字典"""
        固定值字典 = {}
        for 经济系统,经济系统数据对象 in 经济系统配置.items():
            固定值字典[经济系统] = {}
            for 资源,资源数据对象 in 经济系统数据对象.经济系统数据.items():
                    if 资源数据对象.类型 == "固定":
                        固定值字典[经济系统][资源] = 资源数据对象.数值
        return 固定值字典
    # endregion

    # region 获取比例字典
    @classmethod
    def 获取比例字典(cls):
        """获取各系统的比例产出字典"""
        比例字典 = {}
        for 经济系统,经济系统数据对象 in 经济系统配置.items():
            比例字典[经济系统] = {}
            for 资源,资源数据对象 in 经济系统数据对象.经济系统数据.items():
                if 资源数据对象.类型 == "比例":
                    比例字典[经济系统][资源] = 资源数据对象.数值
        return 比例字典
    # endregion

    # region 获取保底字典
    @classmethod
    def 获取保底字典(cls):
        """获取各系统的保底产出字典"""
        保底字典 = {}
        for 经济系统,经济系统数据对象 in 经济系统配置.items():
            保底字典[经济系统] = {}
            for 资源,资源数据对象 in 经济系统数据对象.经济系统数据.items():
                if 资源数据对象.类型 == "保底":
                    保底字典[经济系统][资源] = 资源数据对象.数值
        return 保底字典
    # endregion

    # region 获取无广告系统的字典
    @classmethod
    def 获取无广告系统的字典(cls):
        """获取不包含广告的系统字典"""
        无广告系统字典 = {}
        for 经济系统,经济系统数据对象 in 经济系统配置.items():
            if "广告" not in 经济系统数据对象.经济系统数据.keys():
                无广告系统字典[经济系统] = 经济系统数据对象
        return 无广告系统字典
    # endregion

    # region 获取某资源的比例字典
    @classmethod
    def 获取某资源的比例字典(cls,比例字典:dict,资源名称:str):
        """从比例字典中提取指定资源的比例数据"""
        if not any(资源名称 in 资源字典 for 资源字典 in 比例字典.values()):
             raise ValueError(f"资源{资源名称}不存在")

        某资源的百分比字典 = {}
        for 经济系统,资源字典 in 比例字典.items():
            某资源的百分比字典[经济系统] = {}
            for 资源,数值 in 资源字典.items():
                if 资源 == 资源名称:
                    某资源的百分比字典[经济系统][资源] = 数值
        return 某资源的百分比字典
    # endregion

    # region 去除每日固定资源产出
    @classmethod
    def 去除每日固定资源产出(cls,各品质碎片需求:dict,金币总需求:int,固定值字典:dict):
        """从总需求中减去固定产出，计算剩余需求"""
        
        # 检查各品质碎片需求中的键名格式
        if "绿色" in 各品质碎片需求:
            # 初始化剩余需分配的资源字典 - 使用需求数据
            剩余需分配的资源 = {
                "绿色碎片": 各品质碎片需求['绿色'],
                "蓝色碎片": 各品质碎片需求['蓝色'],
                "紫色碎片": 各品质碎片需求['紫色'],
                "金币": 金币总需求
            }
        else:
            # 如果无法识别键名格式，打印调试信息并抛出异常
            print(f"错误: 无法识别的碎片需求键名格式，各品质碎片需求内容: {各品质碎片需求}")
            raise KeyError("无法识别的碎片需求键名格式")
        
        # 从各系统的固定产出中减去
        for 经济系统, 资源字典 in 固定值字典.items():
            if 经济系统 != "精致宝箱":
                for 资源, 固定数值 in 资源字典.items():
                    if 资源 in 剩余需分配的资源:
                        剩余需分配的资源[资源] -= 固定数值
            else:
                for 资源, 固定数值 in 资源字典.items():
                    if 资源 in 剩余需分配的资源:
                        剩余需分配的资源[资源] -= 固定数值 * 经济系统配置.get("精致宝箱").获得次数
        
                
        
        # 确保没有负值
        for 资源 in 剩余需分配的资源:
            if 剩余需分配的资源[资源] < 0:
                print(f"警告: {资源}的固定产出总和超过了需求，已调整为0")
                剩余需分配的资源[资源] = 0
                
        return 剩余需分配的资源
    # endregion

    # region 获取碎片和金币在各系统中每日产出
    @classmethod
    def 获取碎片和金币在各系统中每日产出(cls):
        """计算各系统中碎片和金币的每日产出分配"""
        
        # 获取固定值字典
        固定值字典 = cls.获取固定值字典()

        # 获取比例字典
        比例字典 = cls.获取比例字典()

        # 获取保底字典
        保底字典 = cls.获取保底字典()

        每日详细数据 = 具体每日详细数据

        系统每日产出 = {}
        
        for 阶段名称,阶段数据 in 每日详细数据.items():
            # 初始化阶段每日产出
            系统每日产出[阶段名称] = {}
            for 日,日数据 in 阶段数据.items():
                # 初始化日每日产出
                系统每日产出[阶段名称][日] = {}
                # 去除每日固定资源产出 - 使用已折算的需求数据而不是原始产出数据
                剩余需分配的资源 = cls.去除每日固定资源产出(日数据["各品质碎片需求"],日数据["每日金币总需求"],固定值字典)
                    
                # 将剩余资源按照比例分配到各个系统
                for 经济系统, 资源字典 in 比例字典.items():
                    系统每日产出[阶段名称][日][经济系统] = {}
                        
                    for 资源, 比例 in 资源字典.items():
                        #筛选了要处理的资源，不处理钻石
                        if 资源 in 剩余需分配的资源 and 资源 in ["绿色碎片", "蓝色碎片", "紫色碎片", "金币"]:
                            剩余资源量 = 剩余需分配的资源[资源]
                            # 根据资源类型应用产出系数
                            if 资源 == "绿色碎片":
                                    剩余资源量 *= 角色养成数据.绿色碎片产出系数
                            elif 资源 == "蓝色碎片":
                                    剩余资源量 *= 角色养成数据.蓝色碎片产出系数
                            elif 资源 == "紫色碎片":
                                    剩余资源量 *= 角色养成数据.紫色碎片产出系数
                            elif 资源 == "金币":
                                    剩余资源量 *= 角色养成数据.金币产出系数
                                
                                # 根据比例分配
                            if 经济系统 != "普通宝箱" and 经济系统 != "精致宝箱":
                                系统每日产出[阶段名称][日][经济系统][资源] = math.ceil(剩余资源量 * 比例)
                                保底产出 = 保底字典[经济系统][f"保底{资源}"]
                                #和保底产出比较，保证最小产出
                                if 保底产出 > 系统每日产出[阶段名称][日][经济系统][资源]:
                                    系统每日产出[阶段名称][日][经济系统][资源] = 保底产出
                                
                            elif 经济系统 == "普通宝箱":
                                系统每日产出[阶段名称][日][经济系统][资源] = math.ceil(剩余资源量 * 比例/ 经济系统配置.get("普通宝箱").获得次数)
                                保底产出 = 保底字典[经济系统][f"保底{资源}"]
                                #和保底产出比较，保证最小产出
                                if 保底产出 > 系统每日产出[阶段名称][日][经济系统][资源]:
                                    系统每日产出[阶段名称][日][经济系统][资源] = 保底产出
                            elif 经济系统 == "精致宝箱":
                                系统每日产出[阶段名称][日][经济系统][资源] = math.ceil(剩余资源量 * 比例/ 经济系统配置.get("精致宝箱").获得次数)
                                保底产出 = 保底字典[经济系统][f"保底{资源}"]
                                #和保底产出比较，保证最小产出
                                if 保底产出 > 系统每日产出[阶段名称][日][经济系统][资源]:
                                    系统每日产出[阶段名称][日][经济系统][资源] = 保底产出
                    

                # 添加固定产出
                for 经济系统, 资源字典 in 固定值字典.items():
                    if 经济系统 not in 系统每日产出[阶段名称][日]:
                            系统每日产出[阶段名称][日][经济系统] = {}
                            
                    for 资源, 固定值 in 资源字典.items():
                        if 资源 in ["绿色碎片", "蓝色碎片", "紫色碎片", "金币"]:
                            if 资源 not in 系统每日产出[阶段名称][日][经济系统]:
                                    系统每日产出[阶段名称][日][经济系统][资源] = 0
                            系统每日产出[阶段名称][日][经济系统][资源] += 固定值

        return 系统每日产出
    # endregion

    # region 去掉阶段的系统每日产出
    @classmethod
    def 去掉阶段的系统每日产出(cls,系统每日产出:dict):
        """移除系统产出中的阶段信息，保留纯净的产出数据"""
        合并后的系统每日产出 = {}
        
        # 遍历所有阶段
        for 阶段名称, 阶段数据 in 系统每日产出.items():
            # 遍历该阶段的每一天
            for 日, 日数据 in 阶段数据.items():
                # 如果该日期还未添加到合并后的字典中，则初始化
                if 日 not in 合并后的系统每日产出:
                    合并后的系统每日产出[日] = {}
                
                # 遍历该日的每个经济系统
                for 经济系统, 系统数据 in 日数据.items():
                    # 如果该经济系统还未添加到该日的合并字典中，则初始化
                    if 经济系统 not in 合并后的系统每日产出[日]:
                        合并后的系统每日产出[日][经济系统] = {}
                    
                    # 遍历该经济系统的每种资源
                    for 资源, 产出量 in 系统数据.items():
                        # 如果该资源还未添加到该经济系统的合并字典中，则初始化为0
                        if 资源 not in 合并后的系统每日产出[日][经济系统]:
                            合并后的系统每日产出[日][经济系统][资源] = 0
                        
                        # 累加产出量
                        合并后的系统每日产出[日][经济系统][资源] += 产出量
        
        return 合并后的系统每日产出
    # endregion

    # region 计算每关的通关产出数值
    @classmethod
    def 计算每关的通关产出数值(cls, 合并后的系统每日产出: dict):
        """根据系统每日产出计算每关的通关产出数值"""
        # 导入关卡阶段枚举
        from EconomySystem.EcoDataType import 关卡阶段枚举
 	    # 初始化每关通关产出字典
        每关通关产出 = {}
        # 遍历所有阶段
        for 阶段 in 关卡阶段枚举.获取所有阶段():
            阶段数据 = 阶段.value
            通关进度字典 = 阶段数据["通关进度字典"]

            # 遍历该阶段的每一天
            for 日, 当日最高关卡 in 通关进度字典.items():
                # 检查该日是否有通关产出数据
                if 日 in 合并后的系统每日产出 and "通关产出" in 合并后的系统每日产出[日]:
                    当日通关产出 = 合并后的系统每日产出[日]["通关产出"]
                    
                    # 计算前一天的最高关卡（用于确定当天新通过的关卡范围）
                    前一日最高关卡 = 0
                    if 日 > 1:
                        # 查找前一天的最高关卡
                        if 日 - 1 in 通关进度字典:
                            前一日最高关卡 = 通关进度字典[日 - 1]
                        else:
                            # 如果前一天不在当前阶段，需要查找前一个阶段的最后一天
                            所有阶段 = 关卡阶段枚举.获取所有阶段()
                            当前阶段索引 = 所有阶段.index(阶段)
                            if 当前阶段索引 > 0:
                                前一阶段 = 所有阶段[当前阶段索引 - 1]
                                前一阶段通关进度 = 前一阶段.value["通关进度字典"]
                                if 前一阶段通关进度:
                                    前一日最高关卡 = max(前一阶段通关进度.values())
                    
                    # 计算当天新通过的关卡数量
                    当天新通过关卡数 = 当日最高关卡 - 前一日最高关卡
                    
                    if 当天新通过关卡数 > 0:
                        # 计算每关的平均产出
                        for 资源, 总产出量 in 当日通关产出.items():
                            # 过滤掉体力等非主要资源
                            if 资源 in ["绿色碎片", "蓝色碎片", "紫色碎片", "金币", "钻石"]:
                                每关产出量 = 总产出量 / 当天新通过关卡数
                                
                                # 分配给当天新通过的每个关卡
                                for 关卡 in range(前一日最高关卡 + 1, 当日最高关卡 + 1):
                                    if 关卡 not in 每关通关产出:
                                        每关通关产出[关卡] = {}
                                    每关通关产出[关卡][资源] = math.ceil(每关产出量)
                    
                    # 调试信息
                    if 当天新通过关卡数 > 0:
                        print(f"第{日}天: 从关卡{前一日最高关卡 + 1}到{当日最高关卡}，共{当天新通过关卡数}关")
                        print(f"当日通关产出: {当日通关产出}")
        
        # 按关卡数排序输出
        排序后的每关通关产出 = dict(sorted(每关通关产出.items()))
        
        print(f"\n=== 每关通关产出计算完成 ===")
        print(f"总计算关卡数: {len(排序后的每关通关产出)}")
        if 排序后的每关通关产出:
            最小关卡 = min(排序后的每关通关产出.keys())
            最大关卡 = max(排序后的每关通关产出.keys())
            print(f"关卡范围: {最小关卡}-{最大关卡}")
        
        return 排序后的每关通关产出
    # endregion

    # region 获取合并后的系统每日产出
    @classmethod
    def 获取合并后的系统每日产出(cls):
        """获取合并处理后的系统每日产出数据"""
        系统每日产出 = cls.获取碎片和金币在各系统中每日产出()

        合并后的系统每日产出 = cls.去掉阶段的系统每日产出(系统每日产出)

        return 合并后的系统每日产出
    # endregion

# endregion

# region 系统产出钻石和钻石宝箱等级计算
class 系统产出钻石和钻石宝箱等级计算:
    """计算钻石需求和宝箱等级相关数据"""
    
    # region 类属性定义
    广告产出金币 = 500
    绿色碎片价格 = 经济系统配置.get("每日商店").钻石价格
    蓝色碎片价格 = 角色养成数据.计算碎片价值比()["蓝色碎片价值"]/角色养成数据.计算碎片价值比()["绿色碎片价值"] * 绿色碎片价格
    紫色碎片价格 = 角色养成数据.计算碎片价值比()["紫色碎片价值"]/角色养成数据.计算碎片价值比()["绿色碎片价值"] * 绿色碎片价格

    绿色碎片数量 = 经济系统配置.get("每日商店").经济系统数据["绿色碎片"].数值 * 经济系统配置.get("每日商店").获得次数
    蓝色碎片数量 = 经济系统配置.get("每日商店").经济系统数据["蓝色碎片"].数值 * 经济系统配置.get("每日商店").获得次数
    紫色碎片数量 = 经济系统配置.get("每日商店").经济系统数据["紫色碎片"].数值 * 经济系统配置.get("每日商店").获得次数

    每日商店消耗钻石 = 绿色碎片数量 * 绿色碎片价格 + 蓝色碎片数量 * 蓝色碎片价格 + 紫色碎片数量 * 紫色碎片价格

    金币兑换 = 经济系统配置.get("金币商店").钻石价格/(经济系统配置.get("金币商店").经济系统数据["金币"].数值 - 广告产出金币*经济系统配置.get("金币商店").经济系统数据["广告"].数值)
    金币商店消耗钻石 = 经济系统配置.get("金币商店").钻石价格
