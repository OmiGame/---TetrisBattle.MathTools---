        当前角色等级 = 0
        宝箱等级 = 1
        等级相同日 = 0
        
        #1级时的产出数据，就是经济配置表中的宝箱保底数据
        宝箱等级字典 = {
            1: {
                "升到此所需经验值": 0,
                "产出数据": {
                    "绿色碎片": 经济系统配置.get("普通宝箱").经济系统数据["保底绿色碎片"].数值,
                    "蓝色碎片": 经济系统配置.get("普通宝箱").经济系统数据["保底蓝色碎片"].数值,
                    "紫色碎片": 0,
                    "金币": 0,
                }
            }
        }
        
        for 日, 各品质等级数据 in 角色每日等级.items():
            for 品质, 等级 in 各品质等级数据.items():
                if 品质 == "蓝色":
                    # 初始化当前角色等级
                    if 当前角色等级 == 0:
                        当前角色等级 = 等级
                        等级相同日 = 1
                        continue
                        
                    # 判断角色等级是否变化
                    if 等级 == 当前角色等级:
                        # 等级没变，累加天数
                        等级相同日 += 1
                    elif 等级 > 当前角色等级:
                        # 等级升级，宝箱等级也提升
                        宝箱等级 += 1
                        # 计算升级所需经验 = 之前等级相同的天数 * 每日花费钻石
                        宝箱等级字典[宝箱等级] = { "升到此所需经验值": 普通宝箱每日花费钻石 * 等级相同日, "产出数据": 合并后的系统每日产出[日]["普通宝箱"]}
                        
