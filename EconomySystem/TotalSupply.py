        """将每关波次奖励产出转换为每日产出
        
        Args:
            每关波次奖励产出: 每关的波次奖励产出，格式为 {关卡: {资源: 产出量}}
            
        Returns:
            dict: 每日波次奖励产出，格式为 {日: {资源: 产出量}}
        """
        from EconomySystem.EcoDataType import 关卡阶段枚举
        
        每日波次奖励产出 = {}
        
        # 遍历所有阶段
        for 阶段 in 关卡阶段枚举.获取所有阶段():
            阶段数据 = 阶段.value
            通关进度字典 = 阶段数据["通关进度字典"]
            
            # 遍历该阶段的每一天
            for 日, 当日最高关卡 in 通关进度字典.items():
                每日波次奖励产出[日] = {}
                
                # 计算前一天的最高关卡（用于确定当天新通过的关卡范围）
                前一日最高关卡 = 0
                if 日 > 1:
                    # 查找前一天的最高关卡
                    if 日 - 1 in 通关进度字典:
                        前一日最高关卡 = 通关进度字典[日 - 1]
                    else:
                        # 如果前一天不在当前阶段，需要查找前一个阶段的最后一天
                        所有阶段 = 关卡阶段枚举.获取所有阶段()
                        当前阶段索引 = 所有阶段.index(阶段)
                        if 当前阶段索引 > 0:
                            前一阶段 = 所有阶段[当前阶段索引 - 1]
                            前一阶段通关进度 = 前一阶段.value["通关进度字典"]
                            if 前一阶段通关进度:
                                前一日最高关卡 = max(前一阶段通关进度.values())
                
                # 计算当天新通过的关卡数量
                当天新通过关卡数 = 当日最高关卡 - 前一日最高关卡
                
                if 当天新通过关卡数 > 0:
                    # 累加当天新通过的所有关卡的波次奖励产出
                    for 关卡 in range(前一日最高关卡 + 1, 当日最高关卡 + 1):
                        if 关卡 in 每关波次奖励产出:
                            关卡产出 = 每关波次奖励产出[关卡]
                            
                            # 累加该关卡的产出到当日总产出
                            for 资源, 产出量 in 关卡产出.items():
                                if 资源 not in 每日波次奖励产出[日]:
                                    每日波次奖励产出[日][资源] = 0
                                每日波次奖励产出[日][资源] += 产出量
                
                # 调试信息
                if 当天新通过关卡数 > 0:
                    print(f"第{日}天: 从关卡{前一日最高关卡 + 1}到{当日最高关卡}，共{当天新通过关卡数}关")
                    print(f"当日实际波次奖励产出: {每日波次奖励产出[日]}")
        
        return 每日波次奖励产出

