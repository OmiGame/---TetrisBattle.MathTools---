    # region 计算精致宝箱升级经验值
    @classmethod
    def 计算精致宝箱升级经验值(cls,合并后的系统每日产出:dict):
        """计算精致宝箱的升级经验值分配"""
        # 计算精致宝箱升级所需经验值
        每日购买精致宝箱次数 = 经济系统配置.get("精致宝箱").购买次数
        精致宝箱价格 = 经济系统配置.get("精致宝箱").钻石价格
        精致宝箱每日花费钻石 = 每日购买精致宝箱次数 * 精致宝箱价格
        当前角色等级 = 0
        宝箱等级 = 1
        等级相同日 = 0
        
        #1级时的产出数据，就是经济配置表中的宝箱保底数据
        宝箱等级字典 = {
            1: {
                "升到此所需经验值": 0,
                "产出数据": {
                    "绿色碎片": 经济系统配置.get("精致宝箱").经济系统数据["保底绿色碎片"].数值,
                    "蓝色碎片": 经济系统配置.get("精致宝箱").经济系统数据["保底蓝色碎片"].数值,
                    "紫色碎片": 经济系统配置.get("精致宝箱").经济系统数据["保底紫色碎片"].数值,
                    "金币": 0,
                }
            }
        }
        
        for 日, 各品质等级数据 in 角色每日等级.items():
            for 品质, 等级 in 各品质等级数据.items():
                if 品质 == "蓝色":
                    # 初始化当前角色等级
                    if 当前角色等级 == 0:
                        当前角色等级 = 等级
                        等级相同日 = 1
                        continue
                        
                    # 判断角色等级是否变化
                    if 等级 == 当前角色等级:
                        # 等级没变，累加天数
                        等级相同日 += 1
                    elif 等级 > 当前角色等级:
                        # 等级升级，宝箱等级也提升
                        宝箱等级 += 1
                        # 计算升级所需经验 = 之前等级相同的天数 * 每日花费钻石
                        宝箱等级字典[宝箱等级] = { "升到此所需经验值": 精致宝箱每日花费钻石 * 等级相同日, "产出数据": 合并后的系统每日产出[日]["精致宝箱"]}
                        
                        # 打印调试信息
                        print(f"宝箱等级升至：{宝箱等级}，经过天数：{等级相同日}，经验值：{宝箱等级字典[宝箱等级]['升到此所需经验值']},产出数据：{宝箱等级字典[宝箱等级]['产出数据']}")
                        
                        # 更新当前角色等级和重置计数器
                        当前角色等级 = 等级
                        等级相同日 = 1
        if len(宝箱等级字典) > 1:
            # 计算总花费和宝箱等级数量（排除1级，因为1级经验值为0）
            需要平滑的等级 = [k for k in 宝箱等级字典.keys() if k > 1]
            总花费 = sum(宝箱等级字典[k]["升到此所需经验值"] for k in 需要平滑的等级)
            需要平滑的等级数量 = len(需要平滑的等级)
            
            if 需要平滑的等级数量 > 0:
                # 计算合适的首项和公差
                # 使用等差数列公式：S = n*(2*a1 + (n-1)*d)/2
                # 为了避免首项过小或公差过大，我们设置首项为总花费的一个合理比例
                平均经验值 = 总花费 / 需要平滑的等级数量
                
                # 设置首项约为平均值的60%，确保分布合理
                理想首项 = 平均经验值 * 0.6
                首项经验值 = max(精致宝箱每日花费钻石, int(理想首项 // 100) * 100)
                
                # 根据等差数列公式计算公差：d = (2*S - 2*n*a1)/(n*(n-1))
                if 需要平滑的等级数量 > 1:
                    理论公差 = (2 * 总花费 - 2 * 需要平滑的等级数量 * 首项经验值) / (需要平滑的等级数量 * (需要平滑的等级数量 - 1))
                    # 将公差调整为合理的整数，确保为100的倍数
                    公差 = max(100, int(理论公差 // 100) * 100)
                    
                    # 如果公差过大，重新调整首项和公差
                    if 公差 > 平均经验值:
                        首项经验值 = int(平均经验值 * 0.8 // 100) * 100
                        理论公差 = (2 * 总花费 - 2 * 需要平滑的等级数量 * 首项经验值) / (需要平滑的等级数量 * (需要平滑的等级数量 - 1))
                        公差 = max(100, int(理论公差 // 100) * 100)
                else:
                    公差 = 100
                
                # 重新分配经验值，保持产出数据不变
                平滑后的宝箱等级字典 = {}
                
                # 首先保留1级数据
                if 1 in 宝箱等级字典:
                    平滑后的宝箱等级字典[1] = 宝箱等级字典[1].copy()
                
                # 为2级及以上等级重新分配经验值
                当前索引 = 0
                for 原宝箱等级 in sorted(需要平滑的等级):
                    新经验值 = 首项经验值 + 当前索引 * 公差
                    平滑后的宝箱等级字典[原宝箱等级] = {
                        "升到此所需经验值": 新经验值,
                        "产出数据": 宝箱等级字典[原宝箱等级]["产出数据"],
                        "原始经验值": 宝箱等级字典[原宝箱等级]["升到此所需经验值"]
                    }
                    当前索引 += 1
                
                # 精确调整以保持总花费不变（分散到需要平滑的等级而不是只调整最后一级）
                新总花费 = sum(平滑后的宝箱等级字典[k]["升到此所需经验值"] for k in 需要平滑的等级)
                花费差额 = 总花费 - 新总花费
                
                if 花费差额 != 0:
                    # 将差额平均分配到需要平滑的等级，避免最后一级突然跳跃
                    每级调整量 = 花费差额 // 需要平滑的等级数量
                    剩余差额 = 花费差额 % 需要平滑的等级数量
                    
                    for i, 等级 in enumerate(需要平滑的等级):
                        平滑后的宝箱等级字典[等级]["升到此所需经验值"] += 每级调整量
                        # 将剩余差额分配给后面的等级
                        if i >= 需要平滑的等级数量 - abs(剩余差额):
                            平滑后的宝箱等级字典[等级]["升到此所需经验值"] += 1 if 剩余差额 > 0 else -1
                        
                        # 确保调整后的经验值仍为100的倍数
                        调整后经验值 = 平滑后的宝箱等级字典[等级]["升到此所需经验值"]
                        平滑后的宝箱等级字典[等级]["升到此所需经验值"] = (调整后经验值 // 100) * 100
                
                print("\n=== 精致宝箱经验值平滑处理前后对比 ===")
                print("原始经验值分配：")
                for 等级, 数据 in sorted(宝箱等级字典.items()):
                    if 等级 == 1:
                        print(f"等级{等级}: {数据['升到此所需经验值']} (1级基础数据)")
                    else:
                        print(f"等级{等级}: {数据['升到此所需经验值']}")
                
                print(f"\n平滑后经验值分配（起始{首项经验值}，递增{公差}）：")
                for 等级, 数据 in sorted(平滑后的宝箱等级字典.items()):
                    if 等级 == 1:
                        print(f"等级{等级}: {数据['升到此所需经验值']} (1级基础数据)")
                    else:
                        print(f"等级{等级}: {数据['升到此所需经验值']} (原始: {数据['原始经验值']})")
                
                print(f"\n调试信息：")
                print(f"需要平滑的等级: {需要平滑的等级}")
                print(f"总花费: {总花费}")
                print(f"平均经验值: {平均经验值:.2f}")
                print(f"理论公差: {理论公差:.2f}")
                print(f"实际使用首项: {首项经验值}")
                print(f"实际使用公差: {公差}")
                新总花费验证 = sum(平滑后的宝箱等级字典[k]["升到此所需经验值"] for k in 需要平滑的等级)
                print(f"平滑后总花费: {新总花费验证}")
                print(f"花费差额: {花费差额}")
                
                宝箱等级字典 = 平滑后的宝箱等级字典
        
        # ----
        # 为每个等级计算递增的资源产出数据
        # ----
        宝箱等级字典 = cls._计算精致宝箱递增产出数据(宝箱等级字典, 合并后的系统每日产出)
            
        return 宝箱等级字典
