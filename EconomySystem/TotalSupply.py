    # region 计算每关宝箱产出
    @classmethod
    def 计算每关宝箱产出(cls, 每关波次产出: Dict) -> Dict:
        """计算每关的宝箱产出分配"""
        from EconomySystem.EcoDataType import 波次产出宝箱数据
        
        每关宝箱产出 = {}
        
        # 获取所有宝箱配置
        宝箱配置列表 = 波次产出宝箱数据.宝箱配置列表
        
        # ----
        # 1. 先按原有逻辑计算每关宝箱产出
        # ----
        for 关卡, 关卡产出 in 每关波次产出.items():
            每关宝箱产出[关卡] = {}
            
            # 为每个宝箱计算产出
            for 宝箱配置 in 宝箱配置列表:
                宝箱名称 = 宝箱配置.宝箱名称
                每关宝箱产出[关卡][宝箱名称] = {}
                
                # 设置解锁波次条件
                每关宝箱产出[关卡][宝箱名称]["解锁波次"] = 宝箱配置.出现波次
                
                # 计算各种资源的产出（钻石和体力除外，它们使用固定值）
                资源映射 = {
                    "绿色碎片": "绿卡比例",
                    "蓝色碎片": "蓝卡比例", 
                    "紫色碎片": "紫卡比例",
                    "金币": "金币比例"
                }
                
                # 计算各种资源的产出（按比例分配）
                for 资源名称, 比例属性名 in 资源映射.items():
                    if 资源名称 in 关卡产出:
                        原始产出 = 关卡产出[资源名称]
                        比例 = getattr(宝箱配置, 比例属性名)
                        宝箱产出 = math.ceil(原始产出 * 比例)
                        每关宝箱产出[关卡][宝箱名称][资源名称] = 宝箱产出
                    else:
                        # 如果关卡产出中没有该资源，设为0
                        每关宝箱产出[关卡][宝箱名称][资源名称] = 0
                
                # 体力和钻石使用固定值，不按比例计算
                每关宝箱产出[关卡][宝箱名称]["体力"] = 宝箱配置.体力固定值
                每关宝箱产出[关卡][宝箱名称]["钻石"] = 宝箱配置.钻石固定值
        
        # ----
        # 2. 对每关宝箱产出进行平滑处理
        # ----
        平滑后的每关宝箱产出 = cls._对宝箱产出进行平滑处理(每关宝箱产出)
        
        return 平滑后的每关宝箱产出
