        实际每日波次奖励产出 = cls._将每关波次奖励转换为每日产出(实际每关波次奖励产出)

        # ----
        # 获取实际的每关通关奖励产出数据，并转换为每日产出
        # ----
        实际每关通关奖励产出 = cls._计算实际每关通关奖励产出()
        实际每日通关奖励产出 = cls._将每关通关奖励转换为每日产出(实际每关通关奖励产出)

        # ----
        # 根据玩家看广告情况处理系统每日产出
        # ----
        
        # 创建考虑玩家看广告情况的每日产出字典
