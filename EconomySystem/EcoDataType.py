# 关卡阶段枚举
class 关卡阶段枚举:
    """关卡阶段枚举，定义每个阶段的起始和结束关卡"""
    阶段1 = {
        "名称": "1-30关", 
        "起始关卡": 1, 
        "结束关卡": 30,
        "碎片掉落比例": ["60%", "30%", "10%"],  # [绿色, 蓝色, 紫色]
        "持续时间": 10,  # 预计完成该阶段所需天数
        "每日碎片掉落量": 100  # 该阶段每日可获得的碎片总量
    }
    阶段2 = {
        "名称": "31-60关", 
        "起始关卡": 31, 
        "结束关卡": 60,
        "碎片掉落比例": ["30%", "60%", "10%"],
        "持续时间": 20,
        "每日碎片掉落量": 120
    }
    阶段3 = {
        "名称": "61-100关", 
        "起始关卡": 61, 
        "结束关卡": 100,
        "碎片掉落比例": ["20%", "20%", "60%"],
        "持续时间": 30,
        "每日碎片掉落量": 150
    }
    
    @staticmethod
    def 获取关卡所在阶段(关卡数):
        """获取关卡所在的阶段"""
        if 关卡阶段枚举.阶段1["起始关卡"] <= 关卡数 <= 关卡阶段枚举.阶段1["结束关卡"]:
            return 关卡阶段枚举.阶段1
        elif 关卡阶段枚举.阶段2["起始关卡"] <= 关卡数 <= 关卡阶段枚举.阶段2["结束关卡"]:
            return 关卡阶段枚举.阶段2
        elif 关卡阶段枚举.阶段3["起始关卡"] <= 关卡数 <= 关卡阶段枚举.阶段3["结束关卡"]:
            return 关卡阶段枚举.阶段3
        else:
            raise ValueError(f"无效的关卡数: {关卡数}")
            
    @staticmethod
    def 获取所有阶段():
        """获取所有关卡阶段"""
        return [关卡阶段枚举.阶段1, 关卡阶段枚举.阶段2, 关卡阶段枚举.阶段3]
    
    @staticmethod
    def 获取阶段数量():
        """获取关卡阶段数量"""
        return len(关卡阶段枚举.获取所有阶段())
    
    @staticmethod
    def 获取阶段总天数():
        """获取所有阶段的总天数"""
        return sum(阶段["持续时间"] for 阶段 in 关卡阶段枚举.获取所有阶段())


