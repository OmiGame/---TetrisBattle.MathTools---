class 关卡生成器:
    """关卡生成器类，负责生成随机关卡配置"""
    
    @staticmethod
    def 生成随机关卡配置(关卡ID: int, 角色平均等级参考: int, 难度: 难度类型) -> 关卡基础配置:
        """
        生成随机关卡配置
        
        Args:
            关卡ID: 关卡编号
            角色平均等级参考: 参考角色平均等级
            难度: 关卡难度
            
        Returns:
            关卡基础配置: 生成的关卡配置对象
        """
        # 检查参数范围
        if 角色平均等级参考 < 1 or 角色平均等级参考 > 全局参数.角色等级上限:
            错误信息 = f"角色平均等级参考应在1-{全局参数.角色等级上限}之间"
            打印警告(错误信息)
            raise ValueError(错误信息)
            
        # 计算最大波次数
        最大波次数 = int(全局参数.关卡时长 * 60 / 全局参数.怪物波次间隔)
        
        # 根据难度确定波次体验
        波次体验映射 = {
            难度类型.轻松: 波次体验.轻松,
            难度类型.一般: 波次体验.一般,
            难度类型.困难: 波次体验.困难,
            难度类型.地狱: 波次体验.地狱
        }
        
        波次体验类型 = 波次体验映射.get(难度)
        if 波次体验类型 is None:
            错误信息 = f"无效的难度类型: {难度}，难度类型应为{[类型.value for 类型 in 难度类型]}中的一个"
            打印警告(错误信息)
            波次体验类型 = 波次体验.一般  # 使用默认值
        
        # 生成随机波次配置
        波次配置列表: List[波次配置] = []
        
        # 根据难度确定波次数量
        波次数量 = max(int(最大波次数 * 0.7), 1)  # 至少有一个波次
        
        # 是否生成Boss波次
        生成Boss波次 = 难度 in [难度类型.困难, 难度类型.地狱]
        
        try:
            # 生成普通波次
            for 波次ID in range(1, 波次数量 + 1):
                # 最后一波可能是Boss波
                当前波次体验 = 波次体验.Boss if 生成Boss波次 and 波次ID == 波次数量 else 波次体验类型
                
                try:
                    波次 = 波次生成器.生成随机波次配置(
                        角色平均等级=角色平均等级参考,
                        波次ID=波次ID,
                        波次体验类型=当前波次体验
                    )
                    
                    波次配置列表.append(波次)
                except ValueError as e:
                    打印警告(f"波次{波次ID}生成失败: {e}")
                    # 生成一个默认波次配置
                    打印调试(f"使用默认波次配置替代")
                    # 这里可以添加生成默认波次配置的逻辑
                
        except Exception as e:
            打印错误(f"生成波次配置时发生未预期的错误: {e}")
        
        # 检查是否至少生成了一个波次配置
        if not 波次配置列表:
            打印错误("没有成功生成任何波次配置，关卡生成失败")
            raise ValueError("关卡生成失败：没有有效的波次配置")
        
        # 生成关卡基础配置
        关卡配置 = 关卡基础配置(
            关卡ID=关卡ID,
            生成延迟时间=2.0,  # 可以根据需要调整
            难度类型=难度,
            我方城堡血量=1000,  # 示例值
            敌方城堡血量=1000,  # 示例值
            限时生成时长=全局参数.关卡时长,
            三星达成百分比=0.8,
            二星达成百分比=0.5,
            一星达成百分比=0.2,
            角色平均等级参考=角色平均等级参考,
            波次配置=波次配置列表
        )
        
        return 关卡配置

