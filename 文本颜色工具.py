"""
文本颜色工具模块

本模块提供控制台文本颜色和格式化输出的工具，包括：
- 各种颜色的控制台文本显示
- 警告、错误、成功等信息的格式化输出
- 自定义样式的文本输出

主要类：
- 文本颜色: 控制台文本颜色常量
- 文本样式: 控制台文本样式常量
"""

from enum import Enum
from typing import Any, Optional


class 文本颜色:
    """控制台文本颜色常量类，其中是ANSI转义序列，可以用于在控制台输出彩色文本"""
    黑色 = "\033[30m"
    红色 = "\033[91m"
    绿色 = "\033[92m"
    黄色 = "\033[93m"
    蓝色 = "\033[94m"
    紫色 = "\033[95m"
    青色 = "\033[96m"
    白色 = "\033[97m"
    灰色 = "\033[90m"
    结束 = "\033[0m"


class 文本样式:
    """控制台文本样式常量类，其中是ANSI转义序列，可以用于在控制台输出彩色文本"""
    加粗 = "\033[1m"
    下划线 = "\033[4m"
    闪烁 = "\033[5m"
    反转 = "\033[7m"
    隐藏 = "\033[8m"
    结束 = "\033[0m"


class 消息类型(Enum):
    """消息类型枚举，用于区分不同类型的消息"""
    信息 = "信息"
    警告 = "警告"
    错误 = "错误"
    成功 = "成功"
    调试 = "调试"


def 格式化消息(消息: str, 颜色: str = 文本颜色.白色, 样式: str = "") -> str:
    """
    格式化消息文本
    
    Args:
        消息: 要格式化的消息文本
        颜色: 文本颜色代码
        样式: 文本样式代码
        
    Returns:
        str: 格式化后的文本
    """
    return f"{样式}{颜色}{消息}{文本颜色.结束}"


def 打印消息(消息: str, 类型: 消息类型 = 消息类型.信息, 是否换行: bool = True) -> None:
    """
    根据消息类型以不同颜色打印消息
    
    Args:
        消息: 要打印的消息
        类型: 消息类型枚举值
        是否换行: 是否在消息末尾添加换行符
    """
    类型颜色映射 = {
        消息类型.信息: 文本颜色.白色,
        消息类型.警告: 文本颜色.黄色,
        消息类型.错误: 文本颜色.红色,
        消息类型.成功: 文本颜色.绿色,
        消息类型.调试: 文本颜色.灰色,
    }
    
    前缀 = f"{类型颜色映射[类型]}{类型.value}: {文本颜色.结束}" if 类型 != 消息类型.信息 else ""
    结尾 = "\n" if 是否换行 else ""
    
    print(f"{前缀}{消息}", end=结尾)


def 打印警告(消息: str) -> None:
    """
    以黄色文字打印警告信息
    
    Args:
        消息: 警告信息
    """
    打印消息(消息, 消息类型.警告)


def 打印错误(消息: str) -> None:
    """
    以红色文字打印错误信息
    
    Args:
        消息: 错误信息
    """
    打印消息(消息, 消息类型.错误)


def 打印成功(消息: str) -> None:
    """
    以绿色文字打印成功信息
    
    Args:
        消息: 成功信息
    """
    打印消息(消息, 消息类型.成功)


def 打印调试(消息: str) -> None:
    """
    以灰色文字打印调试信息
    
    Args:
        消息: 调试信息
    """
    打印消息(消息, 消息类型.调试)


def 打印表格(数据: list, 标题: Optional[list] = None, 对齐: str = "左对齐") -> None:
    """
    打印格式化的表格
    
    Args:
        数据: 表格数据，格式为二维列表
        标题: 表格标题，为None时不显示标题
        对齐: 对齐方式，可选值为"左对齐"、"右对齐"、"居中对齐"
    """
    if not 数据:
        打印警告("表格数据为空")
        return
    
    # 确定列宽
    所有行 = [标题] + 数据 if 标题 else 数据
    列数 = max(len(行) for 行 in 所有行)
    
    # 统一行长度
    所有行 = [行 + [""] * (列数 - len(行)) for 行 in 所有行]
    
    # 计算每列最大宽度
    列宽 = []
    for i in range(列数):
        列宽.append(max(len(str(行[i])) for 行 in 所有行) + 2)
    
    # 对齐方式
    对齐映射 = {
        "左对齐": "<",
        "右对齐": ">",
        "居中对齐": "^"
    }
    对齐符号 = 对齐映射.get(对齐, "<")
    
    # 打印表格
    分隔符 = "+" + "+".join("-" * 宽 for 宽 in 列宽) + "+"
    
    print(分隔符)
    
    # 打印标题
    if 标题:
        格式化标题 = "|" + "|".join(f"{文本颜色.青色}{文本样式.加粗}{str(标题[i]):{对齐符号}{列宽[i] - 2}}{文本颜色.结束}" for i in range(列数)) + "|"
        print(格式化标题)
        print(分隔符)
    
    # 打印数据行
    for 行 in 数据:
        格式化行 = "|" + "|".join(f"{str(行[i]):{对齐符号}{列宽[i] - 2}}" for i in range(列数)) + "|"
        print(格式化行)
    
    print(分隔符)


def 打印进度条(当前进度: int, 总进度: int, 长度: int = 50) -> None:
    """
    打印进度条
    
    Args:
        当前进度: 当前进度值
        总进度: 总进度值
        长度: 进度条长度
    """
    百分比 = int(100 * 当前进度 / 总进度)
    已完成 = int(长度 * 当前进度 / 总进度)
    进度条 = f"{文本颜色.绿色}{'█' * 已完成}{文本颜色.灰色}{'░' * (长度 - 已完成)}{文本颜色.结束}"
    状态 = f"{当前进度}/{总进度} [{百分比}%]"
    
    print(f"\r{进度条} {状态}", end="", flush=True)
    
    if 当前进度 == 总进度:
        print()


