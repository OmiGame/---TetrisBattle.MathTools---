@dataclass
class 组基础配置:
    """怪物组配置"""
    怪物名称: str 
    怪物编号: int    
    数量: int         
    等级: int        
    是否Boss: bool      
    组内间隔: float  
    组间间隔: float   
    生成位置: float  
    Boss血量: Optional[float] = None
    Boss攻击力: Optional[float] = None
    Boss类型: Optional[str] = None  # 新增：Boss类型（"战斗型"或"技能型"）
    技能等级: Optional[int] = None  # 新增：技能型Boss的技能等级
    移动速度: Optional[float] = None  # 新增：Boss的移动速度
    对塔伤害: Optional[int] = None  # 新增：Boss对塔的伤害值
    

@dataclass
class 组配置(组基础配置):
    @property
    def 组战力总值(self) -> float:
        """计算波次总战力值"""
        if not hasattr(self, '_组战力总值'):
            # 计算所有怪物组的战力总和
            self._组战力总值 = tables.Tb怪物基础数据表_3导.get(f"{self.怪物名称} {self.等级}").战力 * self.数量
        return self._组战力总值
    
    @classmethod
    def 从基础配置创建(cls, 基础配置: 组基础配置) -> '组配置':
        """从基础配置创建完整配置"""
        return cls(
            怪物名称=基础配置.怪物名称,
            怪物编号=基础配置.怪物编号,
            数量=基础配置.数量,
            等级=基础配置.等级,
            是否Boss=基础配置.是否Boss,
            Boss血量=基础配置.Boss血量,
            组内间隔=基础配置.组内间隔,
            组间间隔=基础配置.组间间隔,
            生成位置=基础配置.生成位置,
            Boss类型=基础配置.Boss类型,
            技能等级=基础配置.技能等级,
            移动速度=基础配置.移动速度,
            对塔伤害=基础配置.对塔伤害
        )
    
@dataclass
class 波次基础配置:
    """波次基础配置"""
    阵容强度: float
    参考等级: int
    波次ID: int                 #第几波
    波次体验: 波次体验
    怪物组列表: List[组配置]  
    波次间隔时间: float
    
    

@dataclass
class 波次配置(波次基础配置):
    """波次完整配置"""
    _波战力总值: Optional[float] = None             #缓存机制，是为了访问下面的属性时，让波盈缺换算成英雄和剩余时间计算时，不重复计算
    _波期望战力值: Optional[float] = None
    _波盈缺换算成角色: Optional[float] = None
    _剩余时间: Optional[float] = None

    @property
    def 波战力总值(self) -> float:
        """计算波次总战力值"""
        if self._波战力总值 is None:
            # 只计算非Boss组的战力总和
            self._波战力总值 = sum(组.组战力总值 for 组 in self.怪物组列表 if not 组.是否Boss)
            
        return self._波战力总值

    @property
    def 波期望战力值(self) -> float:
        """计算波次期望战力值"""
        if self._波期望战力值 is None:
            print(f"{self.阵容强度}, {self.波次ID}")
            # 修复键计算公式：将阵容强度对齐到0.2步长
            对齐后的阵容强度 = round(self.阵容强度 / 阵容强度的步长数据) * 阵容强度的步长数据
            查找键 = int(对齐后的阵容强度 * 1000 + self.波次ID)
            self._波期望战力值 = tables.Tb所有阵容强度战力成长时间分布表_1导.get(查找键).每波怪物总战力
        return self._波期望战力值

    @property
    def 波盈缺换算成英雄(self) -> float:
        """计算波次盈缺值"""
        if self._波盈缺换算成角色 is None:
            # 修复键计算公式：将阵容强度对齐到0.2步长
            对齐后的阵容强度 = round(self.阵容强度 / 阵容强度的步长数据) * 阵容强度的步长数据
            查找键 = int(对齐后的阵容强度 * 1000 + self.波次ID)
            self._波盈缺换算成角色 = (self.波战力总值 - self.波期望战力值) * 全局参数.最大上阵角色数 / tables.Tb所有阵容强度战力成长时间分布表_1导.get(查找键).当前阵容战力
        
        # # 检查值是否在有效范围内
        # if self._波盈缺换算成角色 > 20 or self._波盈缺换算成角色 < -20:
        #     错误信息 = f"波盈缺换算成英雄值超出范围：当前值为{self._波盈缺换算成角色}，有效范围为[-20, 20]，请重新调整波次配置"
        #     raise ValueError(错误信息)
        
        # 返回保留3位小数的值
        return round(self._波盈缺换算成角色, 3)
    
    @property
    def 剩余时间(self) -> float:
        """计算剩余时间"""
        if self._剩余时间 is None:
            self._剩余时间 = self.波次间隔时间 - sum(组.组内间隔 * 组.数量 for 组 in self.怪物组列表) - sum(组.组间间隔 for 组 in self.怪物组列表)
        
        # 检查值是否在有效范围内
        if self._剩余时间 < 5:
            错误信息 = f"剩余时间值过小：当前值为{self._剩余时间:.3f}，最小值应为5，请重新调整波次配置"
            raise ValueError(错误信息)
        
        # 返回保留3位小数的值
        return round(self._剩余时间, 3)

    @classmethod
    def 从基础配置创建(cls, 基础配置: 波次基础配置) -> '波次配置':
        """从基础配置创建完整配置"""
        return cls(
            阵容强度=基础配置.阵容强度,
            参考等级=基础配置.参考等级,
            波次ID=基础配置.波次ID,
            波次体验=基础配置.波次体验,
            怪物组列表=基础配置.怪物组列表,
            波次间隔时间=基础配置.波次间隔时间,
            _波战力总值=None,
            _波期望战力值=None,
            _波盈缺换算成角色=None,
            _剩余时间=None
        )


