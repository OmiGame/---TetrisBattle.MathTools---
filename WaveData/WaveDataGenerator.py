class 波次生成器:
    """波次生成器类，负责生成随机波次配置"""
    
    # 记录已生成的Boss数量
    已生成Boss数量 = 0
    
    # 记录上次调整方向，如果方向改变，减小调整幅度
    上次调整方向 = 0
    
    # 在波次生成器类中添加一个静态变量
    历史波盈缺值 = []
    
    @staticmethod
    def 检查参数范围(角色平均等级: int, 波次ID: int, 波次体验类型: 波次体验) -> bool:
        """
        检查生成随机波次配置的参数是否在有效范围内
        
        Args:
            角色平均等级: 当前角色的平均等级
            波次ID: 波次编号
            波次体验类型: 波次体验类型
            
        Returns:
            bool: 参数是否有效
        """
        # 检查角色平均等级是否在1到角色等级上限之间
        if 角色平均等级 < 1 or 角色平均等级 > 全局参数.角色等级上限:
            return False
            
        # 计算最大波次数
        最大波次数 = int(全局参数.关卡时长 * 60 / 全局参数.怪物波次间隔)
        
        # 检查波次ID是否在1到最大波次数之间
        if 波次ID < 1 or 波次ID > 最大波次数:
            return False
            
        # 检查波次体验类型是否是波次体验枚举的一个有效值（通过枚举遍历获取有效值列表）
        有效波次体验类型 = [值 for 值 in 波次体验]
        if 波次体验类型 not in 有效波次体验类型:
            return False
            
        return True

    @staticmethod
    def 获取有效波次体验类型() -> List[波次体验]:
        """
        获取所有有效的波次体验类型
        
        Returns:
            List[波次体验]: 所有有效的波次体验类型列表
        """
        return [值 for 值 in 波次体验]
        
    @staticmethod
    def 检查波次数据合理性(波次: 波次配置) -> bool:
        """
        检查波次数据是否合理
        
        Args:
            波次: 要检查的波次配置对象
            
        Returns:
            bool: 如果波次数据合理返回True，否则返回False
            
        Raises:
            ValueError: 当波次数据不合理时抛出异常，并说明原因
        """
        # 检查剩余时间是否不小于5
        try:
            剩余时间 = 波次.剩余时间
            if 剩余时间 < 5:
                错误信息 = f"波次数据不合理: 剩余时间({剩余时间:.3f})小于最小要求值5"
                打印警告(错误信息)
                return False
        except ValueError as e:
            打印警告(f"检查剩余时间时出错: {str(e)}")
            return False
        
        # 检查波盈缺换算成英雄是否在合理范围内
        try:
            波盈缺值 = 波次.波盈缺换算成英雄
            当前波次体验 = 波次.波次体验
            
            最小值, 最大值 = 波盈缺范围[当前波次体验]
            if 波盈缺值 < 最小值 or 波盈缺值 > 最大值:
                错误信息 = f"波次数据不合理: 波盈缺换算成英雄({波盈缺值:.3f})不在{当前波次体验.value}体验的合理范围({最小值}, {最大值})内"
                打印警告(错误信息)
                return False
        except ValueError as e:
            打印警告(f"检查波盈缺换算成英雄时出错: {str(e)}")
            return False
        except KeyError:
            错误信息 = f"未找到波次体验类型 {当前波次体验.value} 的波盈缺范围配置"
            打印警告(错误信息)
            return False
        
        # 所有检查都通过
        return True

    @staticmethod
    def 筛选可用怪物列表(适配地图: str, 筛选数量: int = 6, 波次体验类型: 波次体验 = None) -> list:
        """
        从所有怪物列表中筛选出适配指定地图的怪物，并从中选择指定数量的怪物编号不同的怪物，
        包括一个坦克、一个近战、一个远程、一个刺客，另外的在适配地图的怪物中随机选择。
        
        实现逻辑：
        1. 先筛选出品质对应的怪物列表
        2. 首先筛选出适配当前地图的怪物
        3. 将怪物按职业分类
        4. 从每种职业中选择一个怪物（确保编号不重复）
        5. 如果某种职业没有怪物，则跳过
        6. 从剩余怪物中随机选择，直到总数达到指定数量
        7. 如果可用怪物不足指定数量，则允许重复选择
        
        Args:
            所有怪物列表: 所有可用的怪物列表
            适配地图: 当前关卡的地图类型
            筛选数量: 需要筛选的怪物数量，默认为6
            
        Returns:
            list: 筛选后的可用怪物列表，包含指定数量的怪物（如果可能）
        """
        from MonsterData.MonsterTypes import 怪物职业
        
        配置限制 = 波次体验配置限制[波次体验类型]

        # 获取所有可用的怪物
        所有怪物列表 = tables.Tb怪物基础数据表_3导.getDataList()

          # 根据品质筛选怪物
        品质筛选后怪物列表 = [怪物 for 怪物 in 所有怪物列表 if 怪物.品质 == 配置限制["怪物品质"]]
        print(f"已经筛选品质: {配置限制['怪物品质']}")
        
        # 如果没有符合品质要求的怪物，使用所有可用怪物
        if not 品质筛选后怪物列表:
            打印警告(f"地图 {适配地图} 没有品质为 {配置限制['怪物品质']} 的怪物，将使用所有可用怪物")
            品质筛选后怪物列表 = 可用怪物列表

        # 首先筛选出适配当前地图的怪物
        适配地图怪物列表 = [怪物 for 怪物 in 品质筛选后怪物列表 if 适配地图 in ast.literal_eval(怪物.适配地图)]
        print(f"已经筛选适配地图: {适配地图}")


        # 如果适配地图怪物列表为空，返回空列表
        if not 适配地图怪物列表:
            打印警告(f"地图 {适配地图} 没有适配的怪物")
            return []
        
        # 按职业分类怪物
        职业怪物字典 = {
            怪物职业.坦克.value: [],
            怪物职业.近战.value: [],
            怪物职业.远程.value: [],
            怪物职业.刺客.value: []
        }
        
        # 将怪物按职业分类
        for 怪物 in 适配地图怪物列表:
            if 怪物.职业 in 职业怪物字典:
                职业怪物字典[怪物.职业].append(怪物)
            else:
                打印警告(f"未知职业: {怪物.职业}，怪物: {怪物.名称}")
        
        可用怪物列表 = []
        已选择怪物编号 = set()
        
        # 第一步：确保每种职业至少选择一个怪物
        必选职业 = [怪物职业.坦克.value, 怪物职业.近战.value, 怪物职业.远程.value, 怪物职业.刺客.value]
        for 职业 in 必选职业:
            职业怪物列表 = 职业怪物字典[职业]
            
            # 如果该职业没有怪物，跳过
            if not 职业怪物列表:
                打印警告(f"适配地图中没有{职业}职业的怪物")
                continue
                
            # 筛选出编号不重复的怪物
            可选怪物 = [怪物 for 怪物 in 职业怪物列表 if 怪物.怪物编号 not in 已选择怪物编号]
            
            # 如果没有可选的怪物，使用所有该职业的怪物
            if not 可选怪物:
                可选怪物 = 职业怪物列表
            
            # 随机选择一个怪物
            if 可选怪物:
                所选怪物 = random.choice(可选怪物)
                可用怪物列表.append(所选怪物)
                已选择怪物编号.add(所选怪物.怪物编号)
                打印调试(f"已选择{职业}职业怪物: {所选怪物.名称}，编号: {所选怪物.怪物编号}")
        
        # 第二步：如果已选择的怪物不足指定数量，从剩余怪物中随机选择
        剩余所需数量 = 筛选数量 - len(可用怪物列表)
        if 剩余所需数量 > 0:
            打印调试(f"需要额外选择{剩余所需数量}个怪物")
            
            # 筛选出编号不重复的怪物
            剩余可选怪物 = [怪物 for 怪物 in 适配地图怪物列表 if 怪物.怪物编号 not in 已选择怪物编号]
            
            # 如果剩余可选怪物不足，则允许选择不同实例的怪物
            if len(剩余可选怪物) < 剩余所需数量:
                打印警告(f"剩余可选怪物数量({len(剩余可选怪物)})不足，将允许选择已有编号的怪物")
                剩余可选怪物 = [怪物 for 怪物 in 适配地图怪物列表 if 怪物 not in 可用怪物列表]
                
                # 如果仍然没有足够的怪物，则重复使用已有的怪物
                if len(剩余可选怪物) < 剩余所需数量:
                    打印警告(f"适配地图怪物总数不足{筛选数量}个，将重复使用已有怪物")
                    剩余可选怪物 = 适配地图怪物列表
            
            # 随机选择剩余所需数量的怪物
            for i in range(min(剩余所需数量, len(适配地图怪物列表))):
                if not 剩余可选怪物:
                    打印警告("没有更多可选怪物")
                    break
                
                所选怪物 = random.choice(剩余可选怪物)
                可用怪物列表.append(所选怪物)
                已选择怪物编号.add(所选怪物.怪物编号)
                打印调试(f"额外选择怪物: {所选怪物.名称}，编号: {所选怪物.怪物编号}，职业: {所选怪物.职业}")
                
                # 从剩余可选怪物中移除已选择的怪物
                剩余可选怪物 = [怪物 for 怪物 in 剩余可选怪物 if 怪物 != 所选怪物]
        
        打印调试(f"最终筛选出{len(可用怪物列表)}个怪物")
        return 可用怪物列表

    @staticmethod
    def 生成随机波次配置(角色平均等级: int, 波次ID: int, 波次体验类型: 波次体验, 适配地图: str) -> 波次配置:
        """
        生成随机波次配置
        
        Args:
            角色平均等级: 当前角色的平均等级
            波次ID: 波次编号
            波次体验类型: 波次体验类型（轻松/一般/困难/地狱/Boss）
            适配地图: 当前关卡的地图类型
            
        Returns:
            波次配置: 生成的波次配置对象
            
        Raises:
            ValueError: 当参数不在有效范围内时抛出
        """
        # 检查参数范围
        if not 波次生成器.检查参数范围(角色平均等级, 波次ID, 波次体验类型):
            错误信息 = f"参数超出有效范围: 角色平均等级应在1-{全局参数.角色等级上限}之间，"
            错误信息 += f"波次ID应在1-{int(全局参数.关卡时长 * 60 / 全局参数.怪物波次间隔)}之间，"
            有效波次类型名称 = [值.value for 值 in 波次生成器.获取有效波次体验类型()]
            错误信息 += f"波次体验类型应为{有效波次类型名称}中的一个"
            # 使用文本颜色工具打印警告
            打印警告(错误信息)
            raise ValueError(错误信息)
        
        配置限制 = 波次体验配置限制[波次体验类型]

        # 生成怪物组列表
        怪物组列表 = []
        怪物组数 = random.randint(*配置限制["怪物组数"])
        怪物数量 = random.randint(*配置限制["怪物数量范围"])
        坦克数量 = random.randint(*配置限制["生成坦克数量"])
        
        # 筛选可用的怪物列表（确保包含四种职业各一个，另外两个随机）
        可用怪物列表 = 波次生成器.筛选可用怪物列表(适配地图, 6, 波次体验类型)
        
        # 检查是否有适配地图的怪物
        if not 可用怪物列表:
            错误信息 = f"地图 {适配地图} 没有适配的怪物，无法生成波次配置"
            打印错误(错误信息)
            raise ValueError(错误信息)

        # 获取坦克职业的怪物
        坦克怪物列表 = [怪物 for 怪物 in 可用怪物列表 if 怪物.职业 == "坦克"]
        非坦克怪物列表 = [怪物 for 怪物 in 可用怪物列表 if 怪物.职业 != "坦克"]
        
        # 检查是否有坦克职业的怪物
        if 坦克数量 > 0 and not 坦克怪物列表:
            打印警告(f"品质筛选后，地图 {适配地图} 没有坦克职业的怪物，将调整坦克数量为0")
            坦克数量 = 0
        
        # 检查是否可以生成Boss
        是否可以生成Boss = 波次生成器.已生成Boss数量 < 2 and random.random() < 配置限制["Boss出现概率"]
        
        # 确定Boss类型
        Boss类型 = None
        if 是否可以生成Boss:
            if 波次体验类型 == 波次体验.地狱:
                Boss类型 = "战斗型"
            elif 波次体验类型 == 波次体验.Boss:
                Boss类型 = "技能型"
        
        # 如果可以生成Boss，则从怪物组数中预留一组用于生成Boss
        剩余怪物组数 = 怪物组数 - (1 if 是否可以生成Boss else 0)
        
        # 判断是否需要生成坦克组
        需要生成坦克组 = 坦克数量 > 0 and len(坦克怪物列表) > 0
        
        # 如果需要生成坦克组，则预留一组用于生成坦克
        if 需要生成坦克组:
            剩余怪物组数 -= 1
        
        # 检查非坦克怪物列表是否为空
        if not 非坦克怪物列表:
            打印警告(f"地图 {适配地图} 没有非坦克职业的怪物，无法生成普通怪物组")
            剩余怪物组数 = 0
        
        # 为确保每组选择不同的怪物，我们先进行选择
        已选择的怪物名称 = set()  # 使用集合存储已选择的怪物名称
        已选择的非坦克怪物 = []
        
        # 如果没有足够的非坦克怪物，调整剩余怪物组数
        if len(非坦克怪物列表) < 剩余怪物组数:
            打印警告(f"非坦克怪物数量({len(非坦克怪物列表)})少于需要生成的组数({剩余怪物组数})，将调整组数")
            剩余怪物组数 = len(非坦克怪物列表)
        
        for _ in range(剩余怪物组数):
            # 从尚未选择的非坦克怪物中随机选择（基于名称判断）
            可选怪物 = [怪物 for 怪物 in 非坦克怪物列表 if 怪物.名称 not in 已选择的怪物名称]
            # 如果所有非坦克怪物都被选完了，则重新使用所有非坦克怪物
            if not 可选怪物:
                可选怪物 = 非坦克怪物列表
                已选择的怪物名称.clear()  # 清空已选择的名称记录
            # 随机选择一个怪物
            所选怪物 = random.choice(可选怪物)
            已选择的非坦克怪物.append(所选怪物)
            已选择的怪物名称.add(所选怪物.名称)  # 记录已选择的怪物名称
        
        # 计算非坦克怪物数量（总数减去坦克数量）
        非坦克怪物数量 = 怪物数量 - 坦克数量
        
        # 计算每组分配的怪物数量
        if 剩余怪物组数 > 0:
            平均每组怪物数 = 非坦克怪物数量 // 剩余怪物组数
            额外怪物数 = 非坦克怪物数量 % 剩余怪物组数
        else:
            平均每组怪物数 = 0
            额外怪物数 = 0
        
        # 如果需要生成坦克组，则生成一个坦克组，坦克组先生成，确保坦克组在普通怪物组之前
        if 需要生成坦克组:
            等级偏移 = random.randint(*配置限制["怪物等级范围"])
            怪物等级 = max(1, 角色平均等级 + 等级偏移)
            
            # 从坦克怪物中选择
            怪物 = random.choice(坦克怪物列表)
            
            组内间隔 = random.uniform(*配置限制["组内间隔范围"])
            组间间隔 = random.uniform(*配置限制["组间间隔范围"])
            生成位置 = random.uniform(0,0.1)
            
            坦克组 = 组基础配置(
                怪物名称=怪物.名称,
                怪物编号=怪物.怪物编号,
                数量=坦克数量,  # 坦克数量
                等级=怪物等级,
                是否Boss=False,
                Boss血量=None,
                Boss攻击力=None,
                组内间隔=组内间隔,
                组间间隔=组间间隔,
                生成位置=生成位置
            )
            怪物组列表.append(组配置.从基础配置创建(坦克组))
        
        # 先生成普通怪物组（非坦克）
        for i in range(剩余怪物组数):
            等级偏移 = random.randint(*配置限制["怪物等级范围"])
            怪物等级 = max(1, 角色平均等级 + 等级偏移)
            
            # 分配当前组的怪物数量
            当前组怪物数量 = 平均每组怪物数 + (1 if i < 额外怪物数 else 0)
            
            # 使用预先选择的怪物
            怪物 = 已选择的非坦克怪物[i]
            
            组内间隔 = random.uniform(*配置限制["组内间隔范围"])
            组间间隔 = random.uniform(*配置限制["组间间隔范围"])
            生成位置 = random.uniform(0,0.1)    # 可配置的生成位置范围
            
            非坦克组 = 组基础配置(
                怪物名称=怪物.名称,
                怪物编号=怪物.怪物编号,
                数量=当前组怪物数量,
                等级=怪物等级,
                是否Boss=False,
                Boss血量=None,
                Boss攻击力=None,
                组内间隔=组内间隔,
                组间间隔=组间间隔,
                生成位置=生成位置
            )
            怪物组列表.append(组配置.从基础配置创建(非坦克组))
        
        # 如果可以生成Boss，则生成一个Boss组
        if 是否可以生成Boss:
            等级偏移 = random.randint(*配置限制["怪物等级范围"])
            怪物等级 = max(1, 角色平均等级 + 等级偏移)
            
            if Boss类型 == "战斗型":
                # 从战斗boss初始属性表中随机选择一个战斗型Boss
                战斗boss列表 = 战斗boss初始属性表.getDataList()
                if not 战斗boss列表:
                    打印警告("战斗boss初始属性表为空，无法生成战斗型Boss")
                    是否可以生成Boss = False
                else:
                    所选Boss = random.choice(战斗boss列表)
                    组内间隔 = random.uniform(*配置限制["组内间隔范围"])
                    组间间隔 = random.uniform(*配置限制["组间间隔范围"])
                    生成位置 = random.uniform(0, 0.1)
                    
                    # 创建战斗型Boss组
                    Boss组 = 组基础配置(
                        怪物名称=所选Boss.名称,
                        怪物编号=所选Boss.怪物编号,
                        数量=1,  # Boss数量固定为1
                        等级=怪物等级,
                        是否Boss=True,
                        Boss血量=所选Boss.血量*((所选Boss.血量成长倍率)**(怪物等级-1)) * 所选Boss.血量倍数,  # 战斗型Boss血量倍数为10
                        Boss攻击力=所选Boss.攻击力*((所选Boss.攻击力成长倍率)**(怪物等级-1)) * 所选Boss.攻击力倍数,  # 战斗型Boss攻击力倍数为10
                        组内间隔=组内间隔,
                        组间间隔=组间间隔,
                        生成位置=生成位置,
                        Boss类型="战斗型",
                        对塔伤害=所选Boss.对塔伤害
                    )
                    怪物组列表.append(组配置.从基础配置创建(Boss组))
                    波次生成器.已生成Boss数量 += 1
            
            elif Boss类型 == "技能型":
                # 从技能boss初始属性表中随机选择一个技能型Boss
                技能boss列表 = 技能boss初始属性表.getDataList()
                if not 技能boss列表:
                    打印警告("技能boss初始属性表为空，无法生成技能型Boss")
                    是否可以生成Boss = False
                else:
                    所选Boss = random.choice(技能boss列表)
                    组内间隔 = random.uniform(*配置限制["组内间隔范围"])
                    组间间隔 = random.uniform(*配置限制["组间间隔范围"])
                    生成位置 = random.uniform(0, 0.1)
                    
                    # 创建技能型Boss组
                    Boss组 = 组基础配置(
                        怪物名称=所选Boss.名称,
                        怪物编号=所选Boss.编号,
                        数量=1,  # Boss数量固定为1
                        等级=怪物等级,
                        是否Boss=True,
                        Boss血量=Boss生成器.技能型Boss血量值计算(怪物等级,波次ID),
                        Boss攻击力=None,
                        组内间隔=组内间隔,
                        组间间隔=组间间隔,
                        生成位置=生成位置,
                        Boss类型="技能型",
                        技能等级=所选Boss.技能等级,
                        移动速度=1.2,  # 技能Boss的移动速度默认为1.2
                        对塔伤害=0  # 技能Boss的对塔伤害默认为0
                    )
                    怪物组列表.append(组配置.从基础配置创建(Boss组))
                    波次生成器.已生成Boss数量 += 1
        
        # 检查是否至少生成了一个怪物组
        if not 怪物组列表:
            错误信息 = f"无法为地图 {适配地图} 生成任何怪物组，请检查怪物配置"
            打印错误(错误信息)
            raise ValueError(错误信息)
        
        # 生成波次基础配置
        波次基础 = 波次基础配置(
            角色平均等级=角色平均等级,
            波次ID=波次ID,
            波次体验=波次体验类型,
            怪物组列表=怪物组列表,
            波次间隔时间=random.uniform(*配置限制["波次间隔时间范围"])
        )
        
        # 创建完整波次配置
        波次 = 波次配置.从基础配置创建(波次基础)
        
        # 调整波次使其符合波盈缺范围要求
        # 检查波次合理性，如果不合理则调整怪物等级
        最大调整次数 = 12  # 设置最大调整次数，防止无限循环
        当前调整次数 = 0
        波次合理 = 波次生成器.检查波次数据合理性(波次)
        波次生成器.历史波盈缺值 = []  # 清空历史记录

        while not 波次合理 and 当前调整次数 < 最大调整次数:
            当前调整次数 += 1
            # 获取当前波盈缺值和目标范围
            当前波盈缺值 = 波次.波盈缺换算成英雄
            最小值, 最大值 = 波盈缺范围[波次体验类型]
            
            # 记录历史波盈缺值
            波次生成器.历史波盈缺值.append(当前波盈缺值)
            
            # 计算需要调整的方向和幅度
            if 当前波盈缺值 < 最小值:  # 波盈缺值过低，需要提高怪物等级
                调整方向 = +1
                # 靠近最小值而不是直接跳到中间
                目标波盈缺值 = min(最小值 + 1, (最小值 + 最大值) / 2)
            elif 当前波盈缺值 > 最大值:  # 波盈缺值过高，需要降低怪物等级
                调整方向 = -1
                # 靠近最大值而不是直接跳到中间
                目标波盈缺值 = max(最大值 - 1, (最小值 + 最大值) / 2)
            else:
                break  # 已经在合理范围内，无需调整
            
            # 检测是否存在震荡（方向改变）
            if 当前调整次数 > 1 and 波次生成器.上次调整方向 * 调整方向 < 0:
                # 如果方向改变，说明可能在震荡，需要减小调整幅度
                调整系数 = abs(当前波盈缺值 - 目标波盈缺值) / max(1, abs(当前波盈缺值)) * 0.3
            else:
                调整系数 = abs(当前波盈缺值 - 目标波盈缺值) / max(1, abs(当前波盈缺值))
            
            # 检测是否存在重复值震荡（在同样的值之间来回）
            if len(波次生成器.历史波盈缺值) >= 4:
                最近四个值 = 波次生成器.历史波盈缺值[-4:]
                if len(set([round(值, 1) for 值 in 最近四个值])) <= 2:
                    # 如果最近四个值中只有1-2个不同的值（四舍五入到小数点后一位），说明在震荡
                    调整系数 = 调整系数 * 0.2  # 大幅减小调整幅度
                    print(f"检测到震荡，减小调整幅度: {调整系数:.3f}")
            
            # 限制最大调整幅度
            调整系数 = min(0.1, 调整系数)  # 降低最大调整幅度
            
            # 确保最小调整幅度
            调整系数 = max(0.01, 调整系数)
            
            print(f"调整方向: {调整方向}, 调整系数: {调整系数:.3f}, 目标波盈缺值: {目标波盈缺值:.3f}")
            
            # 记录本次调整方向，用于下次比较
            波次生成器.上次调整方向 = 调整方向
            
            # 调整怪物等级
            有效调整 = False  # 标记是否有效调整
            for 组 in 波次.怪物组列表:
                if not 组.是否Boss:  # 只调整非Boss组的等级
                    原始等级 = 组.等级
                    
                    if 调整方向 < 0:  # 需要降低怪物等级
                        等级减少值 = max(1, min(2, int(原始等级 * 调整系数 + 0.5)))
                        新等级 = max(怪物设计理论值基本参数.怪物最低等级, 原始等级 - 等级减少值)
                    else:  # 需要提高怪物等级
                        等级增加值 = max(1, min(2, int(原始等级 * 调整系数 + 0.5)))
                        新等级 = min(怪物设计理论值基本参数.怪物最高等级, 原始等级 + 等级增加值)
                    
                    if 新等级 != 原始等级:
                        有效调整 = True
                        组.等级 = 新等级
            
            # 如果没有有效调整（所有怪物等级已经达到极限），则跳出循环
            if not 有效调整:
                print("无法进一步调整怪物等级，跳出循环")
                break
            
            # 重要：清除缓存的计算值，确保重新计算波盈缺值
            波次._波战力总值 = None
            波次._波期望战力值 = None
            波次._波盈缺换算成角色 = None
            波次._剩余时间 = None
            
            # 同时清除每个组的缓存值
            for 组 in 波次.怪物组列表:
                if hasattr(组, '_组战力总值'):
                    delattr(组, '_组战力总值')
            
            # 重新检查波次合理性
            波次合理 = 波次生成器.检查波次数据合理性(波次)
        
        # 如果调整到最大次数仍未合理，输出警告
        if 当前调整次数 >= 最大调整次数 and not 波次合理:
            打印警告(f"波次调整达到最大次数({最大调整次数})，但波盈缺值仍不在合理范围内")
        
        print(f"当前调整次数: {当前调整次数}")
        print(f"波盈缺值: {波次.波盈缺换算成英雄:.3f}")
        print(f"波次数据是否合理: {波次合理}")
        print(f"波次剩余时间: {波次.剩余时间:.3f}")
        print(f"组数: {len(波次.怪物组列表)}")
        for i in range(len(波次.怪物组列表)):
            组 = 波次.怪物组列表[i]
            # 格式化输出组配置，确保所有浮点数只保留3位小数
            格式化组内间隔 = f"{组.组内间隔:.3f}"
            格式化组间间隔 = f"{组.组间间隔:.3f}"
            格式化生成位置 = f"{组.生成位置:.3f}"
            
            # 创建可读性更好的组配置输出字符串
            组配置输出 = f"组配置(怪物名称='{组.怪物名称}', 怪物编号={组.怪物编号}, 数量={组.数量}, 等级={组.等级}, " \
                      f"是否Boss={组.是否Boss}, 组内间隔={格式化组内间隔}, 组间间隔={格式化组间间隔}, " \
                      f"生成位置={格式化生成位置}"
            
            # 添加可选参数
            if 组.Boss血量 is not None:
                try:
                    # 尝试将Boss血量格式化为3位小数
                    格式化Boss血量 = f"{float(组.Boss血量):.3f}" if isinstance(组.Boss血量, (float, int)) else f"{组.Boss血量}"
                    组配置输出 += f", Boss血量={格式化Boss血量}"
                except (ValueError, TypeError, OverflowError):
                    # 如果格式化失败，直接输出原值
                    组配置输出 += f", Boss血量={组.Boss血量}"
            else:
                组配置输出 += ", Boss血量=None"
                
            组配置输出 += f", Boss攻击力={组.Boss攻击力}" \
                      f", Boss类型={组.Boss类型}" \
                      f", 技能等级={组.技能等级}" \
                      f", 移动速度={组.移动速度}" \
                      f", 对塔伤害={组.对塔伤害})"
            
            print(f"怪物组列表: {组配置输出}")
        return 波次

