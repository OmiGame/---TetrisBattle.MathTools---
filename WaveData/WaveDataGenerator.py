                    当前组怪物数 = 组.数量
                    当前组需要减少时间 = 平均每怪物减少时间 * 当前组怪物数
                    
                    # 计算需要调整的组内间隔步数
                    需要调整步数 = max(1, int(当前组需要减少时间 / 时间调整步长))
                    调整后间隔 = 组.组内间隔 - (需要调整步数 * 时间调整步长)
                    
