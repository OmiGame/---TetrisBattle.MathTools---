    @staticmethod
    def 生成普通怪物组(参考等级: int, 波次ID: int, 波次体验类型: 波次体验) -> "组配置":
        """
        生成一个普通怪物组，用于在移除Boss组后添加普通怪物组
        
        Args:
            角色平均等级: 当前角色的平均等级
            波次ID: 波次编号
            波次体验类型: 波次体验类型
            
        Returns:
            组配置: 生成的普通怪物组配置
        """
        from WaveData.WaveDataType import 组基础配置, 组配置
        
        # 获取配置限制
        配置限制 = 波次体验配置限制[波次体验类型]
        
        # 获取所有可用的怪物
        所有怪物列表 = tables.Tb怪物基础数据表_3导.getDataList()
        
        # 根据品质筛选怪物
        品质筛选后怪物列表 = [怪物 for 怪物 in 所有怪物列表 if 怪物.品质 == 配置限制["怪物品质"]]
        
        # 如果没有符合品质要求的怪物，使用所有可用怪物
        if not 品质筛选后怪物列表:
            品质筛选后怪物列表 = 所有怪物列表
        
        # 随机选择一个怪物
        if 品质筛选后怪物列表:
            怪物 = random.choice(品质筛选后怪物列表)
        else:
            # 如果没有可用怪物，创建一个默认怪物
            打印警告("没有可用怪物，使用默认怪物")
            from collections import namedtuple
            默认怪物 = namedtuple('默认怪物', ['名称', '怪物编号'])
            怪物 = 默认怪物(名称="默认怪物", 怪物编号=1)
        
        # 生成怪物等级
        等级偏移 = random.randint(*配置限制["怪物等级范围"])
        怪物等级 = max(1, 参考等级 + 等级偏移)
        
        # 生成怪物数量
        怪物数量 = random.randint(*配置限制["怪物数量范围"]) // 2  # 减少数量，因为是替代Boss组
        
        # 生成组内间隔和组间间隔
        组内间隔 = random.uniform(*配置限制["组内间隔范围"])
        组间间隔 = random.uniform(*配置限制["组间间隔范围"])
        生成位置 = random.uniform(0, 0.1)
        
        # 创建普通怪物组
        普通组 = 组基础配置(
            怪物名称=怪物.名称,
            怪物编号=怪物.怪物编号,
            数量=怪物数量,
            等级=怪物等级,
            是否Boss=False,
            Boss血量=None,
            Boss攻击力=None,
            组内间隔=组内间隔,
            组间间隔=组间间隔,
            生成位置=生成位置
        )
        
        return 组配置.从基础配置创建(普通组)

