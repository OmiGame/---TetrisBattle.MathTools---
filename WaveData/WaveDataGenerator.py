    @staticmethod
    def 检查波次数据合理性(波次: 波次配置) -> bool:
        """
        检查波次数据是否合理
        
        Args:
            波次: 要检查的波次配置对象
            
        Returns:
            bool: 如果波次数据合理返回True，否则返回False
            
        Raises:
            ValueError: 当波次数据不合理时抛出异常，并说明原因
        """
        # 检查剩余时间是否不小于5
        try:
            剩余时间 = 波次.剩余时间
            if 剩余时间 < 5:
                错误信息 = f"波次数据不合理: 剩余时间({剩余时间:.2f})小于最小要求值5"
                打印警告(错误信息)
                return False
        except ValueError as e:
            打印警告(f"检查剩余时间时出错: {str(e)}")
            return False
        
        # 检查波盈缺换算成英雄是否在合理范围内
        try:
            波盈缺值 = 波次.波盈缺换算成英雄
            当前波次体验 = 波次.波次体验
            
            最小值, 最大值 = 波盈缺范围[当前波次体验]
            if 波盈缺值 < 最小值 or 波盈缺值 > 最大值:
                错误信息 = f"波次数据不合理: 波盈缺换算成英雄({波盈缺值:.2f})不在{当前波次体验.value}体验的合理范围({最小值}, {最大值})内"
                打印警告(错误信息)
                return False
        except ValueError as e:
            打印警告(f"检查波盈缺换算成英雄时出错: {str(e)}")
            return False
        except KeyError:
            错误信息 = f"未找到波次体验类型 {当前波次体验.value} 的波盈缺范围配置"
            打印警告(错误信息)
            return False
        
        # 所有检查都通过
        return True

    @staticmethod
    def 生成随机波次配置(角色平均等级: int, 波次ID: int, 波次体验类型: 波次体验) -> 波次配置:
        """
        生成随机波次配置
        
        Args:
            角色平均等级: 当前角色的平均等级
            波次ID: 波次编号
            波次体验类型: 波次体验类型（轻松/一般/困难/地狱/Boss）
            
        Returns:
            波次配置: 生成的波次配置对象
            
        Raises:
            ValueError: 当参数不在有效范围内时抛出
        """
        # 检查参数范围
        if not 波次生成器.检查参数范围(角色平均等级, 波次ID, 波次体验类型):
            错误信息 = f"参数超出有效范围: 角色平均等级应在1-{全局参数.角色等级上限}之间，"
            错误信息 += f"波次ID应在1-{int(全局参数.关卡时长 * 60 / 全局参数.怪物波次间隔)}之间，"
            有效波次类型名称 = [值.value for 值 in 波次生成器.获取有效波次体验类型()]
            错误信息 += f"波次体验类型应为{有效波次类型名称}中的一个"
            # 使用文本颜色工具打印警告
            打印警告(错误信息)
            raise ValueError(错误信息)
        
        配置限制 = 波次生成器.获取波次体验配置(波次体验类型)
        
        # 生成怪物组列表
        怪物组列表 = []
        怪物组数 = random.randint(*配置限制["怪物组数"])
        怪物数量 = random.randint(*配置限制["怪物数量范围"])
        坦克数量 = random.randint(*配置限制["生成坦克数量"])
        
        # 获取所有可用的怪物
        可用怪物列表 = tables.Tb怪物基础数据表_3导.getDataList()
        
        # 根据品质筛选怪物
        品质筛选后怪物列表 = [怪物 for 怪物 in 可用怪物列表 if 怪物.品质 == 配置限制["怪物品质"]]

        # 获取坦克职业的怪物
        坦克怪物列表 = [怪物 for 怪物 in 品质筛选后怪物列表 if 怪物.职业 == "坦克"]
        非坦克怪物列表 = [怪物 for 怪物 in 品质筛选后怪物列表 if 怪物.职业 != "坦克"]
        
        # 检查是否可以生成Boss
        是否可以生成Boss = 波次生成器.已生成Boss数量 < 2 and random.random() < 配置限制["Boss出现概率"]
        
        # 如果可以生成Boss，则从怪物组数中预留一组用于生成Boss
        剩余怪物组数 = 怪物组数 - (1 if 是否可以生成Boss else 0)
        
        # 判断是否需要生成坦克组
        需要生成坦克组 = 坦克数量 > 0 and len(坦克怪物列表) > 0
        
        # 如果需要生成坦克组，则预留一组用于生成坦克
        if 需要生成坦克组:
            剩余怪物组数 -= 1
            
        # 为确保每组选择不同的怪物，我们先进行选择
        已选择的怪物名称 = set()  # 使用集合存储已选择的怪物名称
        已选择的非坦克怪物 = []
        for _ in range(剩余怪物组数):
            # 从尚未选择的非坦克怪物中随机选择（基于名称判断）
            可选怪物 = [怪物 for 怪物 in 非坦克怪物列表 if 怪物.名称 not in 已选择的怪物名称]
            # 如果所有非坦克怪物都被选完了，则重新使用所有非坦克怪物
            if not 可选怪物:
                可选怪物 = 非坦克怪物列表
                已选择的怪物名称.clear()  # 清空已选择的名称记录
            # 随机选择一个怪物
            所选怪物 = random.choice(可选怪物)
            已选择的非坦克怪物.append(所选怪物)
            已选择的怪物名称.add(所选怪物.名称)  # 记录已选择的怪物名称
        
        # 计算非坦克怪物数量（总数减去坦克数量）
        非坦克怪物数量 = 怪物数量 - 坦克数量
        
        # 计算每组分配的怪物数量
        if 剩余怪物组数 > 0:
            平均每组怪物数 = 非坦克怪物数量 // 剩余怪物组数
            额外怪物数 = 非坦克怪物数量 % 剩余怪物组数
        else:
            平均每组怪物数 = 0
            额外怪物数 = 0
        
        # 如果需要生成坦克组，则生成一个坦克组，坦克组先生成，确保坦克组在普通怪物组之前
        if 需要生成坦克组:
            等级偏移 = random.randint(*配置限制["怪物等级范围"])
            怪物等级 = max(1, 角色平均等级 + 等级偏移)
            
            # 从坦克怪物中选择
            怪物 = random.choice(坦克怪物列表)
            
            组内间隔 = random.uniform(*配置限制["组内间隔范围"])
            组间间隔 = random.uniform(*配置限制["组间间隔范围"])
            生成位置 = random.uniform(0,0.1)
            
            坦克组 = 组基础配置(
                怪物名称=怪物.名称,
                怪物编号=怪物.等级,
                数量=坦克数量,  # 坦克数量
                等级=怪物等级,
                是否Boss=False,
                Boss血量=None,
                组内间隔=组内间隔,
                组间间隔=组间间隔,
                生成位置=生成位置
            )
            怪物组列表.append(组配置.从基础配置创建(坦克组))
        
        # 先生成普通怪物组（非坦克）
        for i in range(剩余怪物组数):
            等级偏移 = random.randint(*配置限制["怪物等级范围"])
            怪物等级 = max(1, 角色平均等级 + 等级偏移)
            
            # 分配当前组的怪物数量
            当前组怪物数量 = 平均每组怪物数 + (1 if i < 额外怪物数 else 0)
            
            # 使用预先选择的怪物
            怪物 = 已选择的非坦克怪物[i]
            
            组内间隔 = random.uniform(*配置限制["组内间隔范围"])
            组间间隔 = random.uniform(*配置限制["组间间隔范围"])
            生成位置 = random.uniform(0,0.1)    # 可配置的生成位置范围
            
            非坦克组 = 组基础配置(
                怪物名称=怪物.名称,
                怪物编号=怪物.等级,
                数量=当前组怪物数量,
                等级=怪物等级,
                是否Boss=False,
                Boss血量=None,
                组内间隔=组内间隔,
                组间间隔=组间间隔,
                生成位置=生成位置
            )
            怪物组列表.append(组配置.从基础配置创建(非坦克组))
        
        
        
        # 如果可以生成Boss，则生成一个Boss组
        if 是否可以生成Boss:
            等级偏移 = random.randint(*配置限制["怪物等级范围"])
            怪物等级 = max(1, 角色平均等级 + 等级偏移)
            
            # 随机选择一个怪物作为Boss
            怪物 = random.choice(品质筛选后怪物列表)
            
            组内间隔 = random.uniform(*配置限制["组内间隔范围"])
            组间间隔 = random.uniform(*配置限制["组间间隔范围"])
            生成位置 = random.uniform(0,0.1)
            
            Boss组 = 组基础配置(
                怪物名称=怪物.名称,
                怪物编号=怪物.等级,
                数量=1,  # Boss数量固定为1
                等级=怪物等级,
                是否Boss=True,
                Boss血量=怪物.血量 * 2,  # Boss血量为普通怪物的2倍
                组内间隔=组内间隔,
                组间间隔=组间间隔,
                生成位置=生成位置
            )
            怪物组列表.append(组配置.从基础配置创建(Boss组))
            
            # 更新已生成的Boss数量
            波次生成器.已生成Boss数量 += 1
        
        # 生成波次基础配置
        波次基础 = 波次基础配置(
            角色平均等级=角色平均等级,
            波次ID=波次ID,
            波次体验=波次体验类型,
            怪物组列表=怪物组列表,
            波次间隔时间=random.uniform(*配置限制["波次间隔时间范围"])
        )
        
        # 创建完整波次配置
        波次 = 波次配置.从基础配置创建(波次基础)
        
        # 调整波次使其符合波盈缺范围要求
        # 检查波次合理性，如果不合理则调整怪物等级
        最大调整次数 = 10  # 设置最大调整次数，防止无限循环
        当前调整次数 = 0
        波次合理 = 波次生成器.检查波次数据合理性(波次)
        
        while not 波次合理 and 当前调整次数 < 最大调整次数:
            当前调整次数 += 1
            # 获取当前波盈缺值和目标范围
            当前波盈缺值 = 波次.波盈缺换算成英雄
            最小值, 最大值 = 波盈缺范围[波次体验类型]
            
            # 计算需要调整的方向和幅度
            if 当前波盈缺值 < 最小值:  # 波盈缺值过低，需要提高怪物等级
                调整方向 = +1
                目标波盈缺值 = random.uniform(最小值, 最大值)  # 调整到范围中间某个值
            elif 当前波盈缺值 > 最大值:  # 波盈缺值过高，需要降低怪物等级
                调整方向 = -1
                目标波盈缺值 = random.uniform(最小值, 最大值)  # 调整到范围中间某个值
            else:
                break  # 已经在合理范围内，无需调整
            
            # 计算调整系数
            调整系数 = abs(当前波盈缺值 - 目标波盈缺值) / 当前波盈缺值
            调整系数 = min(0.2, 调整系数)  # 限制最大调整幅度
            
            # 调整怪物等级
            for 组 in 波次.怪物组列表:
                if not 组.是否Boss:  # 只调整非Boss组的等级
                    原始等级 = 组.等级
                    
                    if 调整方向 < 0:  # 需要降低怪物等级
                        等级减少值 = max(1, min(3, int(原始等级 * 调整系数 + 1)))
                        新等级 = max(怪物设计基本参数.怪物最低等级, 原始等级 - 等级减少值)  # 确保不低于怪物最低等级
                    else:  # 需要提高怪物等级
                        等级增加值 = max(1, min(3, int(原始等级 * 调整系数 + 1)))
                        新等级 = min(怪物设计基本参数.怪物最高等级, 原始等级 + 等级增加值)  # 确保不超过怪物最高等级
                    
                    组.等级 = 新等级
            
            # 重新检查波次合理性
            波次合理 = 波次生成器.检查波次数据合理性(波次)
            
        # 如果调整到最大次数仍未合理，输出警告
        if 当前调整次数 >= 最大调整次数 and not 波次合理:
            打印警告(f"波次调整达到最大次数({最大调整次数})，但波盈缺值仍不在合理范围内")
        
        return 波次

