    # -------------------------------------
    # 10. 伤害公式
    # -------------------------------------
    #现在没有防御百分比的数值，都默认为0
    @classmethod
    def 计算伤害(cls,攻击力: float, 防御百分比 = 0.0) -> int:
        """
        基础伤害公式
        伤害 = 攻击力 * (1 - 防御百分比)
        参数说明：
        - attack: 攻击力（ 大于0的数值 ）
        - defense_percent: 防御百分比（ 0.0-1.0的小数，超出范围自动约束）
        返回：整数伤害（ 最低为1 ）
        """
        # 防御百分比约束处理
        防御比 = max(0.0, min(防御百分比, 1.0))

        # 计算基础伤害
        伤害 = 攻击力 * (1 - 防御比)

        # 伤害取整并确保最小值为1
        return max(1, int(round(伤害)))
    
