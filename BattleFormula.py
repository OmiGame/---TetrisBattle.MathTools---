    # -------------------------------------
    # 11. 肉鸽技能经验值计算
    # -------------------------------------
    @classmethod
    def 计算肉鸽技能经验值(cls, 升级次数: int) -> int:
        """
        升级和消除行数的节奏有关， 因为想要越往后升级越难， 所以需要设计一个经验值计算公式， 二次函数很符合要求， 小方块经验是10
        """
        N = 升级次数
        H = 280    # 第一次升级经验值
        P = 0.25   # 经验值系数
        Q = 8      # 经验值常数
        K = 25     # 经验值转折等级，超过这个等级后，经验值不再增加
        if N<= K:
            经验值 = H + (N-1) * (P*N+Q)
        else:
            经验值 = H + (K-1) * (P*K+Q)
        return 经验值
    
    @classmethod
    def 实际经验值(cls,升级次数: int) -> float:
        return math.ceil(cls.计算肉鸽技能经验值(升级次数)/全局参数.每行经验值) * 全局参数.每行经验值
    
    @classmethod
    def 计算经验总值(cls,升级次数:int) -> float:
        return sum( cls.实际经验值(i) for i in range(1,升级次数 + 1))
    
