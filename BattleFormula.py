def 查找初始属性字典(角色名称: str) -> dict:
    global _所有角色属性缓存
    if not _所有角色属性缓存:  # 检查是否为空
        所有角色属性数据 = tables.Tb角色初始属性表.getDataList()
        _所有角色属性缓存 = {
            角色属性数据.角色名称: 角色属性数据.__dict__
            for 角色属性数据 in 所有角色属性数据
        }
    if 角色名称 not in _所有角色属性缓存:
        raise ValueError(f"角色名称 '{角色名称}' 不存在！")
    return _所有角色属性缓存[角色名称]


class 战斗公式:
    # -------------------------------------
    # 1. 辅助方法
    # -------------------------------------
    @classmethod
    def _验证角色等级(cls, 角色等级: int) -> None:
        """验证角色等级是否合法"""
        最高等级 = 全局参数.角色等级上限
        if 角色等级 < 1:
            raise ValueError("角色等级不能小于1！")
        if 角色等级 > 最高等级:
            raise ValueError(f"角色等级不能超过{最高等级}！")

    @classmethod
    def _验证肉鸽技能数量(cls, 肉鸽技能列表: list) -> None:
        """验证肉鸽技能数量是否合法"""
        已选肉鸽技能数 = len(肉鸽技能列表)
        最大肉鸽技能选择数 = 全局参数.肉鸽技能选择次数
        if 已选肉鸽技能数 > 最大肉鸽技能选择数 + 50:
            raise ValueError(f"错误：超过参考肉鸽技能数{最大肉鸽技能选择数}50个以上了")

    @classmethod
    def _验证上阵角色数量(cls, 上阵角色数: int) -> None:
        """验证上阵角色数量是否合法"""
        最大上阵角色数 = 全局参数.最大上阵角色数
        if 上阵角色数 > 最大上阵角色数:
            raise ValueError(f"错误：超过最大上阵角色数（最多{最大上阵角色数}个）")
        if 上阵角色数 <= 0:
            raise ValueError(f"不能没有上阵角色，当前为{上阵角色数}")

    @classmethod
    def _计算基础属性(cls, 角色名称: str, 角色等级: int, 属性名称: str) -> float:
        """计算角色的基础属性"""
        cls._验证角色等级(角色等级)
        角色初始属性字典 = 查找初始属性字典(角色名称)
        初始值 = 角色初始属性字典[f"初始{属性名称}"]
        成长倍率 = 角色初始属性字典[f"{属性名称}成长倍率"]
        return 初始值 * (成长倍率 ** (角色等级 - 1))

    @classmethod
    def _计算基础战力(cls, 角色名称: str, 角色等级: int, 战力类型: str) -> float:
        """计算角色的基础战力"""
        if 战力类型 == "血量":
            基础值 = cls.计算基础血量(角色名称, 角色等级)
            战力价值 = tables.Tb游戏基本参数.getDataList()[0].血量战力价值
        elif 战力类型 == "DPS":
            基础值 = cls.计算基础DPS(角色名称, 角色等级)
            战力价值 = tables.Tb游戏基本参数.getDataList()[0].DPS战力价值
        else:
            raise ValueError(f"未知的战力类型: {战力类型}")
        return 基础值 * 战力价值

    # -------------------------------------
    # 2. 计算基础属性和基础DPS
    # -------------------------------------
    @classmethod
    def 计算基础血量(cls, 角色名称: str, 角色等级: int) -> float:
        return cls._计算基础属性(角色名称, 角色等级, "血量")

    @classmethod
    def 计算基础攻击力(cls, 角色名称: str, 角色等级: int) -> float:
        return cls._计算基础属性(角色名称, 角色等级, "攻击力")

    @classmethod
    def 计算基础攻速(cls, 角色名称: str, 角色等级: int) -> float:
        return cls._计算基础属性(角色名称, 角色等级, "攻速")

    @classmethod
    def 计算基础攻击范围(cls, 角色名称: str, 角色等级: int) -> float:
        return cls._计算基础属性(角色名称, 角色等级, "攻击范围")

    @classmethod
    def 计算攻击范围收益倍率(cls, 攻击范围: float) -> float:
        """计算攻击范围的收益倍率
        Args:
            攻击范围: 角色的攻击范围值
        Returns:
            float: 攻击范围收益倍率
        """
        return max(1, 1 + (攻击范围 - 1) * 0.15)

    @classmethod
    def 计算基础移速(cls, 角色名称: str, 角色等级: int) -> float:
        return cls._计算基础属性(角色名称, 角色等级, "移速")

    @classmethod
    def 计算基础DPS(cls,角色名称: str, 角色等级: int) -> float:
        角色基础攻击力 = cls.计算基础攻击力(角色名称, 角色等级)
        角色基础攻速 = cls.计算基础攻速(角色名称, 角色等级)
        角色基础攻击范围 = cls.计算基础攻击范围(角色名称, 角色等级)
        角色攻击范围收益倍率 = cls.计算攻击范围收益倍率(角色基础攻击范围)

        角色基础DPS = 角色基础攻击力 * 角色基础攻速 * 角色攻击范围收益倍率
        # print(f"{角色名称} 的基础DPS战力为：", 角色基础DPS)
        return 角色基础DPS
    # -------------------------------------
    # 3. 计算战力：血量战力、DPS战力、血量特效战力、DPS特效战力和总战力
    # -------------------------------------
    @classmethod
    def 计算基础血量战力(cls, 角色名称: str, 角色等级: int) -> float:
        return cls._计算基础战力(角色名称, 角色等级, "血量")

    @classmethod
    def 计算基础DPS战力(cls, 角色名称: str, 角色等级: int) -> float:
        DPS战力价值 = tables.Tb游戏基本参数.getDataList()[0].DPS战力价值
        return  cls.计算基础DPS(角色名称,角色等级) * DPS战力价值

    @classmethod
    def 计算基础血量特效战力(cls, 角色名称: str, 角色等级: int) -> float:
        角色基础血量战力 = cls.计算基础血量(角色名称, 角色等级)
        血量特效战力系数 = tables.Tb角色初始属性表.get(f"{角色名称}").血量特效战力系数
        return 角色基础血量战力 * 血量特效战力系数

    @classmethod
    def 计算基础DPS特效战力(cls, 角色名称: str, 角色等级: int) -> float:
        角色基础DPS战力 = cls.计算基础DPS战力(角色名称, 角色等级)
        DPS特效战力系数 = tables.Tb角色初始属性表.get(f"{角色名称}").DPS特效战力系数
        return 角色基础DPS战力 * DPS特效战力系数

    @classmethod
    def 计算基础总战力(cls, 角色名称: str, 角色等级: int) -> float:
        return (cls.计算基础血量战力(角色名称, 角色等级) + 
                cls.计算基础DPS战力(角色名称, 角色等级) +
                cls.计算基础血量特效战力(角色名称, 角色等级) + 
                cls.计算基础DPS特效战力(角色名称, 角色等级))

    # -------------------------------------
    # 4. 计算角色天赋技能影响下的各个模块战力
    # -------------------------------------
    @classmethod
    def _获取已解锁技能倍率(cls, 角色名称: str, 角色等级: int) -> list:
        """获取角色已解锁的技能倍率列表"""
        global _所有角色技能数据缓存
        if not _所有角色技能数据缓存:
            _所有角色技能数据缓存 = tables.Tb角色技能数据表.getDataMap()
        if 角色名称 not in _所有角色技能数据缓存:
            raise ValueError(f"未知角色名称: {角色名称}")
        角色数据 = _所有角色技能数据缓存[角色名称]

        已解锁的技能倍率 = []
        for 技能等级 in 角色数据.各等级技能倍率:
            if 技能等级 <= 角色等级:
                已解锁的技能倍率.append(角色数据.各等级技能倍率[技能等级])
        if not 已解锁的技能倍率:
            raise ValueError(f"角色 {角色名称} 等级 {角色等级} 没有解锁任何技能")
        return 已解锁的技能倍率

    @classmethod
    def _计算技能倍率(cls, 已解锁的技能倍率: list, 倍率类型: str) -> float:
        """计算指定类型的技能总倍率"""
        总倍率 = 1.0
        for 技能倍率 in 已解锁的技能倍率:
            总倍率 *= getattr(技能倍率, 倍率类型)
        return 总倍率

    @classmethod
    def 计算解锁技能的血量战力(cls, 角色名称: str, 角色等级: int) -> float:
        已解锁的技能倍率 = cls._获取已解锁技能倍率(角色名称, 角色等级)
        血量总倍率 = cls._计算技能倍率(已解锁的技能倍率, "血量倍率")
        基础血量战力 = cls.计算基础血量战力(角色名称, 角色等级)
        return 基础血量战力 * 血量总倍率

    @classmethod
    def 计算解锁技能的DPS战力(cls, 角色名称: str, 角色等级: int) -> float:
        已解锁的技能倍率 = cls._获取已解锁技能倍率(角色名称, 角色等级)
        DPS总倍率 = cls._计算技能倍率(已解锁的技能倍率, "DPS倍率")
        基础DPS战力 = cls.计算基础DPS战力(角色名称, 角色等级)
        return 基础DPS战力 * DPS总倍率

    @classmethod
    def 计算解锁技能的血量特效战力(cls, 角色名称: str, 角色等级: int) -> float:
        已解锁的技能倍率 = cls._获取已解锁技能倍率(角色名称, 角色等级)
        血量特效总倍率 = cls._计算技能倍率(已解锁的技能倍率, "血量特效倍率")
        基础血量特效战力 = cls.计算基础血量特效战力(角色名称, 角色等级)
        return 基础血量特效战力 * 血量特效总倍率

    @classmethod
    def 计算解锁技能的DPS特效战力(cls, 角色名称: str, 角色等级: int) -> float:
        已解锁的技能倍率 = cls._获取已解锁技能倍率(角色名称, 角色等级)
        DPS特效总倍率 = cls._计算技能倍率(已解锁的技能倍率, "DPS特效倍率")
        基础DPS特效战力 = cls.计算基础DPS特效战力(角色名称, 角色等级)
        return 基础DPS特效战力 * DPS特效总倍率

    @classmethod
    def 计算解锁技能的总战力(cls, 角色名称: str, 角色等级: int) -> float:
        return (cls.计算解锁技能的血量战力(角色名称, 角色等级) +
                cls.计算解锁技能的DPS战力(角色名称, 角色等级) +
                cls.计算解锁技能的血量特效战力(角色名称, 角色等级) +
                cls.计算解锁技能的DPS特效战力(角色名称, 角色等级))

    @classmethod
    def 计算解锁技能数(cls, 角色名称: str, 角色等级: int) -> int:
        return len(cls._获取已解锁技能倍率(角色名称, 角色等级))

    @classmethod
    def 计算已解锁技能名称列表(cls, 角色名称: str, 角色等级: int) -> list:
        已解锁的技能倍率 = cls._获取已解锁技能倍率(角色名称, 角色等级)
        return [技能数据.技能名称 for 技能数据 in 已解锁的技能倍率]

