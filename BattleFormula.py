    # 10. 伤害公式
    # -------------------------------------
    #现在没有防御百分比的数值，都默认为0
    @classmethod
    def 计算伤害(cls,攻击力: float, 防御百分比 = 0.0) -> int:
        """
        伤害 = 攻击力 * (1 - 防御百分比)
        """
        防御比 = max(0.0, min(防御百分比, 1.0))
        伤害 = 攻击力 * (1 - 防御比)
        # 伤害取整并确保最小值为1
        return max(1, int(round(伤害)))
    
