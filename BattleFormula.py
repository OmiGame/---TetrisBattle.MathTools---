def 查找初始属性字典(角色名称: str) -> dict:
    global _所有角色属性缓存
    if not _所有角色属性缓存:  # 检查是否为空
        所有角色属性数据 = tables.Tb角色初始属性表.getDataList()
        _所有角色属性缓存 = {
            角色属性数据.角色名称: 角色属性数据.__dict__
            for 角色属性数据 in 所有角色属性数据
        }
    if 角色名称 not in _所有角色属性缓存:
        raise ValueError(f"角色名称 '{角色名称}' 不存在！")
    return _所有角色属性缓存[角色名称]


