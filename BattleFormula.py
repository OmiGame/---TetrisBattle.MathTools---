    @classmethod
    def _获取肉鸽技能加成(cls, 肉鸽技能名称: str) -> dict:
        """获取单个肉鸽技能的加成数据"""
        global _所有肉鸽技能数据缓存
        if not _所有肉鸽技能数据缓存:
            _所有肉鸽技能数据缓存 = tables.Tb肉鸽技能数据表.getDataMap()
        if 肉鸽技能名称 not in _所有肉鸽技能数据缓存:
            raise ValueError(f"未知肉鸽技能名称: {肉鸽技能名称}")
        return _所有肉鸽技能数据缓存[肉鸽技能名称]

    @classmethod
    def _获取肉鸽技能总加成(cls, 肉鸽技能名称列表: list) -> dict:
        """计算多个肉鸽技能的总加成"""
        cls._验证肉鸽技能数量(肉鸽技能名称列表)
        总加成 = {
            "血量加成": 0.0,
            "攻击力加成": 0.0,
            "攻速加成": 0.0,
            "消除倍率": 1.0,
            "每块小兵增加期望值": 0.0
        }
        for 肉鸽技能名称 in 肉鸽技能名称列表:
            技能数据 = cls._获取肉鸽技能加成(肉鸽技能名称)
            总加成["血量加成"] += 技能数据.血量加成
            总加成["攻击力加成"] += 技能数据.攻击力加成
            总加成["攻速加成"] += 技能数据.攻速加成
            总加成["消除倍率"] *= 技能数据.消除倍率
            总加成["每块小兵增加期望值"] += 技能数据.每块小兵增加期望值
        return 总加成
    
    @classmethod
    def 获取平均消除倍率加成(cls) -> float:
        return cls._获取肉鸽技能总加成(全局参数.所有肉鸽技能名称列表)["消除倍率"]**(1/全局参数.肉鸽技能总数)
    
    @classmethod
    def 获取平均每块小兵增加期望值加成(cls) -> float:
        return cls._获取肉鸽技能总加成(全局参数.所有肉鸽技能名称列表)["每块小兵增加期望值"]/全局参数.肉鸽技能总数

    @classmethod
    def 计算选择一个肉鸽技能后的角色战力(cls, 角色名称: str, 角色等级: int, 肉鸽技能名称: str) -> float:
        技能数据 = cls._获取肉鸽技能加成(肉鸽技能名称)
        血量加成 = 1 + 技能数据.血量加成
        攻击加成 = (1 + 技能数据.攻击力加成) * (1 + 技能数据.攻速加成)
        
        基础血量战力 = cls.计算解锁技能的血量战力(角色名称, 角色等级)
        基础DPS战力 = cls.计算解锁技能的DPS战力(角色名称, 角色等级)
        基础血量特效战力 = cls.计算解锁技能的血量特效战力(角色名称, 角色等级)
        基础DPS特效战力 = cls.计算解锁技能的DPS特效战力(角色名称, 角色等级)
        
        return (基础血量战力 * 血量加成 + 
                基础DPS战力 * 攻击加成 + 
                基础血量特效战力 * 血量加成 + 
                基础DPS特效战力 * 攻击加成)

    @classmethod
    def 计算选择多个肉鸽技能后的角色战力(cls, 角色名称: str, 角色等级: int, 肉鸽技能名称列表: list) -> float:
        总加成 = cls._获取肉鸽技能总加成(肉鸽技能名称列表)
        血量加成 = 1 + 总加成["血量加成"]
        攻击加成 = (1 + 总加成["攻击力加成"]) * (1 + 总加成["攻速加成"])
        
        基础血量战力 = cls.计算解锁技能的血量战力(角色名称, 角色等级)
        基础DPS战力 = cls.计算解锁技能的DPS战力(角色名称, 角色等级)
        基础血量特效战力 = cls.计算解锁技能的血量特效战力(角色名称, 角色等级)
        基础DPS特效战力 = cls.计算解锁技能的DPS特效战力(角色名称, 角色等级)
        
        return (基础血量战力 * 血量加成 + 
                基础DPS战力 * 攻击加成 + 
                基础血量特效战力 * 血量加成 + 
                基础DPS特效战力 * 攻击加成)

    @classmethod
    def 计算选择多次平均肉鸽技能后的角色战力(cls, 角色名称: str, 角色等级: int, 选择次数: int) -> float:
        平均肉鸽技能 = cls.返回平均强度的肉鸽技能字典()
        血量加成 = 1 + 平均肉鸽技能["血量加成"] * 选择次数
        攻击加成 = (1 + 平均肉鸽技能["攻击力加成"] * 选择次数) * (1 + 平均肉鸽技能["攻速加成"] * 选择次数)
        
        基础血量战力 = cls.计算解锁技能的血量战力(角色名称, 角色等级)
        基础DPS战力 = cls.计算解锁技能的DPS战力(角色名称, 角色等级)
        基础血量特效战力 = cls.计算解锁技能的血量特效战力(角色名称, 角色等级)
        基础DPS特效战力 = cls.计算解锁技能的DPS特效战力(角色名称, 角色等级)
        
        return (基础血量战力 * 血量加成 + 
                基础DPS战力 * 攻击加成 + 
                基础血量特效战力 * 血量加成 + 
                基础DPS特效战力 * 攻击加成)

    @classmethod
    def 计算阵容选择多次平均肉鸽技能后的战力(cls, 角色阵容列表: list, 角色等级: int, 选择次数: int) -> float:
        return sum(cls.计算选择多次平均肉鸽技能后的角色战力(角色名称, 角色等级, 选择次数) 
                  for 角色名称 in 角色阵容列表)

    @classmethod
    def 计算角色阵容的初始战力(cls, 角色阵容字典: dict) -> float:
        return sum(cls.计算解锁技能的总战力(角色名称, 角色等级) 
                  for 角色名称, 角色等级 in 角色阵容字典.items())

