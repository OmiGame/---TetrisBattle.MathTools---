    # -------------------------------------
    @classmethod
    def 计算解锁技能的血量战力(cls,角色名称:str, _角色等级:int) -> float:
        global _所有角色技能数据缓存
        if not _所有角色技能数据缓存:
            _所有角色技能数据缓存 = tables.Tb角色技能数据表.getDataMap()
        if 角色名称 not in _所有角色技能数据缓存:
            raise ValueError(f"未知角色名称: {角色名称}")
        角色数据 = _所有角色技能数据缓存[角色名称]

        # 筛选已解锁的技能（等级 <= 当前角色等级）
        已解锁的技能倍率 = []
        for 角色等级 in 角色数据.各等级技能倍率:
            if 角色等级 <= _角色等级:
                已解锁的技能倍率.append(角色数据.各等级技能倍率[角色等级])
        if not 已解锁的技能倍率:
            raise ValueError(f"角色 {角色名称} 等级 {_角色等级} 没有解锁任何技能")

        # 计算各属性的总倍率（所有已解锁技能的对应倍率相乘）
        血量总倍率 = 1.0
        for 每个等级的技能倍率 in 已解锁的技能倍率:
            血量总倍率 *= 每个等级的技能倍率.血量倍率

        基础血量战力 = cls.计算基础血量战力(角色名称, _角色等级)
        解锁技能的血量战力 = 基础血量战力 * 血量总倍率
        # print(f"{角色名称} 的解锁技能的血量战力为：{解锁技能的血量战力}")
        return  解锁技能的血量战力

