    波次战力表 = tables.Tb阵容战力成长时间分布_2导

    初始最大战力波次数据字典 = max(波次战力表.getDataList(), key=lambda x: x.怪物等级和波次)
    初始最小战力波次数据字典 = min(波次战力表.getDataList(), key=lambda x: x.怪物等级和波次)

    最大战力波次 = 初始最大战力波次数据字典.每波怪物总战力 * 怪物设计理论值基本参数.最大战力系数
    最小战力波次 = 初始最小战力波次数据字典.每波怪物总战力 * 怪物设计理论值基本参数.最小战力系数

    单个怪物的最低战力 = 最小战力波次/怪物设计理论值基本参数.波次数量上限
    单个怪物的最高战力 = 最大战力波次/怪物设计理论值基本参数.波次数量下限

    普通怪的战力下限 = 单个怪物的最低战力
    普通怪的战力上限 = 单个怪物的最高战力/怪物设计理论值基本参数.精英怪对比普通怪

    精英怪的战力下限 = 单个怪物的最低战力 * 怪物设计理论值基本参数.精英怪对比普通怪
    精英怪的战力上限 = 单个怪物的最高战力

    血量战力价值 = 全局参数.血量战力价值
    DPS战力价值 = 全局参数.DPS战力价值

    @classmethod
    def 计算职业属性范围(cls, 职业: str, 品质: str) -> Dict[str, tuple]:
        """计算指定职业在给定战力下的属性范围
        
        Args:
            职业: 怪物的职业类型（字符串）
            品质: 怪物的品质类型（字符串）
            
        Returns:
            Dict[str, tuple]: 包含血量范围和DPS范围的字典
            - 血量范围: (最小血量, 最大血量)
            - DPS范围: (最小DPS, 最大DPS)
        """
        # 获取对应的战力上下限
        战力下限 = cls.精英怪的战力下限 if 品质 == "精英" else cls.普通怪的战力下限
        战力上限 = cls.精英怪的战力上限 if 品质 == "精英" else cls.普通怪的战力上限
            
        属性配置 = 怪物属性配置[(怪物职业(职业), 怪物品质(品质))]
        
        # 计算血量和DPS的战力范围
        血量战力下限 = 战力下限 * 属性配置.血量比例
        血量战力上限 = 战力上限 * 属性配置.血量比例
        DPS战力下限 = 战力下限 * 属性配置.DPS比例
        DPS战力上限 = 战力上限 * 属性配置.DPS比例
        
        # 转换为实际属性值
        血量范围 = (血量战力下限 / cls.血量战力价值, 血量战力上限 / cls.血量战力价值)
        DPS范围 = (DPS战力下限 / cls.DPS战力价值, DPS战力上限 / cls.DPS战力价值)
        
        return {
            "血量范围": 血量范围,
            "DPS范围": DPS范围
        }
    
    @classmethod
    def 生成怪物理论数据(cls, 
        怪物名称: str,
        职业: str,
        品质: str,
        攻击速度: float,
        攻击范围: float,
        移动速度: float,
        怪物编号: int,
        适配地图: List[地图类型],
        血量特效倍率: float = 1.0,
        DPS特效倍率: float = 1.0,
        对塔伤害: int = 1
    ) -> 怪物基础属性数据:
        """生成怪物从最低等级到最高等级的所有属性数据
        
        Args:
            怪物名称: 怪物的名称
            职业: 怪物的职业类型（字符串）
            品质: 怪物的品质类型（字符串）
            攻击速度: 基础攻击速度
            攻击范围: 基础攻击范围
            移动速度: 基础移动速度
            怪物编号: 怪物的唯一编号
            适配地图: 怪物适配的地图列表
            血量特效倍率: 血量特效的倍率系数，默认为1.0
            DPS特效倍率: DPS特效的倍率系数，默认为1.0
            对塔伤害: 对塔伤害系数，默认为1，取值范围0-5
            
        Returns:
            怪物基础属性数据: 包含怪物完整基础属性信息的对象
        """
        # 参数检查
        # 检查怪物名称不能与已设计怪物重复（改为警告而不是异常）
            
        # 检查怪物名称不能与已生成怪物重复
        # 已生成怪物名称列表 = 怪物数据保存器.获取所有已保存怪物名称()
        # if 怪物名称 in 已生成怪物名称列表:
        #     raise ValueError(f"怪物名称 '{怪物名称}' 已存在于已生成怪物列表中")
            
        # 检查职业和品质不能为None
        if 职业 is None:
            raise ValueError("怪物职业不能为None")
        if 品质 is None:
            raise ValueError("怪物品质不能为None")
            
        # 检查职业和品质是否有效
        try:
            怪物职业(职业)
        except ValueError:
            raise ValueError(f"无效的职业类型: {职业}")
            
        try:
            怪物品质(品质)
        except ValueError:
            raise ValueError(f"无效的品质类型: {品质}")
            
        # 检查攻击速度必须在0-5之间
        if not 0 <= 攻击速度 <= 5:
            raise ValueError(f"攻击速度必须在0到5之间，当前值: {攻击速度}")
            
        # 检查攻击范围必须在1-5之间(包括1和5)
        if not 1 <= 攻击范围 <= 5:
            raise ValueError(f"攻击范围必须在1到5之间，当前值: {攻击范围}")
            
        # 检查移动速度必须在0到4之间
        if not 0 <= 移动速度 <= 4:
            raise ValueError(f"移动速度必须在0到4之间，当前值: {移动速度}")
            
        # 检查对塔伤害必须在0到5之间
        if not 1 <= 对塔伤害 <= 5:
            raise ValueError(f"对塔伤害必须在1到5之间，当前值: {对塔伤害}")
            
        # 检查怪物编号不能与已有编号重复
        # 已有怪物编号 = [怪物.怪物编号 for 怪物 in 怪物数据保存器.获取所有怪物()]
        # if 怪物编号 in 已有怪物编号:
        #     raise ValueError(f"怪物编号 {怪物编号} 已被占用")
            
        属性数据字典 = {}
        攻击范围收益 = 战斗公式.计算攻击范围收益倍率(攻击范围) 
        
        # 获取该职业的属性范围
        属性范围 = cls.计算职业属性范围(职业, 品质)
        血量范围 = 属性范围["血量范围"]
        DPS范围 = 属性范围["DPS范围"]
        
        # 计算血量和攻击力的成长倍率
        血量成长倍率 = (血量范围[1] / 血量范围[0]) ** (1.0 / (怪物设计理论值基本参数.怪物最高等级 - 怪物设计理论值基本参数.怪度最低等级))
        # print(血量成长倍率)
        攻击力成长倍率 = (DPS范围[1] / DPS范围[0]) ** (1.0 / (怪物设计理论值基本参数.怪物最高等级 - 怪物设计理论值基本参数.怪度最低等级))
        # print(攻击力成长倍率)
        # 计算每个等级的属性
        for 等级 in range(怪物设计理论值基本参数.怪度最低等级, 怪物设计理论值基本参数.怪物最高等级 + 1):
            # 计算当前等级的血量和DPS（使用线性插值）
            等级进度 = (等级 - 怪物设计理论值基本参数.怪度最低等级) / (怪物设计理论值基本参数.怪物最高等级 - 怪物设计理论值基本参数.怪度最低等级)
            当前血量 = 血量范围[0] * ((血量范围[1]/血量范围[0]) ** 等级进度)
            当前DPS = DPS范围[0] * ((DPS范围[1]/DPS范围[0]) ** 等级进度)
            
            # 应用特效折扣
            当前血量 /= (1+血量特效倍率)
            当前DPS /= (1+DPS特效倍率)      
            # 计算攻击力（DPS/（攻击速度*攻击范围收益）
            攻击力 = 当前DPS / (攻击速度 * 攻击范围收益)

            战力 = 当前血量 * (1+血量特效倍率) * cls.血量战力价值 + 当前DPS * (1+DPS特效倍率) * cls.DPS战力价值
            
            属性数据字典[等级] = {
                "血量": 当前血量,
                "攻击力": 攻击力,
                "攻击速度": 攻击速度,
                "攻击范围": 攻击范围,
                "移动速度": 移动速度,
                "DPS": 当前DPS,
                "战力": 战力,
                "血量特效倍率": 血量特效倍率,
                "DPS特效倍率": DPS特效倍率
            }
            
        return 怪物基础属性数据(
            名称=怪物名称,
            职业=怪物职业(职业),
            品质=怪物品质(品质),
            怪物编号=怪物编号,
            适配地图=适配地图,
            各等级属性数据=属性数据字典,
            基础攻击速度=攻击速度,
            基础攻击范围=攻击范围,
            基础移动速度=移动速度,
            血量特效倍率=血量特效倍率,
            DPS特效倍率=DPS特效倍率,
            血量成长倍率=血量成长倍率,
            攻击力成长倍率=攻击力成长倍率,
            对塔伤害=对塔伤害
        )
        

    @classmethod
    def 生成所有理论数据(cls) -> List[怪物基础属性数据]:
        所有怪物数据 = []
        for 怪物 in 怪物设计表.getDataList():
            怪物数据 = cls.生成怪物理论数据(
                怪物名称=怪物.名称,
                职业=怪物.职业,
                品质=怪物.品质,
                攻击速度=怪物.攻速,
                攻击范围=怪物.攻击范围,
                移动速度=怪物.移动速度,
                怪物编号=怪物.怪物编号,
                适配地图=怪物.适配地图.split(','),
                血量特效倍率=怪物.血量特效倍率,
                DPS特效倍率=怪物.DPS特效倍率,
                对塔伤害=怪物.对塔伤害
            )
            所有怪物数据.append(怪物数据)
        return 所有怪物数据

    @classmethod
    def 生成所有怪物实际数据(cls) -> List[怪物基础属性数据]:
        所有怪物数据 = []
        for 怪物 in 怪物设计表.getDataList():
            属性数据字典 = {}
            # 计算每个等级的属性
            for 等级 in range(怪物设计理论值基本参数.怪度最低等级, 怪物设计理论值基本参数.怪物最高等级 + 1):
                # 计算当前等级的血量和DPS（使用线性插值）
                当前血量 = 怪物.血量*(怪物.血量成长倍率**(等级-1))
                当前攻击力 = 怪物.攻击力*(怪物.攻击力成长倍率**(等级-1))

                当前DPS = 当前攻击力* 怪物.攻速 * 战斗公式.计算攻击范围收益倍率(怪物.攻击范围)

                战力 = 当前血量 * (1+怪物.血量特效倍率) * cls.血量战力价值 + 当前DPS * (1+怪物.DPS特效倍率) * cls.DPS战力价值
                
                属性数据字典[等级] = {
                    "血量": 当前血量,
                    "攻击力": 当前攻击力,
                    "攻击速度": 怪物.攻速,
                    "攻击范围": 怪物.攻击范围,
                    "移动速度": 怪物.移动速度,
                    "DPS": 当前DPS,
                    "战力": 战力,
                    "血量特效倍率": 怪物.血量特效倍率,
                    "DPS特效倍率": 怪物.DPS特效倍率
                }

            怪物数据 = 怪物基础属性数据(
                怪物编号=怪物.怪物编号,
                名称=怪物.名称,
                职业=怪物.职业,
                品质=怪物.品质,
                适配地图=怪物.适配地图.split(','),
                各等级属性数据=属性数据字典,
                基础攻击速度=怪物.攻速,
                基础攻击范围=怪物.攻击范围,
                基础移动速度=怪物.移动速度,
                血量特效倍率=怪物.血量特效倍率,
                DPS特效倍率=怪物.DPS特效倍率,
                血量成长倍率=怪物.血量成长倍率,
                攻击力成长倍率=怪物.攻击力成长倍率,
                对塔伤害=怪物.对塔伤害
            )
            所有怪物数据.append(怪物数据)
        return 所有怪物数据


