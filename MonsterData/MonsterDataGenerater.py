class 计算怪物属性:
    波次战力表 = tables.Tb阵容战力成长时间分布_2导

    初始最大战力波次数据字典 = max(波次战力表.getDataList(), key=lambda x: x.怪物等级和波次)
    初始最小战力波次数据字典 = min(波次战力表.getDataList(), key=lambda x: x.怪物等级和波次)


    最大战力波次 = 初始最大战力波次数据字典.每波怪物总战力 * 怪物设计基本参数.最大战力系数
    最小战力波次 = 初始最小战力波次数据字典.每波怪物总战力 * 怪物设计基本参数.最小战力系数

    单个怪物的最低战力 = 最小战力波次/怪物设计基本参数.波次数量上限
    单个怪物的最高战力 = 最大战力波次/怪物设计基本参数.波次数量下限

    普通怪的战力下限 = 单个怪物的最低战力
    普通怪的战力上限 = 单个怪物的最高战力/怪物设计基本参数.精英怪对比普通怪

    精英怪的战力下限 = 单个怪物的最低战力 * 怪物设计基本参数.精英怪对比普通怪
    精英怪的战力上限 = 单个怪物的最高战力

    血量战力价值 = 全局参数.血量战力价值
    DPS战力价值 = 全局参数.DPS战力价值

    @classmethod
    def 计算职业属性范围(cls, 职业: 怪物职业) -> Dict[str, tuple]:
        """计算指定职业在给定战力下的属性范围
        
        Returns:
            Dict[str, tuple]: 包含血量范围和DPS范围的字典
            - 血量范围: (最小血量, 最大血量)
            - DPS范围: (最小DPS, 最大DPS)
        """
        # 判断是否为精英职业
        是精英职业 = "精英" in 职业.value
        
        # 获取对应的战力上下限
        战力下限 = cls.精英怪的战力下限 if 是精英职业 else cls.普通怪的战力下限
        战力上限 = cls.精英怪的战力上限 if 是精英职业 else cls.普通怪的战力上限
            
        属性配置 = 怪物属性配置[职业]
        
        # 计算血量和DPS的战力范围
        血量战力下限 = 战力下限 * 属性配置.血量比例
        血量战力上限 = 战力上限 * 属性配置.血量比例
        DPS战力下限 = 战力下限 * 属性配置.DPS比例
        DPS战力上限 = 战力上限 * 属性配置.DPS比例
        
        # 转换为实际属性值
        血量范围 = (血量战力下限 / cls.血量战力价值, 血量战力上限 / cls.血量战力价值)
        DPS范围 = (DPS战力下限 / cls.DPS战力价值, DPS战力上限 / cls.DPS战力价值)
        
        return {
            "血量范围": 血量范围,
            "DPS范围": DPS范围
        }

    @classmethod
    def 生成怪物基础属性数据(cls, 
        怪物名称: str,
        职业: 怪物职业,
        攻击速度: float,
        攻击范围: float,
        移动速度: float,
        血量特效倍率: float = 1.0,
        DPS特效倍率: float = 1.0
    ) -> 怪物基础属性数据:
        """生成怪物从最低等级到最高等级的所有属性数据
        
        Args:
            怪物名称: 怪物的名称
            职业: 怪物的职业类型
            攻击速度: 基础攻击速度
            攻击范围: 基础攻击范围
            移动速度: 基础移动速度
            血量特效倍率: 血量特效的倍率系数，默认为1.0
            DPS特效倍率: DPS特效的倍率系数，默认为1.0
            
        Returns:
            怪物基础属性数据: 包含怪物完整基础属性信息的对象
        """
        属性数据字典 = {}
        攻击范围收益 = 战斗公式.计算攻击范围收益倍率(攻击范围) 
        print(f"攻击范围收益为 ： {攻击范围收益}")
        
        # 获取该职业的属性范围
        属性范围 = cls.计算职业属性范围(职业)
        血量范围 = 属性范围["血量范围"]
        DPS范围 = 属性范围["DPS范围"]
        
        # 计算每个等级的属性
        for 等级 in range(怪物设计基本参数.怪度最低等级, 怪物设计基本参数.怪物最高等级 + 1):
            # 计算当前等级的血量和DPS（使用线性插值）
            等级进度 = (等级 - 怪物设计基本参数.怪度最低等级) / (怪物设计基本参数.怪物最高等级 - 怪物设计基本参数.怪度最低等级)
            当前血量 = 血量范围[0] * ((血量范围[1]/血量范围[0]) ** 等级进度)
            当前DPS = DPS范围[0] * ((DPS范围[1]/DPS范围[0]) ** 等级进度)
            
            # 应用特效折扣
            当前血量 /= (1+血量特效倍率)
            当前DPS /= (1+DPS特效倍率)      
            # 计算攻击力（DPS/（攻击速度*攻击范围收益）
            攻击力 = 当前DPS / (攻击速度 * 攻击范围收益)

            战力 = 当前血量 * (1+血量特效倍率) * cls.血量战力价值 + 当前DPS * (1+DPS特效倍率) * cls.DPS战力价值
            
            属性数据字典[等级] = {
                "血量": 当前血量,
                "攻击力": 攻击力,
                "攻击速度": 攻击速度,
                "攻击范围": 攻击范围,
                "移动速度": 移动速度,
                "DPS": 当前DPS,
                "战力": 战力,
                "血量特效倍率": 血量特效倍率,
                "DPS特效倍率": DPS特效倍率
            }
            
        return 怪物基础属性数据(
            名称=怪物名称,
            职业=职业,
            各等级属性数据=属性数据字典,
            基础攻击速度=攻击速度,
            基础攻击范围=攻击范围,
            基础移动速度=移动速度,
            血量特效倍率=血量特效倍率,
            DPS特效倍率=DPS特效倍率
        )

