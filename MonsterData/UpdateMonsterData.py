"""更新怪物数据示例脚本

此脚本演示了如何使用覆盖已有怪物数据功能，允许你修改已经存在的怪物数据，并直接保存到json中。
"""
from MonsterData.MonsterTypes import (
    怪物职业,
    怪物品质,
    已设计怪物,
    地图类型
)
from MonsterData.MonsterDataGenerater import 计算怪物属性
from MonsterData.MonsterDataManager import 怪物数据保存器

def 打印怪物基础信息(怪物名称):
    """打印指定怪物的基本信息"""
    try:
        怪物 = 怪物数据保存器.获取怪物(怪物名称)
        print(f"\n怪物 '{怪物.名称}' 的基本信息:")
        print(f"职业: {怪物.职业.value}")
        print(f"品质: {怪物.品质.value}")
        print(f"怪物编号: {怪物.怪物编号}")
        print(f"适配地图: {[地图.value for 地图 in 怪物.适配地图]}")
        print(f"基础攻击速度: {怪物.基础攻击速度}")
        print(f"基础攻击范围: {怪物.基础攻击范围}")
        print(f"基础移动速度: {怪物.基础移动速度}")
        print(f"血量特效倍率: {怪物.血量特效倍率}")
        print(f"DPS特效倍率: {怪物.DPS特效倍率}")
        print(f"对塔伤害: {怪物.对塔伤害}")
        
        # 打印第一个等级的数据作为示例
        等级 = list(怪物.各等级属性数据.keys())[0]
        数据 = 怪物.各等级属性数据[等级]
        print(f"\n等级 {等级} 的属性:")
        print(f"血量: {数据['血量']:.2f}")
        print(f"攻击力: {数据['攻击力']:.2f}")
        print(f"DPS: {数据['DPS']:.2f}")
        print(f"战力: {数据['战力']:.2f}")
    except KeyError:
        print(f"找不到名为 '{怪物名称}' 的怪物数据")

def 示例_更新怪物数据():
    """示例如何使用覆盖已有怪物数据功能更新怪物"""
    # 首先显示当前怪物数据
    目标怪物名称 = 已设计怪物.普通坦克.value
    print(f"更新前的怪物数据:")
    打印怪物基础信息(目标怪物名称)
    
    # 使用重新生成怪物数据方法更新怪物
    print(f"\n正在更新怪物 '{目标怪物名称}'...")
    更新的怪物 = 计算怪物属性.重新生成怪物数据(
        怪物名称=目标怪物名称,
        职业=怪物职业.坦克,
        品质=怪物品质.普通,
        攻击速度=1.2,  # 修改攻击速度
        攻击范围=1.3,  # 修改攻击范围
        移动速度=1.3,  # 修改移动速度
        怪物编号=10011,  # 保持相同的编号
        适配地图=[地图类型.草原, 地图类型.森林, 地图类型.火山],  # 修改适配地图
        血量特效倍率=0.3,  # 修改血量特效倍率
        DPS特效倍率=0.1,  # 修改DPS特效倍率
        对塔伤害=3  # 修改对塔伤害
    )
    
    # 显示更新后的怪物数据
    print(f"\n更新后的怪物数据:")
    打印怪物基础信息(目标怪物名称)

def 示例_批量更新怪物数据():
    """示例如何批量更新多个怪物的数据"""
    # 定义要更新的怪物列表
    怪物列表 = [
        {
            "名称": 已设计怪物.残酷射手.value,
            "职业": 怪物职业.远程,
            "品质": 怪物品质.普通,
            "攻击速度": 1.1,
            "攻击范围": 3.2,
            "移动速度": 1.3,
            "怪物编号": 10021,
            "适配地图": [地图类型.雪山, 地图类型.草原, 地图类型.森林],
            "血量特效倍率": 0.1,
            "DPS特效倍率": 0.4,
            "对塔伤害": 1
        },
        {
            "名称": 已设计怪物.泰森.value,
            "职业": 怪物职业.近战,
            "品质": 怪物品质.普通,
            "攻击速度": 1.2,
            "攻击范围": 1.1,
            "移动速度": 1.2,
            "怪物编号": 10031,
            "适配地图": [地图类型.火山, 地图类型.森林, 地图类型.草原],
            "血量特效倍率": 0.1,
            "DPS特效倍率": 0.1,
            "对塔伤害": 2
        }
    ]
    
    print("\n开始批量更新怪物数据...")
    for 怪物参数 in 怪物列表:
        打印怪物基础信息(怪物参数["名称"])
        
        计算怪物属性.重新生成怪物数据(**怪物参数)
        
        print(f"\n已更新怪物 '{怪物参数['名称']}'")
        打印怪物基础信息(怪物参数["名称"])
        print("\n" + "-" * 50)

if __name__ == "__main__":
    # 先加载所有现有的怪物数据
    怪物数据保存器()
    
    print("已加载的怪物列表:", 怪物数据保存器.获取所有已保存怪物名称())
    
    # 示例1：更新单个怪物数据
    示例_更新怪物数据()
    
    # 示例2：批量更新多个怪物数据
    示例_批量更新怪物数据()
    
    print("\n所有怪物更新完成!") 